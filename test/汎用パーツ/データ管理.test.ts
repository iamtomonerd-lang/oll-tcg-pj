import { describe, it, expect, beforeEach } from '@jest/globals';
import {
  キーが存在するか,
  値を取得,
  複数を取得,
  キーたちを取得,
  サイズを取得,
  が空か,
  深くコピー,
  キーをフィルタリング,
  データ管理
} from '../../src/汎用パーツ/データ管理/index';

describe('データ管理 - ユーティリティ関数', () => {
  const サンプルデータ = {
    名前: 'テスト',
    値: 42,
    フラグ: true
  };

  describe('キーが存在するか', () => {
    it('存在するキーを確認', () => {
      expect(キーが存在するか(サンプルデータ, '名前')).toBe(true);
    });

    it('存在しないキーを確認', () => {
      expect(キーが存在するか(サンプルデータ, '存在しない')).toBe(false);
    });
  });

  describe('値を取得', () => {
    it('存在する値を取得', () => {
      expect(値を取得(サンプルデータ, '値')).toBe(42);
    });

    it('存在しない値はundefinedを返す', () => {
      expect(値を取得(サンプルデータ, '存在しない')).toBeUndefined();
    });

    it('デフォルト値を使用できる', () => {
      expect(値を取得(サンプルデータ, '存在しない', '既定値')).toBe('既定値');
    });

    it('型ジェネリクスで型指定できる', () => {
      const 値 = 値を取得<number>(サンプルデータ, '値');
      expect(値).toBe(42);
    });
  });

  describe('複数を取得', () => {
    it('複数のキーから値を取得', () => {
      const 結果 = 複数を取得(サンプルデータ, ['名前', '値']);
      expect(結果).toEqual({ 名前: 'テスト', 値: 42 });
    });

    it('存在しないキーは含めない', () => {
      const 結果 = 複数を取得(サンプルデータ, ['名前', '存在しない']);
      expect(結果).toEqual({ 名前: 'テスト' });
    });

    it('空の配列で空のオブジェクトを返す', () => {
      const 結果 = 複数を取得(サンプルデータ, []);
      expect(結果).toEqual({});
    });
  });

  describe('キーたちを取得', () => {
    it('すべてのキーを取得', () => {
      const キー = キーたちを取得(サンプルデータ);
      expect(キー).toEqual(expect.arrayContaining(['名前', 'フラグ', '値']));
      expect(キー).toHaveLength(3);
    });

    it('空のオブジェクトで空の配列を返す', () => {
      const キー = キーたちを取得({});
      expect(キー).toEqual([]);
    });
  });

  describe('サイズを取得', () => {
    it('キーの数を返す', () => {
      expect(サイズを取得(サンプルデータ)).toBe(3);
    });

    it('空のオブジェクトで0を返す', () => {
      expect(サイズを取得({})).toBe(0);
    });
  });

  describe('が空か', () => {
    it('空のオブジェクトでtrueを返す', () => {
      expect(が空か({})).toBe(true);
    });

    it('要素があるオブジェクトでfalseを返す', () => {
      expect(が空か(サンプルデータ)).toBe(false);
    });
  });

  describe('深くコピー', () => {
    it('オブジェクトを深くコピーできる', () => {
      const コピー = 深くコピー(サンプルデータ);
      expect(コピー).toEqual(サンプルデータ);
      expect(コピー).not.toBe(サンプルデータ);
    });

    it('ネストされたオブジェクトもコピーされる', () => {
      const ネストデータ = {
        外側: {
          内側: { 値: 42 }
        }
      };
      const コピー = 深くコピー(ネストデータ);
      (コピー.外側 as any).内側.値 = 99;
      expect((ネストデータ.外側 as any).内側.値).toBe(42);
    });
  });

  describe('キーをフィルタリング', () => {
    it('条件に合致するキーをフィルタリング', () => {
      const 結果 = キーをフィルタリング(
        サンプルデータ,
        (_キー, 値) => typeof 値 === 'number'
      );
      expect(結果).toEqual(['値']);
    });

    it('すべてがフィルタリングされるケース', () => {
      const 結果 = キーをフィルタリング(サンプルデータ, () => false);
      expect(結果).toEqual([]);
    });
  });
});

describe('データ管理 - 管理クラス', () => {
  let 管理: データ管理;

  beforeEach(() => {
    管理 = new データ管理({ 初期: 'データ', 値: 10 });
  });

  describe('取得・参照', () => {
    it('現在のデータを取得できる', () => {
      const データ = 管理.取得();
      expect(データ).toEqual({ 初期: 'データ', 値: 10 });
    });

    it('取得したデータは独立したコピー', () => {
      const データ = 管理.取得();
      (データ as any).値 = 999;
      const 再取得 = 管理.取得();
      expect(再取得.値).toBe(10);
    });

    it('キーが存在するか確認', () => {
      expect(管理.キーが存在するか('初期')).toBe(true);
      expect(管理.キーが存在するか('存在しない')).toBe(false);
    });

    it('すべてのキーを取得', () => {
      const キー = 管理.キーたちを取得();
      expect(キー).toEqual(expect.arrayContaining(['初期', '値']));
      expect(キー).toHaveLength(2);
    });

    it('データのサイズを取得', () => {
      expect(管理.サイズを取得()).toBe(2);
    });

    it('データが空かを確認', () => {
      expect(管理.が空か()).toBe(false);
      const 空 = new データ管理();
      expect(空.が空か()).toBe(true);
    });
  });

  describe('イミュータビリティ', () => {
    it('値の設定は新しい管理インスタンスを返す', () => {
      const 新しい管理 = 管理.値を設定('新しいキー', 'あたらしい値');
      expect(管理).not.toBe(新しい管理);
      expect(管理.キーが存在するか('新しいキー')).toBe(false);
      expect(新しい管理.キーが存在するか('新しいキー')).toBe(true);
    });

    it('削除は新しい管理インスタンスを返す', () => {
      const 新しい管理 = 管理.削除('初期');
      expect(管理).not.toBe(新しい管理);
      expect(管理.キーが存在するか('初期')).toBe(true);
      expect(新しい管理.キーが存在するか('初期')).toBe(false);
    });

    it('クリアは新しい管理インスタンスを返す', () => {
      const 新しい管理 = 管理.クリア();
      expect(管理).not.toBe(新しい管理);
      expect(管理.サイズを取得()).toBe(2);
      expect(新しい管理.サイズを取得()).toBe(0);
    });
  });

  describe('値の操作', () => {
    it('値を設定できる', () => {
      const 新しい管理 = 管理.値を設定('新キー', 'あたらしい');
      expect(新しい管理.取得()['新キー']).toBe('あたらしい');
    });

    it('既存のキーを上書きできる', () => {
      const 新しい管理 = 管理.値を設定('値', 999);
      expect(新しい管理.取得().値).toBe(999);
    });

    it('複数の値を設定できる', () => {
      const 新しい管理 = 管理.複数を設定({
        a: 1,
        b: 2,
        c: 3
      });
      expect(新しい管理.取得()).toEqual({
        初期: 'データ',
        値: 10,
        a: 1,
        b: 2,
        c: 3
      });
    });

    it('キーを削除できる', () => {
      const 新しい管理 = 管理.削除('初期');
      expect(新しい管理.キーが存在するか('初期')).toBe(false);
      expect(新しい管理.キーが存在するか('値')).toBe(true);
    });

    it('存在しないキーを削除しても元の管理を返す', () => {
      const 新しい管理 = 管理.削除('存在しないキー');
      expect(新しい管理).toBe(管理);
    });

    it('複数のキーを削除できる', () => {
      const 新しい管理 = 管理.複数削除(['初期', '値']);
      expect(新しい管理.サイズを取得()).toBe(0);
    });

    it('すべてのデータをクリアできる', () => {
      const 新しい管理 = 管理.クリア();
      expect(新しい管理.が空か()).toBe(true);
    });
  });

  describe('リスナー機能', () => {
    it('値が設定されるとリスナーが呼ばれる', () => {
      const リスナー = jest.fn();
      管理.リスナーを登録(リスナー);

      管理.値を設定('新キー', 'あたらしい');

      expect(リスナー).toHaveBeenCalledTimes(1);
      const 変更 = リスナー.mock.calls[0][0];
      expect(変更.キー).toBe('新キー');
      expect(変更.変更後).toBe('あたらしい');
    });

    it('複数のリスナーが登録できる', () => {
      const リスナー1 = jest.fn();
      const リスナー2 = jest.fn();

      管理.リスナーを登録(リスナー1);
      管理.リスナーを登録(リスナー2);

      管理.値を設定('キー', '値');

      expect(リスナー1).toHaveBeenCalledTimes(1);
      expect(リスナー2).toHaveBeenCalledTimes(1);
    });

    it('削除時もリスナーが呼ばれる', () => {
      const リスナー = jest.fn();
      管理.リスナーを登録(リスナー);

      管理.削除('初期');

      expect(リスナー).toHaveBeenCalledTimes(1);
      const 変更 = リスナー.mock.calls[0][0];
      expect(変更.キー).toBe('初期');
      expect(変更.変更後).toBeUndefined();
    });

    it('リスナーを削除できる', () => {
      const リスナー = jest.fn();
      管理.リスナーを登録(リスナー);

      管理.値を設定('キー1', '値1');
      expect(リスナー).toHaveBeenCalledTimes(1);

      管理.リスナーを削除(リスナー);
      管理.値を設定('キー2', '値2');

      expect(リスナー).toHaveBeenCalledTimes(1);
    });

    it('イミュータブル操作後も新しい管理にリスナーが引き継がれる', () => {
      const リスナー = jest.fn();
      管理.リスナーを登録(リスナー);

      const 新しい管理 = 管理.値を設定('キー1', '値1');
      新しい管理.値を設定('キー2', '値2');

      expect(リスナー).toHaveBeenCalledTimes(2);
    });
  });

  describe('バリデーション', () => {
    it('バリデーターを設定できる', () => {
      管理.バリデーターを設定((キー, 値) => {
        if (typeof 値 !== 'number') {
          return { 有効: false, エラー: '数値のみ許可' };
        }
        return { 有効: true };
      });

      expect(() => {
        管理.値を設定('キー', 'テキスト');
      }).toThrow('バリデーション失敗');
    });

    it('バリデーション成功時は値が設定される', () => {
      管理.バリデーターを設定((キー, 値) => {
        if (typeof 値 !== 'number') {
          return { 有効: false, エラー: '数値のみ許可' };
        }
        return { 有効: true };
      });

      const 新しい管理 = 管理.値を設定('数値キー', 42);
      expect(新しい管理.取得()['数値キー']).toBe(42);
    });
  });

  describe('マージ操作', () => {
    it('別のデータをマージできる', () => {
      const マージデータ = { 新キー: 'あたらしい', 値: 999 };
      const 新しい管理 = 管理.マージ(マージデータ);

      const データ = 新しい管理.取得();
      expect(データ.初期).toBe('データ');
      expect(データ.値).toBe(999);
      expect(データ['新キー']).toBe('あたらしい');
    });

    it('マージは新しい管理インスタンスを返す', () => {
      const 新しい管理 = 管理.マージ({ キー: '値' });
      expect(管理).not.toBe(新しい管理);
      expect(管理.キーが存在するか('キー')).toBe(false);
    });
  });

  describe('フィルタリング', () => {
    it('条件に合致するデータをフィルタリング', () => {
      const 拡張管理 = 管理
        .値を設定('a', 1)
        .値を設定('b', 2)
        .値を設定('c', 'テキスト');

      const フィルタ = 拡張管理.フィルタリング(
        (_キー, 値) => typeof 値 === 'number'
      );

      const データ = フィルタ.取得();
      expect(データ).toEqual(expect.objectContaining({ a: 1, b: 2, 値: 10 }));
      expect(データ.c).toBeUndefined();
      expect(データ.初期).toBeUndefined();
    });

    it('フィルタリングは新しい管理インスタンスを返す', () => {
      const フィルタ = 管理.フィルタリング(() => true);
      expect(管理).not.toBe(フィルタ);
    });
  });

  describe('実践的なシナリオ', () => {
    it('プレイヤー情報を管理できる', () => {
      const プレイヤー = new データ管理({
        名前: 'Hero',
        HP: 100,
        MP: 50
      });

      const ダメージ後 = プレイヤー
        .値を設定('HP', 80)
        .値を設定('MP', 40);

      expect(ダメージ後.取得()).toEqual({
        名前: 'Hero',
        HP: 80,
        MP: 40
      });
    });

    it('複数のデータセットを独立して管理', () => {
      const システムA = new データ管理({ x: 1, y: 2 });
      const システムB = new データ管理({ a: 10, b: 20 });

      const 変更A = システムA.値を設定('x', 999);
      const 変更B = システムB.値を設定('a', 888);

      expect(変更A.取得().x).toBe(999);
      expect(変更B.取得().a).toBe(888);
      expect(システムA.取得().x).toBe(1);
      expect(システムB.取得().a).toBe(10);
    });

    it('チェーン操作で複数変更', () => {
      const 最終状態 = 管理
        .値を設定('a', 1)
        .値を設定('b', 2)
        .値を設定('c', 3)
        .削除('初期');

      const データ = 最終状態.取得();
      expect(データ).toEqual({
        値: 10,
        a: 1,
        b: 2,
        c: 3
      });
    });
  });
});
