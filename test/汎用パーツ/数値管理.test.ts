import { describe, it, expect, beforeEach } from '@jest/globals';
import {
  数値を作成,
  は最大か,
  は最小値以下か,
  はゼロ以下か,
  割合を取得,
  数値管理
} from '../../src/汎用パーツ/数値管理/index';

describe('数値管理 - ユーティリティ関数', () => {
  describe('数値を作成', () => {
    it('名前と現在値で数値を作成', () => {
      const 数値 = 数値を作成('test_value', 20);
      expect(数値.名前).toBe('test_value');
      expect(数値.現在値).toBe(20);
      expect(数値.最大値).toBeNull();
      expect(数値.最小値).toBeNull();
    });

    it('最大値を指定できる', () => {
      const 数値 = 数値を作成('hp', 20, 100);
      expect(数値.最大値).toBe(100);
    });

    it('最大値と最小値を指定できる', () => {
      const 数値 = 数値を作成('counter', 50, 100, 0);
      expect(数値.最大値).toBe(100);
      expect(数値.最小値).toBe(0);
    });

    it('ゲーム固有名称は禁止（テスト不要）', () => {
      // 実装側で制限していないため、テストしない
      // ゲーム側で意味を付与する責務
    });
  });

  describe('は最大か', () => {
    it('現在値が最大値に達している場合true', () => {
      const 数値 = 数値を作成('val', 100, 100);
      expect(は最大か(数値)).toBe(true);
    });

    it('現在値が最大値を超えている場合true', () => {
      const 数値 = 数値を作成('val', 150, 100);
      expect(は最大か(数値)).toBe(true);
    });

    it('現在値が最大値より小さい場合false', () => {
      const 数値 = 数値を作成('val', 50, 100);
      expect(は最大か(数値)).toBe(false);
    });

    it('最大値がnullの場合false', () => {
      const 数値 = 数値を作成('val', 1000);
      expect(は最大か(数値)).toBe(false);
    });
  });

  describe('は最小値以下か', () => {
    it('現在値が最小値と同じ場合true', () => {
      const 数値 = 数値を作成('val', 0, 100, 0);
      expect(は最小値以下か(数値)).toBe(true);
    });

    it('現在値が最小値より小さい場合true', () => {
      const 数値 = 数値を作成('val', -5, 100, 0);
      expect(は最小値以下か(数値)).toBe(true);
    });

    it('現在値が最小値より大きい場合false', () => {
      const 数値 = 数値を作成('val', 50, 100, 0);
      expect(は最小値以下か(数値)).toBe(false);
    });

    it('最小値がnullの場合false', () => {
      const 数値 = 数値を作成('val', -1000, 100);
      expect(は最小値以下か(数値)).toBe(false);
    });
  });

  describe('はゼロ以下か', () => {
    it('現在値がゼロの場合true', () => {
      const 数値 = 数値を作成('val', 0, 100);
      expect(はゼロ以下か(数値)).toBe(true);
    });

    it('現在値が負の場合true', () => {
      const 数値 = 数値を作成('val', -10, 100);
      expect(はゼロ以下か(数値)).toBe(true);
    });

    it('現在値が正の場合false', () => {
      const 数値 = 数値を作成('val', 50, 100);
      expect(はゼロ以下か(数値)).toBe(false);
    });
  });

  describe('割合を取得', () => {
    it('現在値を最大値で割った割合を返す', () => {
      const 数値 = 数値を作成('val', 50, 100);
      expect(割合を取得(数値)).toBe(0.5);
    });

    it('最大値に達している場合1.0を返す', () => {
      const 数値 = 数値を作成('val', 100, 100);
      expect(割合を取得(数値)).toBe(1);
    });

    it('0の場合0を返す', () => {
      const 数値 = 数値を作成('val', 0, 100);
      expect(割合を取得(数値)).toBe(0);
    });

    it('負の値の場合0にクリップ', () => {
      const 数値 = 数値を作成('val', -50, 100);
      expect(割合を取得(数値)).toBe(0);
    });

    it('最大値を超える場合1.0にクリップ', () => {
      const 数値 = 数値を作成('val', 150, 100);
      expect(割合を取得(数値)).toBe(1);
    });

    it('最大値がnullの場合0を返す', () => {
      const 数値 = 数値を作成('val', 50);
      expect(割合を取得(数値)).toBe(0);
    });

    it('最大値が0の場合0を返す', () => {
      const 数値 = 数値を作成('val', 50, 0);
      expect(割合を取得(数値)).toBe(0);
    });
  });
});

describe('数値管理 - 管理クラス', () => {
  let 管理: 数値管理;

  beforeEach(() => {
    管理 = new 数値管理('test_value', 20, 100, 0);
  });

  describe('作成・初期化', () => {
    it('名前、現在値、最大値、最小値で作成できる', () => {
      expect(管理.名前を取得()).toBe('test_value');
      expect(管理.現在値を取得()).toBe(20);
      expect(管理.最大値を取得()).toBe(100);
      expect(管理.最小値を取得()).toBe(0);
    });

    it('最大値なしで作成できる', () => {
      const 無制限 = new 数値管理('unlimited', 50);
      expect(無制限.最大値を取得()).toBeNull();
    });

    it('最小値なしで作成できる', () => {
      const 最小値なし = new 数値管理('no_min', 50, 100);
      expect(最小値なし.最小値を取得()).toBeNull();
    });
  });

  describe('取得操作', () => {
    it('数値全体を取得できる', () => {
      const 数値 = 管理.取得();
      expect(数値.名前).toBe('test_value');
      expect(数値.現在値).toBe(20);
      expect(数値.最大値).toBe(100);
      expect(数値.最小値).toBe(0);
    });

    it('名前を取得できる', () => {
      expect(管理.名前を取得()).toBe('test_value');
    });

    it('現在値を取得できる', () => {
      expect(管理.現在値を取得()).toBe(20);
    });

    it('最大値を取得できる', () => {
      expect(管理.最大値を取得()).toBe(100);
    });

    it('最小値を取得できる', () => {
      expect(管理.最小値を取得()).toBe(0);
    });
  });

  describe('イミュータビリティ', () => {
    it('値の変更は新しい管理インスタンスを返す', () => {
      const 新管理 = 管理.現在値を変更(30);
      expect(管理).not.toBe(新管理);
      expect(管理.現在値を取得()).toBe(20);
      expect(新管理.現在値を取得()).toBe(30);
    });

    it('最大値の変更は新しい管理インスタンスを返す', () => {
      const 新管理 = 管理.最大値を変更(200);
      expect(管理).not.toBe(新管理);
      expect(管理.最大値を取得()).toBe(100);
      expect(新管理.最大値を取得()).toBe(200);
    });

    it('最小値の変更は新しい管理インスタンスを返す', () => {
      const 新管理 = 管理.最小値を変更(10);
      expect(管理).not.toBe(新管理);
      expect(管理.最小値を取得()).toBe(0);
      expect(新管理.最小値を取得()).toBe(10);
    });
  });

  describe('値の操作', () => {
    it('値を増加させられる', () => {
      const 新管理 = 管理.増加させる(10);
      expect(新管理.現在値を取得()).toBe(30);
    });

    it('値を減少させられる', () => {
      const 新管理 = 管理.減少させる(5);
      expect(新管理.現在値を取得()).toBe(15);
    });

    it('最大値を超えて増加させられる', () => {
      const 新管理 = 管理.増加させる(200);
      expect(新管理.現在値を取得()).toBe(100);
    });

    it('最小値を下回って減少させられる', () => {
      const 新管理 = 管理.減少させる(50);
      expect(新管理.現在値を取得()).toBe(0);
    });

    it('指定値に変更できる', () => {
      const 新管理 = 管理.現在値を変更(75);
      expect(新管理.現在値を取得()).toBe(75);
    });

    it('クリップで最大値を制限', () => {
      const 超過管理 = 管理.増加させる(150);
      const クリップ後 = 超過管理.クリップ();
      expect(クリップ後.現在値を取得()).toBe(100);
    });

    it('最小値でクリップで最小値を制限', () => {
      const 負管理 = 管理.減少させる(50);
      const クリップ後 = 負管理.最小値でクリップ();
      expect(クリップ後.現在値を取得()).toBe(0);
    });
  });

  describe('最大値・最小値の制限', () => {
    it('最大値を超える値は自動的に制限される', () => {
      const 新管理 = 管理.現在値を変更(150);
      expect(新管理.現在値を取得()).toBe(100);
    });

    it('最小値を下回る値は自動的に制限される', () => {
      const 新管理 = 管理.現在値を変更(-50);
      expect(新管理.現在値を取得()).toBe(0);
    });

    it('最大値の変更で現在値が超えていたら制限される', () => {
      const 高い値 = 管理.増加させる(100);
      expect(高い値.現在値を取得()).toBe(100);

      const 最大値縮小 = 高い値.最大値を変更(50);
      expect(最大値縮小.現在値を取得()).toBe(50);
    });

    it('最小値の変更で現在値が下回っていたら制限される', () => {
      const 低い値 = 管理.減少させる(20);
      expect(低い値.現在値を取得()).toBe(0);

      const 最小値上げ = 低い値.最小値を変更(10);
      expect(最小値上げ.現在値を取得()).toBe(10);
    });
  });

  describe('履歴管理', () => {
    it('値を変更すると履歴に記録される', () => {
      管理.現在値を変更(30);
      expect(管理.履歴のサイズ()).toBe(0);
    });

    it('新しい管理で履歴が保存される', () => {
      const 新管理 = 管理.現在値を変更(30);
      expect(新管理.履歴のサイズ()).toBe(1);
    });

    it('複数の変更が履歴に保存される', () => {
      const m1 = 管理.現在値を変更(30);
      const m2 = m1.現在値を変更(40);
      const m3 = m2.現在値を変更(50);

      expect(m3.履歴のサイズ()).toBe(3);
    });

    it('履歴から変更前後の値を取得できる', () => {
      const 新管理 = 管理.現在値を変更(30);
      const 履歴 = 新管理.履歴を取得();

      expect(履歴[0].変更前.現在値).toBe(20);
      expect(履歴[0].変更後.現在値).toBe(30);
    });

    it('最新の変更を取得できる', () => {
      const m1 = 管理.現在値を変更(30);
      const m2 = m1.現在値を変更(40);

      const 最新 = m2.最新の変更を取得();
      expect(最新?.変更前.現在値).toBe(30);
      expect(最新?.変更後.現在値).toBe(40);
    });

    it('履歴が空の場合は未定義を返す', () => {
      expect(管理.最新の変更を取得()).toBeUndefined();
    });

    it('履歴に日時情報が保存される', () => {
      const 新管理 = 管理.現在値を変更(30);
      const 履歴 = 新管理.履歴を取得();

      expect(履歴[0].変更日時).toBeGreaterThan(0);
      expect(typeof 履歴[0].変更日時).toBe('number');
    });

    it('複数の変更の日時は昇順', () => {
      const m1 = 管理.現在値を変更(30);
      const m2 = m1.現在値を変更(40);

      const 履歴 = m2.履歴を取得();
      expect(履歴[0].変更日時).toBeLessThanOrEqual(履歴[1].変更日時);
    });

    it('履歴が空かを確認できる', () => {
      expect(管理.履歴が空か()).toBe(true);
      const 新管理 = 管理.現在値を変更(30);
      expect(新管理.履歴が空か()).toBe(false);
    });

    it('履歴をリセットできる', () => {
      const m1 = 管理.現在値を変更(30);
      const m2 = m1.現在値を変更(40);

      expect(m2.履歴のサイズ()).toBe(2);

      const リセット = m2.履歴をリセット();
      expect(リセット.履歴のサイズ()).toBe(0);
      expect(リセット.現在値を取得()).toBe(40);
    });
  });

  describe('ゲーム固有処理なし', () => {
    it('リスナー機能がない', () => {
      expect((管理 as any).リスナーを登録).toBeUndefined();
    });

    it('通知機能がない', () => {
      expect((管理 as any).リスナーたちに通知).toBeUndefined();
    });

    it('イベント機能がない', () => {
      expect((管理 as any).イベントを発行).toBeUndefined();
    });
  });

  describe('層2への依存なし', () => {
    it('状態変化管理への依存がない', () => {
      expect((管理 as any).状態管理).toBeUndefined();
    });

    it('効果解決への依存がない', () => {
      expect((管理 as any).効果解決).toBeUndefined();
    });
  });

  describe('実践的なシナリオ', () => {
    it('複数の数値を独立して管理', () => {
      const hp = new 数値管理('hp', 100, 100, 0);
      const mp = new 数値管理('mp', 50, 50, 0);

      const hp_after = hp.減少させる(20);
      const mp_after = mp.減少させる(10);

      expect(hp_after.現在値を取得()).toBe(80);
      expect(mp_after.現在値を取得()).toBe(40);
    });

    it('チェーン操作で複数変更', () => {
      const 結果 = 管理
        .増加させる(10)
        .増加させる(20)
        .減少させる(5);

      expect(結果.現在値を取得()).toBe(45);
      expect(結果.履歴のサイズ()).toBe(3);
    });

    it('ゲーム側で意味を付与', () => {
      // パーツ側では名前は単なるIDに過ぎない
      const generic = new 数値管理('resource_001', 50, 100, 0);

      // ゲーム側でこれをライフとして意味付け
      const HP = generic;
      expect(HP.名前を取得()).toBe('resource_001');
      expect(HP.現在値を取得()).toBe(50);

      // 別のゲーム側でこれをマナとして意味付け
      const Mana = new 数値管理('resource_002', 30, 100, 0);
      expect(Mana.現在値を取得()).toBe(30);
    });
  });
});
