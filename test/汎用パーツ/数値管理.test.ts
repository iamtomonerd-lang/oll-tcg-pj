/**
 * 汎用パーツ：数値管理 - テスト
 */

import { describe, it, expect, beforeEach } from '@jest/globals';
import {
  数値を作成,
  は最大か,
  はゼロ以下か,
  割合を取得,
  数値管理
} from '../../src/汎用パーツ/数値管理/index';

describe('数値管理 - 基本機能', () => {
  describe('数値を作成', () => {
    it('現在値と最大値を設定できる', () => {
      const 数値 = 数値を作成(20, 100);
      expect(数値.現在値).toBe(20);
      expect(数値.最大値).toBe(100);
    });

    it('最大値なしで作成できる', () => {
      const 数値 = 数値を作成(50);
      expect(数値.現在値).toBe(50);
      expect(数値.最大値).toBeNull();
    });

    it('負の値で作成できる', () => {
      const 数値 = 数値を作成(-10, 100);
      expect(数値.現在値).toBe(-10);
      expect(数値.最大値).toBe(100);
    });
  });

  describe('は最大か', () => {
    it('現在値が最大値に達している場合true', () => {
      const 数値 = 数値を作成(100, 100);
      expect(は最大か(数値)).toBe(true);
    });

    it('現在値が最大値を超えている場合true', () => {
      const 数値 = 数値を作成(150, 100);
      expect(は最大か(数値)).toBe(true);
    });

    it('現在値が最大値より小さい場合false', () => {
      const 数値 = 数値を作成(50, 100);
      expect(は最大か(数値)).toBe(false);
    });

    it('最大値がnullの場合false', () => {
      const 数値 = 数値を作成(1000);
      expect(は最大か(数値)).toBe(false);
    });
  });

  describe('はゼロ以下か', () => {
    it('現在値がゼロの場合true', () => {
      const 数値 = 数値を作成(0, 100);
      expect(はゼロ以下か(数値)).toBe(true);
    });

    it('現在値が負の場合true', () => {
      const 数値 = 数値を作成(-10, 100);
      expect(はゼロ以下か(数値)).toBe(true);
    });

    it('現在値が正の場合false', () => {
      const 数値 = 数値を作成(50, 100);
      expect(はゼロ以下か(数値)).toBe(false);
    });
  });

  describe('割合を取得', () => {
    it('現在値を最大値で割った割合を返す', () => {
      const 数値 = 数値を作成(50, 100);
      expect(割合を取得(数値)).toBe(0.5);
    });

    it('最大値に達している場合1.0を返す', () => {
      const 数値 = 数値を作成(100, 100);
      expect(割合を取得(数値)).toBe(1);
    });

    it('0の場合0を返す', () => {
      const 数値 = 数値を作成(0, 100);
      expect(割合を取得(数値)).toBe(0);
    });

    it('負の値の場合0にクリップ', () => {
      const 数値 = 数値を作成(-50, 100);
      expect(割合を取得(数値)).toBe(0);
    });

    it('最大値を超える場合1.0にクリップ', () => {
      const 数値 = 数値を作成(150, 100);
      expect(割合を取得(数値)).toBe(1);
    });

    it('最大値がnullの場合0を返す', () => {
      const 数値 = 数値を作成(50);
      expect(割合を取得(数値)).toBe(0);
    });

    it('最大値が0の場合0を返す', () => {
      const 数値 = 数値を作成(50, 0);
      expect(割合を取得(数値)).toBe(0);
    });
  });
});

describe('数値管理 - 管理クラス', () => {
  let 管理: 数値管理;

  beforeEach(() => {
    管理 = new 数値管理(20, 100);
  });

  describe('取得・参照', () => {
    it('現在の数値を取得できる', () => {
      const 数値 = 管理.取得();
      expect(数値.現在値).toBe(20);
      expect(数値.最大値).toBe(100);
    });

    it('現在値を直接取得できる', () => {
      expect(管理.現在値を取得()).toBe(20);
    });

    it('最大値を直接取得できる', () => {
      expect(管理.最大値を取得()).toBe(100);
    });
  });

  describe('イミュータビリティ', () => {
    it('現在値の変更は新しい管理インスタンスを返す', () => {
      const 新しい管理 = 管理.現在値を変更(50);
      expect(管理).not.toBe(新しい管理);
      expect(管理.現在値を取得()).toBe(20);
      expect(新しい管理.現在値を取得()).toBe(50);
    });

    it('最大値の変更は新しい管理インスタンスを返す', () => {
      const 新しい管理 = 管理.最大値を変更(200);
      expect(管理).not.toBe(新しい管理);
      expect(管理.最大値を取得()).toBe(100);
      expect(新しい管理.最大値を取得()).toBe(200);
    });

    it('増加させるは新しい管理インスタンスを返す', () => {
      const 新しい管理 = 管理.増加させる(10);
      expect(管理).not.toBe(新しい管理);
      expect(管理.現在値を取得()).toBe(20);
      expect(新しい管理.現在値を取得()).toBe(30);
    });

    it('減少させるは新しい管理インスタンスを返す', () => {
      const 新しい管理 = 管理.減少させる(5);
      expect(管理).not.toBe(新しい管理);
      expect(管理.現在値を取得()).toBe(20);
      expect(新しい管理.現在値を取得()).toBe(15);
    });
  });

  describe('値の操作', () => {
    it('値を増加させられる', () => {
      const 新しい管理 = 管理.増加させる(30);
      expect(新しい管理.現在値を取得()).toBe(50);
    });

    it('値を減少させられる', () => {
      const 新しい管理 = 管理.減少させる(10);
      expect(新しい管理.現在値を取得()).toBe(10);
    });

    it('0以下に減少させられる', () => {
      const 新しい管理 = 管理.減少させる(50);
      expect(新しい管理.現在値を取得()).toBe(-30);
    });

    it('最大値を超えて増加させられる', () => {
      const 新しい管理 = 管理.増加させる(200);
      expect(新しい管理.現在値を取得()).toBe(220);
    });

    it('クリップで値を最大値以下にできる', () => {
      const 新しい管理 = 管理.増加させる(150).クリップ();
      expect(新しい管理.現在値を取得()).toBe(100);
    });

    it('最大値以下の値はクリップしない', () => {
      const 新しい管理 = 管理.クリップ();
      expect(新しい管理).toBe(管理);
      expect(新しい管理.現在値を取得()).toBe(20);
    });

    it('最大値がnullの場合クリップしない', () => {
      const 無制限管理 = new 数値管理(1000);
      const 新しい管理 = 無制限管理.クリップ();
      expect(新しい管理).toBe(無制限管理);
    });
  });

  describe('リスナー機能', () => {
    it('値が変更されるとリスナーが呼ばれる', () => {
      const リスナー = jest.fn();
      管理.リスナーを登録(リスナー);

      管理.現在値を変更(50);

      expect(リスナー).toHaveBeenCalledTimes(1);
      const 変更 = リスナー.mock.calls[0][0];
      expect(変更.変更前.現在値).toBe(20);
      expect(変更.変更後.現在値).toBe(50);
    });

    it('複数のリスナーが登録できる', () => {
      const リスナー1 = jest.fn();
      const リスナー2 = jest.fn();

      管理.リスナーを登録(リスナー1);
      管理.リスナーを登録(リスナー2);

      管理.現在値を変更(50);

      expect(リスナー1).toHaveBeenCalledTimes(1);
      expect(リスナー2).toHaveBeenCalledTimes(1);
    });

    it('リスナーを削除できる', () => {
      const リスナー = jest.fn();
      管理.リスナーを登録(リスナー);

      管理.現在値を変更(50);
      expect(リスナー).toHaveBeenCalledTimes(1);

      管理.リスナーを削除(リスナー);
      管理.現在値を変更(60);

      expect(リスナー).toHaveBeenCalledTimes(1);
    });

    it('すべてのリスナーをクリアできる', () => {
      const リスナー1 = jest.fn();
      const リスナー2 = jest.fn();

      管理.リスナーを登録(リスナー1);
      管理.リスナーを登録(リスナー2);

      管理.リスナーをクリア();
      管理.現在値を変更(50);

      expect(リスナー1).not.toHaveBeenCalled();
      expect(リスナー2).not.toHaveBeenCalled();
    });

    it('イミュータブル操作後も新しい管理にリスナーが引き継がれる', () => {
      const リスナー = jest.fn();
      管理.リスナーを登録(リスナー);

      const 新しい管理 = 管理.現在値を変更(50);
      新しい管理.現在値を変更(60);

      expect(リスナー).toHaveBeenCalledTimes(2);
    });
  });

  describe('最大値の操作', () => {
    it('最大値を変更できる', () => {
      const 新しい管理 = 管理.最大値を変更(200);
      expect(新しい管理.最大値を取得()).toBe(200);
    });

    it('最大値をnullに変更できる', () => {
      const 新しい管理 = 管理.最大値を変更(null);
      expect(新しい管理.最大値を取得()).toBeNull();
    });

    it('最大値の変更でもリスナーが呼ばれる', () => {
      const リスナー = jest.fn();
      管理.リスナーを登録(リスナー);

      管理.最大値を変更(200);

      expect(リスナー).toHaveBeenCalledTimes(1);
    });
  });

  describe('実践的なシナリオ', () => {
    it('HPのように使える', () => {
      const HP管理 = new 数値管理(20, 20);

      let ダメージ記録: number[] = [];
      const ダメージリスナー = (変更: any) => {
        ダメージ記録.push(変更.変更前.現在値 - 変更.変更後.現在値);
      };

      HP管理.リスナーを登録(ダメージリスナー);

      const HP1 = HP管理.減少させる(5);
      const HP2 = HP1.減少させる(3);

      expect(HP2.現在値を取得()).toBe(12);
      expect(ダメージ記録).toEqual([5, 3]);
    });

    it('複数の異なる目的で再利用できる', () => {
      const マナ管理 = new 数値管理(5, 10);
      const スタミナ管理 = new 数値管理(100, 100);
      const 経験値管理 = new 数値管理(0, 1000);

      const マナ変更 = マナ管理.増加させる(2);
      const スタミナ変更 = スタミナ管理.減少させる(20);
      const 経験値変更 = 経験値管理.増加させる(50);

      expect(マナ変更.現在値を取得()).toBe(7);
      expect(スタミナ変更.現在値を取得()).toBe(80);
      expect(経験値変更.現在値を取得()).toBe(50);
    });

    it('チェーン操作ができる', () => {
      const 管理 = new 数値管理(50, 100);
      const 最終状態 = 管理
        .増加させる(20)
        .増加させる(15)
        .減少させる(10)
        .クリップ();

      expect(最終状態.現在値を取得()).toBe(75);
    });
  });
});
