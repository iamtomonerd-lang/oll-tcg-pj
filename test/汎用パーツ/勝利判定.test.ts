describe('勝利判定 - 管理クラス', () => {
  let 判定: 勝利判定;

  beforeEach(() => {
    判定 = new 勝利判定();
  });

  describe('初期状態', () => {
    it('初期状態は未決定', () => {
      expect(判定.現在の結果を取得()).toBe('未決定');
    });

    it('仮結果は設定されていない', () => {
      expect(判定.仮結果が設定されているか()).toBe(false);
    });

    it('仮結果を取得するとnullを返す', () => {
      expect(判定.仮結果を取得()).toBeNull();
    });

    it('履歴が空', () => {
      expect(判定.履歴が空か()).toBe(true);
      expect(判定.履歴のサイズ()).toBe(0);
    });
  });

  describe('現在の結果を取得', () => {
    it('仮結果がない場合、確定状態を返す', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      expect(判定2.現在の結果を取得()).toBe('敗北');
    });

    it('仮結果がある場合、仮結果を返す', () => {
      const 判定2 = 判定.仮結果を設定('敗北');
      expect(判定2.現在の結果を取得()).toBe('敗北');
    });
  });

  describe('確定済み状態を取得', () => {
    it('仮結果がある場合でも確定状態を返す', () => {
      const 判定2 = 判定.仮結果を設定('敗北');
      expect(判定2.確定済み状態を取得()).toBe('未決定');
    });

    it('確定後は確定状態を返す', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      expect(判定2.確定済み状態を取得()).toBe('敗北');
    });
  });

  describe('イミュータビリティ', () => {
    it('仮結果設定は新しいインスタンスを返す', () => {
      const 新判定 = 判定.仮結果を設定('敗北');
      expect(判定).not.toBe(新判定);
      expect(判定.現在の結果を取得()).toBe('未決定');
      expect(新判定.現在の結果を取得()).toBe('敗北');
    });

    it('仮結果変更は新しいインスタンスを返す', () => {
      const 判定2 = 判定.仮結果を設定('敗北');
      const 判定3 = 判定2.仮結果を変更('勝利');
      expect(判定2).not.toBe(判定3);
      expect(判定2.現在の結果を取得()).toBe('敗北');
      expect(判定3.現在の結果を取得()).toBe('勝利');
    });

    it('結果確定は新しいインスタンスを返す', () => {
      const 判定2 = 判定.仮結果を設定('敗北');
      const 判定3 = 判定2.結果を確定();
      expect(判定2).not.toBe(判定3);
      expect(判定2.仮結果が設定されているか()).toBe(true);
      expect(判定3.仮結果が設定されているか()).toBe(false);
    });
  });

  describe('仮結果設定', () => {
    it('勝利に設定できる', () => {
      const 判定2 = 判定.仮結果を設定('勝利');
      expect(判定2.現在の結果を取得()).toBe('勝利');
      expect(判定2.仮結果を取得()).toBe('勝利');
    });

    it('敗北に設定できる', () => {
      const 判定2 = 判定.仮結果を設定('敗北');
      expect(判定2.現在の結果を取得()).toBe('敗北');
    });

    it('引き分けに設定できる', () => {
      const 判定2 = 判定.仮結果を設定('引き分け');
      expect(判定2.現在の結果を取得()).toBe('引き分け');
    });
  });

  describe('仮結果変更', () => {
    it('仮結果を別の結果に変更できる', () => {
      const 判定2 = 判定.仮結果を設定('敗北');
      const 判定3 = 判定2.仮結果を変更('勝利');
      expect(判定3.現在の結果を取得()).toBe('勝利');
    });

    it('仮結果がない場合は新規設定', () => {
      const 判定2 = 判定.仮結果を変更('敗北');
      expect(判定2.現在の結果を取得()).toBe('敗北');
    });

    it('同じ値に変更すると元のインスタンスを返す', () => {
      const 判定2 = 判定.仮結果を設定('敗北');
      const 判定3 = 判定2.仮結果を変更('敗北');
      expect(判定3).toBe(判定2);
    });
  });

  describe('結果確定', () => {
    it('仮結果を確定できる', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      expect(判定2.確定済み状態を取得()).toBe('敗北');
      expect(判定2.仮結果が設定されているか()).toBe(false);
    });

    it('確定後は仮結果がクリアされる', () => {
      const 判定2 = 判定.仮結果を設定('勝利').結果を確定();
      expect(判定2.仮結果を取得()).toBeNull();
    });

    it('既に確定している場合は元のインスタンスを返す', () => {
      const 判定2 = 判定.結果を確定();
      expect(判定2).toBe(判定);
    });

    it('仮結果がない場合は現在の状態を確定', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      const 判定3 = 判定2.結果を確定();
      expect(判定3).toBe(判定2);
    });
  });

  describe('置換処理シナリオ', () => {
    it('敗北予定→勝利へ変更→確定できる', () => {
      // 1. 敗北予定
      const 判定2 = 判定.仮結果を設定('敗北');
      expect(判定2.現在の結果を取得()).toBe('敗北');

      // 2. 置換処理で勝利へ変更
      const 判定3 = 判定2.仮結果を変更('勝利');
      expect(判定3.現在の結果を取得()).toBe('勝利');

      // 3. 勝利確定
      const 判定4 = 判定3.結果を確定();
      expect(判定4.確定済み状態を取得()).toBe('勝利');
    });

    it('複数の置換処理に対応できる', () => {
      // 敗北 → 勝利 → 敗北 → 引き分け
      const j1 = 判定.仮結果を設定('敗北');
      const j2 = j1.仮結果を変更('勝利');
      const j3 = j2.仮結果を変更('敗北');
      const j4 = j3.仮結果を変更('引き分け');

      expect(j4.現在の結果を取得()).toBe('引き分け');

      const j5 = j4.結果を確定();
      expect(j5.確定済み状態を取得()).toBe('引き分け');
    });
  });

  describe('仮結果クリア', () => {
    it('仮結果をクリアできる', () => {
      const 判定2 = 判定.仮結果を設定('敗北');
      const 判定3 = 判定2.仮結果をクリア();

      expect(判定3.仮結果が設定されているか()).toBe(false);
      expect(判定3.確定済み状態を取得()).toBe('未決定');
    });

    it('仮結果がない場合は元のインスタンスを返す', () => {
      const 判定2 = 判定.仮結果をクリア();
      expect(判定2).toBe(判定);
    });
  });

  describe('結果変更', () => {
    it('確定済み結果を変更できる', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      const 判定3 = 判定2.結果を変更('勝利');

      expect(判定3.確定済み状態を取得()).toBe('勝利');
    });

    it('変更後は仮結果がクリアされる', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      const 判定3 = 判定2.仮結果を設定('勝利');
      const 判定4 = 判定3.結果を変更('引き分け');

      expect(判定4.仮結果が設定されているか()).toBe(false);
      expect(判定4.確定済み状態を取得()).toBe('引き分け');
    });

    it('同じ値に変更すると元のインスタンスを返す', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      const 判定3 = 判定2.結果を変更('敗北');
      expect(判定3).toBe(判定2);
    });
  });

  describe('リセット', () => {
    it('状態を未決定にできる', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      const 判定3 = 判定2.リセット();

      expect(判定3.確定済み状態を取得()).toBe('未決定');
    });

    it('仮結果もクリアされる', () => {
      const 判定2 = 判定.仮結果を設定('敗北');
      const 判定3 = 判定2.リセット();

      expect(判定3.仮結果が設定されているか()).toBe(false);
    });

    it('既に未決定の場合は元のインスタンスを返す', () => {
      const 判定2 = 判定.リセット();
      expect(判定2).toBe(判定);
    });

    it('履歴は保持される', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      expect(判定2.履歴のサイズ()).toBe(1);

      const 判定3 = 判定2.リセット();
      expect(判定3.履歴のサイズ()).toBe(1);
    });
  });

  describe('履歴管理 - 状態変更', () => {
    it('結果確定時に履歴が記録される', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      expect(判定2.履歴のサイズ()).toBe(1);
    });

    it('履歴に変更前後の状態が記録される', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      const 履歴 = 判定2.履歴を取得();

      expect(履歴[0].変更前).toBe('未決定');
      expect(履歴[0].変更後).toBe('敗北');
    });

    it('履歴に時刻が記録される', () => {
      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      const 履歴 = 判定2.履歴を取得();

      expect(履歴[0].時刻).toBeGreaterThan(0);
      expect(typeof 履歴[0].時刻).toBe('number');
    });

    it('複数の変更が履歴に記録される', () => {
      const j1 = 判定.仮結果を設定('敗北').結果を確定();
      const j2 = j1.結果を変更('勝利');
      const j3 = j2.結果を変更('引き分け');

      expect(j3.履歴のサイズ()).toBe(3);
    });

    it('履歴の時刻は昇順', () => {
      const j1 = 判定.仮結果を設定('敗北').結果を確定();
      const j2 = j1.結果を変更('勝利');

      const 履歴 = j2.履歴を取得();
      expect(履歴[0].時刻).toBeLessThanOrEqual(履歴[1].時刻);
    });
  });

  describe('履歴取得', () => {
    it('履歴を取得できる', () => {
      const j1 = 判定.仮結果を設定('敗北').結果を確定();
      const 履歴 = j1.履歴を取得();

      expect(履歴.length).toBe(1);
      expect(履歴[0].変更後).toBe('敗北');
    });

    it('最新履歴を取得できる', () => {
      const j1 = 判定.仮結果を設定('敗北').結果を確定();
      const j2 = j1.結果を変更('勝利');

      const 最新 = j2.最新履歴を取得();
      expect(最新?.変更後).toBe('勝利');
    });

    it('履歴が空の場合は未定義を返す', () => {
      expect(判定.最新履歴を取得()).toBeUndefined();
    });

    it('履歴が空かを確認できる', () => {
      expect(判定.履歴が空か()).toBe(true);

      const 判定2 = 判定.仮結果を設定('敗北').結果を確定();
      expect(判定2.履歴が空か()).toBe(false);
    });
  });

  describe('履歴クリア', () => {
    it('履歴をクリアできる', () => {
      const j1 = 判定.仮結果を設定('敗北').結果を確定();
      const j2 = j1.結果を変更('勝利');

      expect(j2.履歴のサイズ()).toBe(2);

      const j3 = j2.履歴をクリア();
      expect(j3.履歴のサイズ()).toBe(0);
      expect(j3.確定済み状態を取得()).toBe('勝利');
    });

    it('履歴が空の場合は元のインスタンスを返す', () => {
      const 判定2 = 判定.履歴をクリア();
      expect(判定2).toBe(判定);
    });

    it('状態は保持される', () => {
      const j1 = 判定.仮結果を設定('敗北').結果を確定();
      const j2 = j1.履歴をクリア();

      expect(j2.確定済み状態を取得()).toBe('敗北');
    });
  });

  describe('完全リセット', () => {
    it('状態と履歴をすべてリセット', () => {
      const j1 = 判定.仮結果を設定('敗北').結果を確定();
      const j2 = j1.結果を変更('勝利');

      expect(j2.履歴のサイズ()).toBe(2);
      expect(j2.確定済み状態を取得()).toBe('勝利');

      const j3 = j2.完全リセット();

      expect(j3.履歴のサイズ()).toBe(0);
      expect(j3.確定済み状態を取得()).toBe('未決定');
    });
  });

  describe('ゲーム非依存性確認', () => {
    it('ゲーム固有の状態がない', () => {
      // 状態は：未決定、勝利、敗北、引き分け のみ
      // ゲーム固有状態（例：ターン中、攻撃可能など）がない
      const 状態一覧 = ['未決定', '勝利', '敗北', '引き分け'];
      const 判定2 = 判定.仮結果を設定('勝利');
      expect(状態一覧).toContain(判定2.現在の結果を取得());
    });

    it('ゲーム固有メソッドがない', () => {
      expect((判定 as any).ライフを計算).toBeUndefined();
      expect((判定 as any).山札をチェック).toBeUndefined();
      expect((判定 as any).ターンを進行).toBeUndefined();
    });

    it('層0への依存がない', () => {
      expect((判定 as any).カード).toBeUndefined();
      expect((判定 as any).プレイヤー).toBeUndefined();
    });

    it('層1への依存がない', () => {
      expect((判定 as any).ダメージを適用).toBeUndefined();
    });

    it('層2への依存がない', () => {
      expect((判定 as any).リスナーを登録).toBeUndefined();
      expect((判定 as any).状態を通知).toBeUndefined();
    });
  });

  describe('実践的なシナリオ', () => {
    it('複数ゲームで独立して使用', () => {
      const ゲームA = new 勝利判定();
      const ゲームB = new 勝利判定();

      const A結果 = ゲームA.仮結果を設定('勝利').結果を確定();
      const B結果 = ゲームB.仮結果を設定('敗北').結果を確定();

      expect(A結果.確定済み状態を取得()).toBe('勝利');
      expect(B結果.確定済み状態を取得()).toBe('敗北');
    });

    it('チェーン操作で複数ステップ実行', () => {
      const 結果 = 判定
        .仮結果を設定('敗北')
        .仮結果を変更('勝利')
        .結果を確定();

      expect(結果.確定済み状態を取得()).toBe('勝利');
      expect(結果.履歴のサイズ()).toBe(1);
    });

    it('置換効果をシミュレート', () => {
      // 通常はプレイヤーAの敗北
      let 判定A = new 勝利判定();
      判定A = 判定A.仮結果を設定('敗北');

      // 置換効果：「敗北する代わりに、勝利する」
      判定A = 判定A.仮結果を変更('勝利');

      // 確定
      判定A = 判定A.結果を確定();

      expect(判定A.確定済み状態を取得()).toBe('勝利');
    });

    it('ゲーム側で意味を付与', () => {
      // パーツ側では状態は単なる状態
      const 判定 = new 勝利判定();
      const 結果 = 判定.仮結果を設定('勝利').結果を確定();

      // ゲーム側でこの勝利を「プレイヤーAが勝利」として解釈
      const プレイヤーA勝利 = 結果.確定済み状態を取得() === '勝利';
      expect(プレイヤーA勝利).toBe(true);

      // 別のゲーム側でこれを「イベントクリア」として解釈
      const イベントクリア = 結果.確定済み状態を取得() === '勝利';
      expect(イベントクリア).toBe(true);
    });
  });
});
