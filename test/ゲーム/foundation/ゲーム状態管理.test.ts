/**
 * ゲーム基盤層：ゲーム状態管理 - テスト
 *
 * テスト対象：
 * - ゲーム初期化
 * - ターン管理
 * - フェイズ移動
 * - ライフ管理
 * - ゲーム終了
 * - 操作履歴
 */

import { describe, it, expect, beforeEach } from '@jest/globals';
import { ゲーム状態管理 } from '../../../src/ゲーム/foundation';
import type { ゲーム設定 } from '../../../src/ゲーム/foundation';

describe('ゲーム状態管理', () => {
  let ゲーム: ゲーム状態管理;
  let 設定: ゲーム設定;

  beforeEach(() => {
    設定 = {
      プレイヤー数: 2,
      初期ライフ: 20,
      デッキ枚数: 60,
      初期手札数: 5
    };
    ゲーム = new ゲーム状態管理('game-1', 設定);
  });

  describe('初期状態', () => {
    it('ゲームIDが保持される', () => {
      expect(ゲーム.ゲームID).toBe('game-1');
    });

    it('初期状態は「初期化前」', () => {
      expect(ゲーム.状態).toBe('初期化前');
    });

    it('ターン番号は0', () => {
      expect(ゲーム.ターン番号).toBe(0);
    });

    it('プレイヤー数は0', () => {
      expect(ゲーム.プレイヤー数).toBe(0);
    });

    it('現在のプレイヤーIDはnull', () => {
      expect(ゲーム.現在のプレイヤーID).toBe(null);
    });
  });

  describe('ゲーム初期化', () => {
    it('ゲームを初期化できる', () => {
      const ゲーム2 = ゲーム.ゲームを初期化();
      expect(ゲーム2.状態).toBe('プレイ中');
      expect(ゲーム2.プレイヤー数).toBe(2);
    });

    it('プレイヤーが生成される', () => {
      const ゲーム2 = ゲーム.ゲームを初期化();
      expect(ゲーム2.プレイヤー数).toBe(設定.プレイヤー数);
    });

    it('プレイヤーのライフが初期化される', () => {
      const ゲーム2 = ゲーム.ゲームを初期化();
      const プレイヤーID = ゲーム2.現在のプレイヤーID;
      if (プレイヤーID) {
        const ライフ = ゲーム2.ライフを取得(プレイヤーID);
        expect(ライフ).toBe(設定.初期ライフ);
      }
    });

    it('状態が「プレイ中」に変更される', () => {
      const ゲーム2 = ゲーム.ゲームを初期化();
      expect(ゲーム2.状態).toBe('プレイ中');
    });

    it('初期化済みのゲームは再度初期化できない', () => {
      const ゲーム2 = ゲーム.ゲームを初期化();
      expect(() => ゲーム2.ゲームを初期化()).toThrow();
    });

    it('イミュータブル設計で新規インスタンスを返す', () => {
      const ゲーム2 = ゲーム.ゲームを初期化();
      expect(ゲーム2).not.toBe(ゲーム);
      expect(ゲーム.状態).toBe('初期化前');
    });

    it('操作履歴に記録される', () => {
      const ゲーム2 = ゲーム.ゲームを初期化();
      const 履歴 = ゲーム2.履歴を取得();
      expect(履歴.length).toBe(1);
      expect(履歴[0].種類).toBe('初期化');
    });
  });

  describe('ターン管理', () => {
    beforeEach(() => {
      ゲーム = ゲーム.ゲームを初期化();
    });

    it('ターンを開始できる', () => {
      const ゲーム2 = ゲーム.ターンを開始();
      expect(ゲーム2.現在のフェイズ).toBe('メイン1');
    });

    it('フェイズを移動できる', () => {
      let ゲーム実例 = ゲーム.ターンを開始();
      expect(ゲーム実例.現在のフェイズ).toBe('メイン1');

      ゲーム実例 = ゲーム実例.フェイズを移動();
      expect(ゲーム実例.現在のフェイズ).toBe('攻撃');

      ゲーム実例 = ゲーム実例.フェイズを移動();
      expect(ゲーム実例.現在のフェイズ).toBe('メイン2');

      ゲーム実例 = ゲーム実例.フェイズを移動();
      expect(ゲーム実例.現在のフェイズ).toBe('終了');

      ゲーム実例 = ゲーム実例.フェイズを移動();
      expect(ゲーム実例.現在のフェイズ).toBe('開始');
    });

    it('ターンを終了できる', () => {
      let ゲーム実例 = ゲーム.ターンを開始();
      const 最初のプレイヤーID = ゲーム実例.現在のプレイヤーID;

      ゲーム実例 = ゲーム実例.ターンを終了();
      const 次のプレイヤーID = ゲーム実例.現在のプレイヤーID;

      expect(最初のプレイヤーID).not.toBe(次のプレイヤーID);
    });

    it('全プレイヤーのターンが終わるとターン番号が増える', () => {
      let ゲーム実例 = ゲーム.ターンを開始();
      const 最初のターン = ゲーム実例.ターン番号;

      ゲーム実例 = ゲーム実例.ターンを終了(); // プレイヤー2へ
      expect(ゲーム実例.ターン番号).toBe(最初のターン);

      ゲーム実例 = ゲーム実例.ターンを終了(); // プレイヤー1へ（ターン終了）
      expect(ゲーム実例.ターン番号).toBe(最初のターン + 1);
    });

    it('複数のターンをシミュレートできる', () => {
      let ゲーム実例 = ゲーム;
      for (let i = 0; i < 5; i++) {
        ゲーム実例 = ゲーム実例.ターンを開始();
        ゲーム実例 = ゲーム実例.フェイズを移動(); // 攻撃
        ゲーム実例 = ゲーム実例.フェイズを移動(); // メイン2
        ゲーム実例 = ゲーム実例.フェイズを移動(); // 終了
        ゲーム実例 = ゲーム実例.ターンを終了();
      }
      expect(ゲーム実例.ターン番号).toBeGreaterThan(0);
    });
  });

  describe('ライフ管理', () => {
    beforeEach(() => {
      ゲーム = ゲーム.ゲームを初期化();
    });

    it('ライフを取得できる', () => {
      const プレイヤーID = ゲーム.現在のプレイヤーID;
      if (プレイヤーID) {
        const ライフ = ゲーム.ライフを取得(プレイヤーID);
        expect(ライフ).toBe(設定.初期ライフ);
      }
    });

    it('ライフを減らせる', () => {
      const プレイヤーID = ゲーム.現在のプレイヤーID;
      if (プレイヤーID) {
        const ゲーム2 = ゲーム.ライフを変更(プレイヤーID, -5);
        const 新しいライフ = ゲーム2.ライフを取得(プレイヤーID);
        expect(新しいライフ).toBe(設定.初期ライフ - 5);
      }
    });

    it('ライフを増やせる', () => {
      const プレイヤーID = ゲーム.現在のプレイヤーID;
      if (プレイヤーID) {
        let ゲーム実例 = ゲーム.ライフを変更(プレイヤーID, -5);
        ゲーム実例 = ゲーム実例.ライフを変更(プレイヤーID, 3);
        const 新しいライフ = ゲーム実例.ライフを取得(プレイヤーID);
        expect(新しいライフ).toBe(設定.初期ライフ - 2);
      }
    });

    it('ライフが負の値になることができる', () => {
      const プレイヤーID = ゲーム.現在のプレイヤーID;
      if (プレイヤーID) {
        const ゲーム2 = ゲーム.ライフを変更(プレイヤーID, -30);
        const 新しいライフ = ゲーム2.ライフを取得(プレイヤーID);
        expect(新しいライフ).toBeLessThan(0);
      }
    });

    it('存在しないプレイヤーのライフ変更はエラー', () => {
      expect(() => ゲーム.ライフを変更('non-existent-player', -5)).toThrow();
    });

    it('イミュータブル設計で新規インスタンスを返す', () => {
      const プレイヤーID = ゲーム.現在のプレイヤーID;
      if (プレイヤーID) {
        const ゲーム2 = ゲーム.ライフを変更(プレイヤーID, -5);
        expect(ゲーム2).not.toBe(ゲーム);
        const 元のライフ = ゲーム.ライフを取得(プレイヤーID);
        expect(元のライフ).toBe(設定.初期ライフ);
      }
    });

    it('操作履歴に記録される', () => {
      const プレイヤーID = ゲーム.現在のプレイヤーID;
      if (プレイヤーID) {
        const ゲーム2 = ゲーム.ライフを変更(プレイヤーID, -5);
        const 履歴 = ゲーム2.履歴を取得();
        expect(履歴[履歴.length - 1].種類).toBe('その他');
      }
    });
  });

  describe('ゲーム終了', () => {
    beforeEach(() => {
      ゲーム = ゲーム.ゲームを初期化();
    });

    it('ゲームを終了できる', () => {
      const プレイヤーID = ゲーム.現在のプレイヤーID;
      const ゲーム2 = ゲーム.ゲームを終了(プレイヤーID || null, 'テスト終了');
      expect(ゲーム2.状態).toBe('終了');
    });

    it('ゲーム終了時に理由が記録される', () => {
      const ゲーム2 = ゲーム.ゲームを終了('winner', 'ライフが0になった');
      const 履歴 = ゲーム2.履歴を取得();
      const 終了履歴 = 履歴.find(h => h.種類 === 'ゲーム終了');
      expect(終了履歴?.内容).toEqual(expect.objectContaining({ 理由: 'ライフが0になった' }));
    });

    it('操作履歴に記録される', () => {
      const ゲーム2 = ゲーム.ゲームを終了('winner', 'テスト');
      const 履歴 = ゲーム2.履歴を取得();
      expect(履歴[履歴.length - 1].種類).toBe('ゲーム終了');
    });
  });

  describe('操作履歴', () => {
    beforeEach(() => {
      ゲーム = ゲーム.ゲームを初期化();
    });

    it('操作履歴を取得できる', () => {
      const 履歴 = ゲーム.履歴を取得();
      expect(履歴.length).toBeGreaterThan(0);
    });

    it('複数の操作が記録される', () => {
      let ゲーム実例 = ゲーム;
      ゲーム実例 = ゲーム実例.ターンを開始();
      ゲーム実例 = ゲーム実例.フェイズを移動();
      const プレイヤーID = ゲーム実例.現在のプレイヤーID;
      if (プレイヤーID) {
        ゲーム実例 = ゲーム実例.ライフを変更(プレイヤーID, -5);
      }

      const 履歴 = ゲーム実例.履歴を取得();
      expect(履歴.length).toBeGreaterThan(1);
    });

    it('操作履歴は参照を保護される', () => {
      const 履歴1 = ゲーム.履歴を取得();
      const 履歴2 = ゲーム.履歴を取得();
      expect(履歴1).not.toBe(履歴2);
      expect(履歴1).toEqual(履歴2);
    });
  });

  describe('複合シナリオ', () => {
    it('ゲーム開始から終了までをシミュレートできる', () => {
      let ゲーム実例 = new ゲーム状態管理('game-test', {
        プレイヤー数: 2,
        初期ライフ: 20,
        デッキ枚数: 60,
        初期手札数: 5
      });

      // ゲーム初期化
      ゲーム実例 = ゲーム実例.ゲームを初期化();
      expect(ゲーム実例.状態).toBe('プレイ中');

      // ターン開始
      ゲーム実例 = ゲーム実例.ターンを開始();
      expect(ゲーム実例.現在のフェイズ).toBe('メイン1');

      // フェイズ進行
      ゲーム実例 = ゲーム実例.フェイズを移動();
      expect(ゲーム実例.現在のフェイズ).toBe('攻撃');

      // ライフ変更（攻撃）
      const 相手プレイヤーID = ゲーム実例.現在のプレイヤーID;
      if (相手プレイヤーID) {
        ゲーム実例 = ゲーム実例.ライフを変更(相手プレイヤーID, -10);
        const 相手ライフ = ゲーム実例.ライフを取得(相手プレイヤーID);
        expect(相手ライフ).toBe(10);
      }

      // ターン終了
      ゲーム実例 = ゲーム実例.ターンを終了();

      // ゲーム終了
      ゲーム実例 = ゲーム実例.ゲームを終了('winner', 'テスト完了');
      expect(ゲーム実例.状態).toBe('終了');
    });

    it('複数ターンの進行を管理できる', () => {
      let ゲーム実例 = ゲーム.ゲームを初期化();

      for (let turn = 0; turn < 3; turn++) {
        ゲーム実例 = ゲーム実例.ターンを開始();

        // フェイズ進行
        ゲーム実例 = ゲーム実例.フェイズを移動(); // 攻撃
        ゲーム実例 = ゲーム実例.フェイズを移動(); // メイン2
        ゲーム実例 = ゲーム実例.フェイズを移動(); // 終了

        // ターン終了
        ゲーム実例 = ゲーム実例.ターンを終了();
      }

      expect(ゲーム実例.ターン番号).toBe(2);
    });
  });

  describe('イミュータビリティ確認', () => {
    beforeEach(() => {
      ゲーム = ゲーム.ゲームを初期化();
    });

    it('すべての操作が新規インスタンスを返す', () => {
      const ゲーム1 = ゲーム;
      const ゲーム2 = ゲーム.ターンを開始();
      expect(ゲーム1).not.toBe(ゲーム2);

      const プレイヤーID = ゲーム2.現在のプレイヤーID;
      if (プレイヤーID) {
        const ゲーム3 = ゲーム2.ライフを変更(プレイヤーID, -5);
        expect(ゲーム2).not.toBe(ゲーム3);
      }
    });
  });
});
