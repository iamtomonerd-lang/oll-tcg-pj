/**
 * 層2：効果解決 - テスト
 */

import { describe, it, expect, beforeEach } from '@jest/globals';
import type { 処理 } from '../../src/層2/処理エンジン/処理';
import type { 効果 } from '../../src/層2/効果解決/効果';
import { 効果解決 } from '../../src/層2/効果解決/効果解決';

describe('効果解決', () => {
  let 解決: 効果解決;

  beforeEach(() => {
    解決 = new 効果解決();
  });

  describe('効果を登録できる', () => {
    it('単一の効果を解決できる', () => {
      const 効果: 効果 = {
        名前: 'テスト効果',
        解決: () => [
          { 種類: '処理A', 実行: () => {} }
        ]
      };

      解決.解決(効果);

      expect(解決.処理数()).toBe(1);
    });

    it('複数の効果を登録できる', () => {
      const 効果1: 効果 = {
        名前: '効果1',
        解決: () => [
          { 種類: '処理A', 実行: () => {} }
        ]
      };

      const 効果2: 効果 = {
        名前: '効果2',
        解決: () => [
          { 種類: '処理B', 実行: () => {} }
        ]
      };

      解決.解決(効果1);
      解決.解決(効果2);

      expect(解決.処理数()).toBe(2);
    });
  });

  describe('効果から処理が生成される', () => {
    it('効果が複数の処理を生成できる', () => {
      const 効果: 効果 = {
        名前: '複合効果',
        解決: () => [
          { 種類: '処理A', 実行: () => {} },
          { 種類: '処理B', 実行: () => {} },
          { 種類: '処理C', 実行: () => {} }
        ]
      };

      解決.解決(効果);

      expect(解決.処理数()).toBe(3);
    });

    it('複数効果から複数処理が生成される', () => {
      const 効果1: 効果 = {
        名前: '効果1',
        解決: () => [
          { 種類: '処理A', 実行: () => {} },
          { 種類: '処理B', 実行: () => {} }
        ]
      };

      const 効果2: 効果 = {
        名前: '効果2',
        解決: () => [
          { 種類: '処理C', 実行: () => {} }
        ]
      };

      解決.解決(効果1);
      解決.解決(効果2);

      expect(解決.処理数()).toBe(3);
    });
  });

  describe('生成された処理がキューに追加される', () => {
    it('処理がキューに格納される', () => {
      const 実行記録: string[] = [];

      const 効果: 効果 = {
        名前: 'テスト効果',
        解決: () => [
          { 種類: '処理A', 実行: () => { 実行記録.push('A'); } }
        ]
      };

      解決.解決(効果);
      const キュー = 解決.キューを取得();

      expect(キュー.次の処理を確認()?.種類).toBe('処理A');
    });

    it('複数処理が順番でキューに格納される', () => {
      const デバッグ情報1: 効果 = {
        名前: '効果1',
        解決: () => [
          { 種類: '処理A', 実行: () => {} },
          { 種類: '処理B', 実行: () => {} }
        ]
      };

      解決.解決(デバッグ情報1);

      const キュー情報 = 解決.デバッグ情報();
      expect(キュー情報).toEqual(['処理A', '処理B']);
    });
  });

  describe('複数処理を順番維持できる', () => {
    it('効果から生成された処理が登録順に実行される', async () => {
      const 実行順序: string[] = [];

      const 効果1: 効果 = {
        名前: '効果1',
        解決: () => [
          { 種類: '処理A', 実行: () => { 実行順序.push('A'); } },
          { 種類: '処理B', 実行: () => { 実行順序.push('B'); } }
        ]
      };

      const 効果2: 効果 = {
        名前: '効果2',
        解決: () => [
          { 種類: '処理C', 実行: () => { 実行順序.push('C'); } }
        ]
      };

      解決.解決(効果1);
      解決.解決(効果2);

      await 解決.実行();

      expect(実行順序).toEqual(['A', 'B', 'C']);
    });

    it('複数効果から生成された処理が順番を維持する', async () => {
      const 実行順序: number[] = [];

      const 効果を作成 = (効果番号: number, 処理数: number): 効果 => ({
        名前: `効果${効果番号}`,
        解決: () => {
          const 処理配列: 処理[] = [];
          for (let i = 0; i < 処理数; i++) {
            const 処理番号 = 効果番号 * 100 + i;
            処理配列.push({
              種類: `処理${処理番号}`,
              実行: () => { 実行順序.push(処理番号); }
            });
          }
          return 処理配列;
        }
      });

      // 効果1：2個の処理を生成
      // 効果2：3個の処理を生成
      // 効果3：1個の処理を生成
      解決.解決(効果を作成(1, 2));
      解決.解決(効果を作成(2, 3));
      解決.解決(効果を作成(3, 1));

      await 解決.実行();

      // 登録順に実行される
      expect(実行順序).toEqual([
        100, 101,  // 効果1の処理
        200, 201, 202,  // 効果2の処理
        300  // 効果3の処理
      ]);
    });

    it('非同期処理が含まれても順番を維持される', async () => {
      const 実行順序: string[] = [];

      const 効果1: 効果 = {
        名前: '効果1',
        解決: () => [
          { 種類: '処理A', 実行: () => { 実行順序.push('A'); } }
        ]
      };

      const 効果2: 効果 = {
        名前: '効果2',
        解決: () => [
          {
            種類: '処理B（非同期）',
            実行: async () => {
              await new Promise(resolve => setTimeout(resolve, 10));
              実行順序.push('B');
            }
          }
        ]
      };

      const 効果3: 効果 = {
        名前: '効果3',
        解決: () => [
          { 種類: '処理C', 実行: () => { 実行順序.push('C'); } }
        ]
      };

      解決.解決(効果1);
      解決.解決(効果2);
      解決.解決(効果3);

      await 解決.実行();

      // 遅延があっても順番を維持
      expect(実行順序).toEqual(['A', 'B', 'C']);
    });
  });

  describe('複数解決メソッド', () => {
    it('複数の効果を一度に解決できる', async () => {
      const 実行順序: string[] = [];

      const 効果配列: 効果[] = [
        {
          名前: '効果1',
          解決: () => [{ 種類: '処理A', 実行: () => { 実行順序.push('A'); } }]
        },
        {
          名前: '効果2',
          解決: () => [{ 種類: '処理B', 実行: () => { 実行順序.push('B'); } }]
        },
        {
          名前: '効果3',
          解決: () => [{ 種類: '処理C', 実行: () => { 実行順序.push('C'); } }]
        }
      ];

      解決.複数解決(効果配列);

      await 解決.実行();

      expect(実行順序).toEqual(['A', 'B', 'C']);
    });
  });

  describe('キュー管理機能', () => {
    it('キューをリセットできる', () => {
      const 効果: 効果 = {
        名前: 'テスト効果',
        解決: () => [
          { 種類: '処理A', 実行: () => {} },
          { 種類: '処理B', 実行: () => {} }
        ]
      };

      解決.解決(効果);
      expect(解決.処理数()).toBe(2);

      解決.リセット();
      expect(解決.処理数()).toBe(0);
    });

    it('デバッグ情報を取得できる', () => {
      const 効果: 効果 = {
        名前: 'テスト効果',
        解決: () => [
          { 種類: '処理A', 実行: () => {} },
          { 種類: '処理B', 実行: () => {} },
          { 種類: '処理C', 実行: () => {} }
        ]
      };

      解決.解決(効果);

      const デバッグ情報 = 解決.デバッグ情報();
      expect(デバッグ情報).toEqual(['処理A', '処理B', '処理C']);
    });
  });

  describe('処理エンジンとの接続', () => {
    it('キューを取得して外部で実行できる', async () => {
      const 実行順序: string[] = [];

      const 効果: 効果 = {
        名前: 'テスト効果',
        解決: () => [
          { 種類: '処理A', 実行: () => { 実行順序.push('A'); } },
          { 種類: '処理B', 実行: () => { 実行順序.push('B'); } }
        ]
      };

      解決.解決(効果);

      // キューを取得して実行
      const キュー = 解決.キューを取得();
      await キュー.実行();

      expect(実行順序).toEqual(['A', 'B']);
    });

    it('効果解決の実行メソッドで処理が実行される', async () => {
      const 実行順序: string[] = [];

      const 効果: 効果 = {
        名前: 'テスト効果',
        解決: () => [
          { 種類: '処理A', 実行: () => { 実行順序.push('A'); } }
        ]
      };

      解決.解決(効果);
      await 解決.実行();

      expect(実行順序).toEqual(['A']);
    });
  });
});
