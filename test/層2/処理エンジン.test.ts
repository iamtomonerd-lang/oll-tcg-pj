/**
 * 層2：処理エンジン - テスト
 */

import { describe, it, expect, beforeEach } from '@jest/globals';
import type { 処理 } from '../../src/層2/処理エンジン/処理';
import { 処理キュー } from '../../src/層2/処理エンジン/処理キュー';

describe('処理エンジン', () => {
  let キュー: 処理キュー;

  beforeEach(() => {
    キュー = new 処理キュー();
  });

  describe('処理追加', () => {
    it('処理を追加できる', () => {
      const 処理: 処理 = {
        種類: 'テスト処理',
        実行: () => {
          // テスト用の空実装
        }
      };

      キュー.追加(処理);

      expect(キュー.処理数()).toBe(1);
      expect(キュー.が空か()).toBe(false);
    });

    it('複数の処理を追加できる', () => {
      const 処理A: 処理 = { 種類: '処理A', 実行: () => {} };
      const 処理B: 処理 = { 種類: '処理B', 実行: () => {} };
      const 処理C: 処理 = { 種類: '処理C', 実行: () => {} };

      キュー.追加(処理A);
      キュー.追加(処理B);
      キュー.追加(処理C);

      expect(キュー.処理数()).toBe(3);
    });
  });

  describe('キュー確認', () => {
    it('空のキューを確認できる', () => {
      expect(キュー.が空か()).toBe(true);
      expect(キュー.処理数()).toBe(0);
      expect(キュー.次の処理を確認()).toBeUndefined();
    });

    it('次の処理を確認できる', () => {
      const 処理A: 処理 = { 種類: '処理A', 実行: () => {} };
      const 処理B: 処理 = { 種類: '処理B', 実行: () => {} };

      キュー.追加(処理A);
      キュー.追加(処理B);

      const 次の処理 = キュー.次の処理を確認();
      expect(次の処理?.種類).toBe('処理A');

      // 確認後もキューに残っている
      expect(キュー.処理数()).toBe(2);
    });
  });

  describe('順番実行', () => {
    it('登録順に処理が実行される', async () => {
      const 実行順序: string[] = [];

      const 処理A: 処理 = {
        種類: '処理A',
        実行: () => {
          実行順序.push('A');
        }
      };

      const 処理B: 処理 = {
        種類: '処理B',
        実行: () => {
          実行順序.push('B');
        }
      };

      const 処理C: 処理 = {
        種類: '処理C',
        実行: () => {
          実行順序.push('C');
        }
      };

      キュー.追加(処理A);
      キュー.追加(処理B);
      キュー.追加(処理C);

      await キュー.実行();

      expect(実行順序).toEqual(['A', 'B', 'C']);
      expect(キュー.が空か()).toBe(true);
    });

    it('処理実行後、キューが空になる', async () => {
      const 処理: 処理 = {
        種類: 'テスト処理',
        実行: () => {
          // 空実装
        }
      };

      キュー.追加(処理);
      expect(キュー.処理数()).toBe(1);

      await キュー.実行();

      expect(キュー.処理数()).toBe(0);
      expect(キュー.が空か()).toBe(true);
    });
  });

  describe('非同期処理対応', () => {
    it('非同期処理が順番を維持される', async () => {
      const 実行順序: string[] = [];

      const 処理A: 処理 = {
        種類: '処理A（同期）',
        実行: () => {
          実行順序.push('A');
        }
      };

      const 処理B: 処理 = {
        種類: '処理B（非同期）',
        実行: async () => {
          await new Promise(resolve => setTimeout(resolve, 10));
          実行順序.push('B');
        }
      };

      const 処理C: 処理 = {
        種類: '処理C（同期）',
        実行: () => {
          実行順序.push('C');
        }
      };

      キュー.追加(処理A);
      キュー.追加(処理B);
      キュー.追加(処理C);

      await キュー.実行();

      expect(実行順序).toEqual(['A', 'B', 'C']);
    });

    it('複数の非同期処理を順番に実行', async () => {
      const 実行順序: number[] = [];

      const 非同期処理を作成 = (番号: number, 遅延: number): 処理 => ({
        種類: `非同期処理${番号}`,
        実行: async () => {
          await new Promise(resolve => setTimeout(resolve, 遅延));
          実行順序.push(番号);
        }
      });

      キュー.追加(非同期処理を作成(1, 20));
      キュー.追加(非同期処理を作成(2, 10));
      キュー.追加(非同期処理を作成(3, 5));

      await キュー.実行();

      // 遅延時間に関わらず、登録順に実行される
      expect(実行順序).toEqual([1, 2, 3]);
    });
  });

  describe('クリア機能', () => {
    it('キューをクリアできる', () => {
      const 処理: 処理 = { 種類: 'テスト', 実行: () => {} };

      キュー.追加(処理);
      キュー.追加(処理);
      キュー.追加(処理);

      expect(キュー.処理数()).toBe(3);

      キュー.クリア();

      expect(キュー.処理数()).toBe(0);
      expect(キュー.が空か()).toBe(true);
    });
  });

  describe('デバッグ機能', () => {
    it('デバッグ情報を取得できる', () => {
      const 処理A: 処理 = { 種類: '処理A', 実行: () => {} };
      const 処理B: 処理 = { 種類: '処理B', 実行: () => {} };
      const 処理C: 処理 = { 種類: '処理C', 実行: () => {} };

      キュー.追加(処理A);
      キュー.追加(処理B);
      キュー.追加(処理C);

      const デバッグ情報 = キュー.デバッグ情報();

      expect(デバッグ情報).toEqual(['処理A', '処理B', '処理C']);
    });
  });
});
