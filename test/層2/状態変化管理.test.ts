/**
 * 層2：状態変化管理 - テスト
 */

import { describe, it, expect, beforeEach } from '@jest/globals';
import type { 状態変化 } from '../../src/層2/状態変化管理/状態変化';
import { 状態管理 } from '../../src/層2/状態変化管理/状態管理';

describe('状態変化管理', () => {
  let 管理: 状態管理;

  beforeEach(() => {
    管理 = new 状態管理();
  });

  describe('状態変化を記録できる', () => {
    it('単一の状態変化を記録できる', () => {
      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化);

      expect(管理.サイズ()).toBe(1);
    });

    it('複数の状態変化を順番に記録できる', () => {
      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変更(変化1);
      管理.変更(変化2);

      expect(管理.サイズ()).toBe(2);
    });

    it('複数の状態変化を一度に記録できる', () => {
      const 変化配列: 状態変化[] = [
        {
          種類: '数値変更',
          対象: 'player1',
          変更前: 20,
          変更後: 15
        },
        {
          種類: '位置変更',
          対象: 'card1',
          変更前: 'zone1',
          変更後: 'zone2'
        },
        {
          種類: '状態追加',
          対象: 'card2',
          変更前: [],
          変更後: ['毒']
        }
      ];

      管理.複数変更(変化配列);

      expect(管理.サイズ()).toBe(3);
    });
  });

  describe('履歴を取得できる', () => {
    it('記録された変化を取得できる', () => {
      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変更(変化1);
      管理.変更(変化2);

      const 履歴 = 管理.履歴を取得();
      expect(履歴).toEqual([変化1, 変化2]);
    });

    it('指定インデックスの変化を取得できる', () => {
      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化);

      expect(管理.を取得(0)).toEqual(変化);
    });

    it('存在しないインデックスはundefinedを返す', () => {
      expect(管理.を取得(0)).toBeUndefined();
      expect(管理.を取得(999)).toBeUndefined();
    });
  });

  describe('最新状態変化を取得できる', () => {
    it('最後に記録された変化を取得できる', () => {
      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変更(変化1);
      管理.変更(変化2);

      expect(管理.最新()).toEqual(変化2);
    });

    it('履歴が空の場合はundefinedを返す', () => {
      expect(管理.最新()).toBeUndefined();
    });
  });

  describe('リスナーで状態変化を通知できる', () => {
    it('リスナーが登録できる', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化);

      expect(リスナー).toHaveBeenCalledWith(変化);
    });

    it('複数のリスナーに通知できる', () => {
      const リスナー1 = jest.fn();
      const リスナー2 = jest.fn();
      const リスナー3 = jest.fn();

      管理.リスナーを登録(リスナー1);
      管理.リスナーを登録(リスナー2);
      管理.リスナーを登録(リスナー3);

      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化);

      expect(リスナー1).toHaveBeenCalledWith(変化);
      expect(リスナー2).toHaveBeenCalledWith(変化);
      expect(リスナー3).toHaveBeenCalledWith(変化);
    });

    it('複数の変化を記録するたびにリスナーが呼ばれる', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変更(変化1);
      管理.変更(変化2);

      expect(リスナー).toHaveBeenCalledTimes(2);
      expect(リスナー).toHaveBeenNthCalledWith(1, 変化1);
      expect(リスナー).toHaveBeenNthCalledWith(2, 変化2);
    });

    it('リスナーを削除できる', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化1);
      expect(リスナー).toHaveBeenCalledTimes(1);

      管理.リスナーを削除(リスナー);

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変更(変化2);
      expect(リスナー).toHaveBeenCalledTimes(1); // 追加で呼ばれていない
    });

    it('すべてのリスナーを削除できる', () => {
      const リスナー1 = jest.fn();
      const リスナー2 = jest.fn();

      管理.リスナーを登録(リスナー1);
      管理.リスナーを登録(リスナー2);

      管理.リスナーをクリア();

      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化);

      expect(リスナー1).not.toHaveBeenCalled();
      expect(リスナー2).not.toHaveBeenCalled();
    });
  });

  describe('履歴管理', () => {
    it('履歴をリセットできる', () => {
      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化);
      expect(管理.サイズ()).toBe(1);

      管理.リセット();
      expect(管理.サイズ()).toBe(0);
    });

    it('リセット後もリスナーは保持される', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化1);
      管理.リセット();

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変更(変化2);

      expect(リスナー).toHaveBeenCalledTimes(2); // 変化2のみで呼ばれている
      expect(リスナー).toHaveBeenLastCalledWith(変化2);
    });

    it('が空かを確認できる', () => {
      expect(管理.が空か()).toBe(true);

      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化);
      expect(管理.が空か()).toBe(false);

      管理.リセット();
      expect(管理.が空か()).toBe(true);
    });

    it('デバッグ情報を取得できる', () => {
      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      const 変化3: 状態変化 = {
        種類: '状態追加',
        対象: 'card2',
        変更前: [],
        変更後: ['毒']
      };

      管理.変更(変化1);
      管理.変更(変化2);
      管理.変更(変化3);

      const デバッグ情報 = 管理.デバッグ情報();
      expect(デバッグ情報).toEqual(['数値変更', '位置変更', '状態追加']);
    });
  });

  describe('完全リセット', () => {
    it('履歴とリスナーをすべて削除できる', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化1);

      expect(リスナー).toHaveBeenCalledTimes(1);

      管理.完全リセット();

      expect(管理.サイズ()).toBe(0);
      expect(管理.が空か()).toBe(true);

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変更(変化2);

      // 変化2が記録されたが、リスナーは削除されているので呼ばれない
      expect(リスナー).toHaveBeenCalledTimes(1); // 変化1の時だけ
    });
  });

  describe('イミュータビリティ', () => {
    it('取得した履歴を修正しても元の履歴に影響しない', () => {
      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変更(変化);

      const 履歴 = 管理.履歴を取得();
      履歴.push({
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      });

      expect(管理.サイズ()).toBe(1); // 元の履歴は変わらない
    });
  });

  describe('実践的なシナリオ', () => {
    it('複数のイベントリスナーを使い分けられる', () => {
      const イベント記録: 状態変化[] = [];
      const ログ記録: 状態変化[] = [];

      const イベントリスナー = (変化: 状態変化) => {
        イベント記録.push(変化);
      };

      const ログリスナー = (変化: 状態変化) => {
        ログ記録.push(変化);
      };

      管理.リスナーを登録(イベントリスナー);
      管理.リスナーを登録(ログリスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変更(変化1);
      管理.変更(変化2);

      expect(イベント記録).toEqual([変化1, 変化2]);
      expect(ログ記録).toEqual([変化1, 変化2]);
      expect(管理.履歴を取得()).toEqual([変化1, 変化2]);
    });

    it('状態変化の履歴から現在状態を再構築できる', () => {
      const 変化1: 状態変化 = {
        種類: 'HP変更',
        対象: 'player1',
        変更前: 20,
        変更後: 18
      };

      const 変化2: 状態変化 = {
        種類: 'HP変更',
        対象: 'player1',
        変更前: 18,
        変更後: 15
      };

      const 変化3: 状態変化 = {
        種類: 'HP変更',
        対象: 'player1',
        変更前: 15,
        変更後: 20
      };

      管理.変更(変化1);
      管理.変更(変化2);
      管理.変更(変化3);

      // 最新の状態は変化3の変更後
      const 現在状態 = 管理.最新()?.変更後;
      expect(現在状態).toBe(20);

      // 履歴全体から変更過程を確認
      const 履歴 = 管理.履歴を取得();
      expect(履歴[0].変更前).toBe(20);
      expect(履歴[2].変更後).toBe(20);
    });
  });
});
