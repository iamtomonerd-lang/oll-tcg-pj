/**
 * 層2：イベント管理 - テスト
 */

import { describe, it, expect, beforeEach } from '@jest/globals';
import type { 状態変化 } from '../../src/層2/状態変化管理/状態変化';
import type { イベント } from '../../src/層2/状態変化管理/イベント管理/イベント';
import { イベント管理 } from '../../src/層2/状態変化管理/イベント管理/イベント管理';

describe('イベント管理', () => {
  let 管理: イベント管理;

  beforeEach(() => {
    管理 = new イベント管理();
  });

  describe('状態変化からイベントを生成できる', () => {
    it('単一の状態変化からイベントを生成できる', () => {
      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化);

      expect(管理.サイズ()).toBe(1);
    });

    it('複数の状態変化からイベントを順番に生成できる', () => {
      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.複数変化から通知([変化1, 変化2]);

      expect(管理.サイズ()).toBe(2);
    });

    it('生成されたイベントが正しい情報を持つ', () => {
      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化);

      const イベント = 管理.最新();
      expect(イベント).toBeDefined();
      expect(イベント?.種類).toBe('数値変更');
      expect(イベント?.元となる変化).toEqual(変化);
      expect(イベント?.発生時刻).toBeGreaterThan(0);
    });

    it('イベントにメタデータが含まれている', () => {
      const 変化: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変化から通知(変化);

      const イベント = 管理.最新();
      expect(イベント?.データ).toEqual({
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      });
    });
  });

  describe('イベントリスナーで通知できる', () => {
    it('リスナーが登録できる', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化);

      expect(リスナー).toHaveBeenCalledTimes(1);
      const 呼び出しイベント = リスナー.mock.calls[0][0] as イベント;
      expect(呼び出しイベント.種類).toBe('数値変更');
      expect(呼び出しイベント.元となる変化).toEqual(変化);
    });

    it('複数のリスナーに通知できる', () => {
      const リスナー1 = jest.fn();
      const リスナー2 = jest.fn();
      const リスナー3 = jest.fn();

      管理.リスナーを登録(リスナー1);
      管理.リスナーを登録(リスナー2);
      管理.リスナーを登録(リスナー3);

      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化);

      expect(リスナー1).toHaveBeenCalledTimes(1);
      expect(リスナー2).toHaveBeenCalledTimes(1);
      expect(リスナー3).toHaveBeenCalledTimes(1);
    });

    it('複数のイベントをリスナーが受け取る', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変化から通知(変化1);
      管理.変化から通知(変化2);

      expect(リスナー).toHaveBeenCalledTimes(2);
      expect(リスナー.mock.calls[0][0].種類).toBe('数値変更');
      expect(リスナー.mock.calls[1][0].種類).toBe('位置変更');
    });

    it('リスナーを削除できる', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化1);
      expect(リスナー).toHaveBeenCalledTimes(1);

      管理.リスナーを削除(リスナー);

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変化から通知(変化2);
      expect(リスナー).toHaveBeenCalledTimes(1); // 追加で呼ばれていない
    });

    it('すべてのリスナーを削除できる', () => {
      const リスナー1 = jest.fn();
      const リスナー2 = jest.fn();

      管理.リスナーを登録(リスナー1);
      管理.リスナーを登録(リスナー2);

      管理.リスナーをクリア();

      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化);

      expect(リスナー1).not.toHaveBeenCalled();
      expect(リスナー2).not.toHaveBeenCalled();
    });
  });

  describe('イベント履歴を管理できる', () => {
    it('イベント履歴を取得できる', () => {
      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変化から通知(変化1);
      管理.変化から通知(変化2);

      const 履歴 = 管理.履歴を取得();
      expect(履歴.length).toBe(2);
      expect(履歴[0].種類).toBe('数値変更');
      expect(履歴[1].種類).toBe('位置変更');
    });

    it('指定インデックスのイベントを取得できる', () => {
      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化);

      const イベント = 管理.を取得(0);
      expect(イベント).toBeDefined();
      expect(イベント?.種類).toBe('数値変更');
    });

    it('最新のイベントを取得できる', () => {
      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変化から通知(変化1);
      管理.変化から通知(変化2);

      const イベント = 管理.最新();
      expect(イベント?.種類).toBe('位置変更');
    });

    it('イベント履歴をリセットできる', () => {
      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化);
      expect(管理.サイズ()).toBe(1);

      管理.リセット();
      expect(管理.サイズ()).toBe(0);
      expect(管理.が空か()).toBe(true);
    });

    it('リセット後もリスナーは保持される', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化1);
      管理.リセット();

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変化から通知(変化2);

      expect(リスナー).toHaveBeenCalledTimes(2); // 変化2でも呼ばれている
    });
  });

  describe('完全リセット', () => {
    it('履歴とリスナーをすべて削除できる', () => {
      const リスナー = jest.fn();

      管理.リスナーを登録(リスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化1);

      管理.完全リセット();

      expect(管理.サイズ()).toBe(0);
      expect(管理.が空か()).toBe(true);

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.変化から通知(変化2);

      expect(リスナー).toHaveBeenCalledTimes(1); // 変化1の時だけ
    });
  });

  describe('デバッグ情報', () => {
    it('イベント種類の一覧を取得できる', () => {
      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      const 変化3: 状態変化 = {
        種類: '状態追加',
        対象: 'card2',
        変更前: [],
        変更後: ['毒']
      };

      管理.複数変化から通知([変化1, 変化2, 変化3]);

      const デバッグ情報 = 管理.デバッグ情報();
      expect(デバッグ情報).toEqual(['数値変更', '位置変更', '状態追加']);
    });
  });

  describe('イミュータビリティ', () => {
    it('取得したイベント履歴を修正しても元の履歴に影響しない', () => {
      const 変化: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      管理.変化から通知(変化);

      const 履歴 = 管理.履歴を取得();
      履歴.push({
        種類: '位置変更',
        元となる変化: {
          種類: '位置変更',
          対象: 'card1',
          変更前: 'zone1',
          変更後: 'zone2'
        },
        発生時刻: Date.now()
      });

      expect(管理.サイズ()).toBe(1); // 元の履歴は変わらない
    });
  });

  describe('実践的なシナリオ', () => {
    it('複数の用途でリスナーを使い分けられる', () => {
      const UI更新リスナー = jest.fn();
      const ログ記録リスナー = jest.fn();

      管理.リスナーを登録(UI更新リスナー);
      管理.リスナーを登録(ログ記録リスナー);

      const 変化1: 状態変化 = {
        種類: '数値変更',
        対象: 'player1',
        変更前: 20,
        変更後: 15
      };

      const 変化2: 状態変化 = {
        種類: '位置変更',
        対象: 'card1',
        変更前: 'zone1',
        変更後: 'zone2'
      };

      管理.複数変化から通知([変化1, 変化2]);

      expect(UI更新リスナー).toHaveBeenCalledTimes(2);
      expect(ログ記録リスナー).toHaveBeenCalledTimes(2);
    });

    it('状態変化 → イベント → リスナーの流れが機能する', () => {
      const イベント記録: イベント[] = [];

      管理.リスナーを登録((イベント: イベント) => {
        イベント記録.push(イベント);
      });

      const 状態変化1: 状態変化 = {
        種類: 'HP変更',
        対象: 'player1',
        変更前: 20,
        変更後: 18
      };

      管理.変化から通知(状態変化1);

      expect(イベント記録.length).toBe(1);
      expect(イベント記録[0].種類).toBe('HP変更');
      expect(イベント記録[0].元となる変化).toEqual(状態変化1);
      expect(イベント記録[0].発生時刻).toBeGreaterThan(0);
    });
  });
});
