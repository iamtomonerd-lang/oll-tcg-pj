/**
 * 層0：プレイヤー型定義
 *
 * プレイヤーが持つことができる基本情報のみを定義する。
 * 具体的なゲームルール、TCG固有の数値は上位層で定義される。
 *
 * 重要：プレイヤーはゾーンを持たない。
 * ゾーンの所有者管理はゾーン側で行う。
 *
 * 詳細仕様は PLAYER_SPEC.md を参照
 */

/**
 * プレイヤー状態情報
 *
 * プレイヤーが持つ複数の状態を管理する。
 * 例：ターン開始、待機中 など
 * 具体的な状態名は上位層で定義される。
 */
export interface プレイヤー状態 {
  /** 状態の数 */
  所持数: number;

  /** 状態の名前配列 */
  内容: string[];
}

/**
 * プレイヤー数値情報
 *
 * プレイヤーが持つ複数の数値を管理する。
 * 例：ライフポイント、マナ、エネルギー など
 * 具体的な数値の意味は上位層で定義される。
 */
export interface プレイヤー数値情報 {
  /** 数値の種類数 */
  所持数: number;

  /** 数値の配列 */
  内容: number[];
}

/**
 * プレイヤー分類情報
 *
 * プレイヤーが持つ複数の分類タグを管理する。
 * 例：チーム、ランク、プレイヤータイプ など
 * 具体的な分類の意味は上位層で定義される。
 */
export interface プレイヤー分類情報 {
  /** 分類の種類数 */
  所持数: number;

  /** 分類の配列 */
  内容: string[];
}

/**
 * プレイヤー
 *
 * ゲームに参加するプレイヤーの基本構造。
 * TCG全般に共通する情報のみを定義する。
 */
export interface プレイヤー {
  /** プレイヤーの名前 */
  名前: string;

  /** プレイヤーが持つ複数の状態 */
  状態: プレイヤー状態;

  /** プレイヤーが持つ複数の数値情報 */
  数値情報: プレイヤー数値情報;

  /** プレイヤーが持つ複数の分類情報 */
  分類情報: プレイヤー分類情報;

  /** プレイヤーの権限レベルまたは役割 */
  権限: string;
}
