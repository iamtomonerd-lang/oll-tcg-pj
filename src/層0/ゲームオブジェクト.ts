/**
 * 層0：ゲームオブジェクト型定義
 *
 * カードではないがゲーム内に存在するもの全般の基本構造を表す。
 *
 * このファイルでは、ゲームオブジェクトが持つことができる基本情報のみを定義する。
 * 具体的なゲームオブジェクトの種類や意味は上位層で定義される。
 *
 * ## ゲームオブジェクトに含まれるもの
 *
 * - トークン
 * - コア
 * - カウンター
 * - コイン
 * - 紋章
 * - その他カードではないゲーム上の存在
 *
 * ## 0層の責務
 *
 * - ゲームオブジェクトの構造を定義する
 * - ゲームオブジェクトが何を持つことができるかのみを定義
 * - 可変長配列で柔軟に複数の値を保持可能にする
 *
 * ## 上位層で定義すること
 *
 * - 具体的なゲームオブジェクトの種類（「トークン」「コア」など）
 * - 数値情報の意味（「パワー」「レベル」など）
 * - 分類情報の意味（「属性」「タイプ」など）
 * - ゲームオブジェクトのルール
 *
 * 詳細仕様は ゲームオブジェクト仕様.md を参照
 */

/**
 * ゲームオブジェクト状態情報
 *
 * ゲームオブジェクトが持つ複数の状態を管理する。
 * 例：配置済み、アクティブ など
 * 具体的な状態名は上位層で定義される。
 */
export interface ゲームオブジェクト状態 {
  /** 状態の数 */
  所持数: number;

  /** 状態の名前配列 */
  内容: string[];
}

/**
 * ゲームオブジェクト数値情報
 *
 * ゲームオブジェクトが持つ複数の数値を管理する。
 * 例：パワー、レベル、カウント値 など
 * 具体的な数値の意味は上位層で定義される。
 */
export interface ゲームオブジェクト数値情報 {
  /** 数値の種類数 */
  所持数: number;

  /** 数値の配列 */
  内容: number[];
}

/**
 * ゲームオブジェクト分類情報
 *
 * ゲームオブジェクトが持つ複数の分類タグを管理する。
 * 例：属性、タイプ など
 * 具体的な分類の意味は上位層で定義される。
 */
export interface ゲームオブジェクト分類情報 {
  /** 分類の種類数 */
  所持数: number;

  /** 分類の配列 */
  内容: string[];
}

/**
 * ゲームオブジェクト
 *
 * カードではないがゲーム内に存在するもの全般の基本構造。
 * トークン、コア、カウンターなど、TCG全般に共通する情報のみを定義する。
 */
export interface ゲームオブジェクト {
  /** ゲームオブジェクトの名前 */
  名前: string;

  /** ゲームオブジェクトが持つ複数の状態 */
  状態: ゲームオブジェクト状態;

  /** ゲームオブジェクトが持つ複数の数値情報 */
  数値情報: ゲームオブジェクト数値情報;

  /** ゲームオブジェクトが持つ複数の分類情報 */
  分類情報: ゲームオブジェクト分類情報;

  /** ゲームオブジェクトが効果を持つかどうか */
  効果: boolean;
}
