/**
 * 層0：カード型定義
 *
 * ゲーム内で使用されるカードの基本構造を表す。
 *
 * このファイルでは、カードが持つことができる情報の種類のみを定義する。
 * 「攻撃力」「コスト」など具体的なゲームルール、
 * 効果内容、数値の意味は上位層で定義される。
 *
 * ## 0層の責務
 *
 * - カードの構造を定義する
 * - カードが何を持つことができるかのみを定義
 * - 可変長配列で柔軟に複数の値を保持可能にする
 *
 * ## 上位層で定義すること
 *
 * - 具体的なカード効果
 * - 数値情報の意味（「攻撃力」「コスト」など）
 * - 分類情報の意味（「属性」「種族」など）
 * - カードのルール
 *
 * 詳細仕様は カード仕様.md を参照
 */

/**
 * カード状態情報
 *
 * カードが持つ複数の状態を管理する。
 * 例：横向き、疲れた、中毒 など
 * 具体的な状態名は上位層で定義される。
 */
export interface カード状態 {
  /** 状態の数 */
  所持数: number;

  /** 状態の名前配列 */
  内容: string[];
}

/**
 * カード数値情報
 *
 * カードが持つ複数の数値を管理する。
 * 例：攻撃力、コスト、体力 など
 * 具体的な数値の意味は上位層で定義される。
 */
export interface カード数値情報 {
  /** 数値の種類数 */
  所持数: number;

  /** 数値の配列 */
  内容: number[];
}

/**
 * カード分類情報
 *
 * カードが持つ複数の分類タグを管理する。
 * 例：種族、属性、レアリティ など
 * 具体的な分類の意味は上位層で定義される。
 */
export interface カード分類情報 {
  /** 分類の種類数 */
  所持数: number;

  /** 分類の配列 */
  内容: string[];
}

/**
 * カード
 *
 * ゲーム内で使用されるカードの基本構造。
 * TCG全般に共通する情報のみを定義する。
 */
export interface カード {
  /** カードの名前 */
  名前: string;

  /** カードの種類 */
  種類: string;

  /** カードが状態を持つかどうか */
  状態: カード状態;

  /** カードが持つ複数の数値情報 */
  数値情報: カード数値情報;

  /** カードが持つ複数の分類情報 */
  分類情報: カード分類情報;

  /** カードが効果を持つかどうか */
  効果: boolean;
}
