/**
 * 層0：ゾーン型定義
 *
 * ゲーム内のカードやトークンを置く場所「ゾーン」を表す。
 *
 * このファイルでは、ゾーンが持つことができる情報の種類のみを定義する。
 * 具体的なルール（手札の上限数など）、ゾーン間の移動ルール、
 * ゾーンに対する効果などは上位層で定義される。
 *
 * ## 0層の責務
 *
 * - ゾーンの構造を定義する
 * - ゾーンが何を持つことができるかのみを定義
 *
 * ## 上位層で定義すること
 *
 * - 具体的なゾーンの種類（「手札」「場」など）
 * - ゾーン制限（「手札は最大7枚」など）
 * - ゾーン間の移動ルール
 *
 * 詳細仕様は ゾーン仕様.md を参照
 */

/** ゾーンの所有者タイプ */
type ゾーン所有者 = '自分' | '相手' | '共有';

/** ゾーンに入れるもののタイプ */
type ゾーン内容物 = 'カード' | 'トークン';

/** ゾーンの公開領域設定 */
type ゾーン公開領域 = '全員' | 'いいえ' | '所有者' | '指定者';

/**
 * ゾーン
 *
 * ゲーム内のカードやトークンを置く場所を表す。
 * 手札、場、山札、墓地など、様々なゾーンに対応する構造。
 */
export interface ゾーン {
  /** ゾーンの名前 */
  名前: string;

  /** ゾーンの所有者 */
  所有者: ゾーン所有者;

  /** このゾーンに入るものの種類 */
  入るもの: ゾーン内容物;

  /** ゾーン内のカード/トークンが誰に見えるか */
  公開領域: ゾーン公開領域;

  /** ゾーン内のカード/トークンの順番が意味を持つかどうか */
  順番管理: boolean;

  /** このゾーン自体が移動可能かどうか */
  移動可能: boolean;

  /** このゾーンに入るカード/トークンの数に制限があるかどうか */
  数量制限: number | null;
}
