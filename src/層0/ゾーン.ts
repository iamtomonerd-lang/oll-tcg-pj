/**
 * 層0：ゾーン型定義
 *
 * ゾーンが持つことができる情報の種類のみを定義する。
 * 具体的なルール、移動先、効果などは上位層で定義される。
 *
 * 詳細仕様は ZONE_SPEC.md を参照
 */

/** ゾーンの所有者タイプ */
type ゾーン所有者 = '自分' | '相手' | '共有';

/** ゾーンに入れるもののタイプ */
type ゾーン内容物 = 'カード' | 'トークン';

/** ゾーンの公開領域設定 */
type ゾーン公開領域 = '全員' | 'いいえ' | '所有者' | '指定者';

/**
 * ゾーン
 *
 * ゲーム内のカードやトークンを置く場所を表す。
 * 手札、場、山札、墓地など、様々なゾーンに対応する構造。
 */
export interface ゾーン {
  /** ゾーンの名前 */
  名前: string;

  /** ゾーンの所有者 */
  所有者: ゾーン所有者;

  /** このゾーンに入るものの種類 */
  入るもの: ゾーン内容物;

  /** ゾーン内のカード/トークンが誰に見えるか */
  公開領域: ゾーン公開領域;

  /** ゾーン内のカード/トークンの順番が意味を持つかどうか */
  順番管理: boolean;

  /** このゾーン自体が移動可能かどうか */
  移動可能: boolean;

  /** このゾーンに入るカード/トークンの数に制限があるかどうか */
  数量制限: number | null;
}
