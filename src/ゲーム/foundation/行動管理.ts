/**
 * ゲーム基盤層：基本行動システム
 *
 * プレイヤーがゲーム中に実行できる基本的な行動を管理
 * 役割：「プレイヤーの意思によるゲーム操作を表現する」
 *
 * 担当する責務：
 * - 行動の定義
 * - 行動可能状態の管理
 * - 行動要求の受付
 * - 行動実行の流れ管理
 * - 行動履歴情報の生成
 *
 * 担当しない責務：
 * - カード効果処理（効果解決システム）
 * - ダメージ計算（ゲームエンジン）
 * - コスト計算（ゲームエンジン）
 * - 勝利判定（勝利判定パーツ）
 * - ターン進行（ターン管理）
 * - フェイズ進行（フェイズ処理）
 */

/**
 * 行動状態
 */
export type 行動状態 = '未実行' | '実行中' | '成功' | '失敗' | 'キャンセル';

/**
 * 行動可能性チェック結果
 */
export interface 行動可能性チェック結果 {
  readonly 可能: boolean;
  readonly 理由?: string;
}

/**
 * 行動情報
 */
export interface 行動情報 {
  readonly 行動ID: string;
  readonly 行動種類: string;
  readonly 実行者ID: string;
  readonly 対象情報?: unknown;
  readonly パラメータ?: unknown;
  readonly 状態: 行動状態;
  readonly 作成日時: number;
}

/**
 * 行動可能確認インターフェース
 * ゲーム側で実装する条件確認ロジック
 */
export interface 行動可能確認 {
  (行動: 行動情報): 行動可能性チェック結果;
}

/**
 * 行動管理
 *
 * プレイヤー操作としての行動を管理
 * イミュータブル設計：すべての操作が新規インスタンスを返す
 */
export class 行動管理 {
  readonly ゲームID: string;
  private readonly 行動履歴内部: readonly 行動情報[];
  private readonly 行動IDカウンター内部: number;
  private readonly 行動可能確認関数内部: 行動可能確認 | null;

  /**
   * コンストラクタ
   * @param ゲームID ゲーム識別子
   */
  constructor(ゲームID: string) {
    this.ゲームID = ゲームID;
    this.行動履歴内部 = [];
    this.行動IDカウンター内部 = 1;
    this.行動可能確認関数内部 = null;
  }

  /**
   * プライベートコンストラクタ用ヘルパー
   */
  private static 複製作成(
    ゲームID: string,
    行動履歴: readonly 行動情報[],
    カウンター: number,
     行動可能確認関数: 行動可能確認 | null
  ): 行動管理 {
    const インスタンス = new 行動管理(ゲームID);
    (インスタンス as any).行動履歴内部 = 行動履歴;
    (インスタンス as any).行動IDカウンター内部 = カウンター;
    (インスタンス as any).行動可能確認関数内部 = 行動可能確認関数;
    return インスタンス;
  }

  /**
   * 行動可能確認関数を設定
   * @param 確認関数 行動可能性をチェックする関数
   * @returns 更新された行動管理
   */
  行動可能確認を設定(確認関数: 行動可能確認 | null): 行動管理 {
    return 行動管理.複製作成(
      this.ゲームID,
      this.行動履歴内部,
      this.行動IDカウンター内部,
      確認関数
    );
  }

  /**
   * 行動を作成
   * @param 行動種類 行動の種類
   * @param 実行者ID 実行者のID
   * @param 対象情報 対象情報（オプション）
   * @param パラメータ パラメータ（オプション）
   * @returns 作成された行動情報と更新された行動管理のタプル
   */
  行動を作成(
    行動種類: string,
    実行者ID: string,
    対象情報?: unknown,
    パラメータ?: unknown
  ): [行動情報, 行動管理] {
    const 行動ID = `action_${this.ゲームID}_${this.行動IDカウンター内部}`;

    const 新しい行動: 行動情報 = {
      行動ID,
      行動種類,
      実行者ID,
      対象情報,
      パラメータ,
      状態: '未実行',
      作成日時: Date.now()
    };

    const 次のカウンター = this.行動IDカウンター内部 + 1;
    const 更新された管理 = 行動管理.複製作成(
      this.ゲームID,
      this.行動履歴内部,
      次のカウンター,
      this.行動可能確認関数内部
    );

    return [新しい行動, 更新された管理];
  }

  /**
   * 行動を登録
   * @param 行動 登録する行動
   * @returns 更新された行動管理
   */
  行動を登録(行動: 行動情報): 行動管理 {
    const 新しい履歴 = [...this.行動履歴内部, 行動];
    const 次のカウンター = this.行動IDカウンター内部 + 1;

    return 行動管理.複製作成(
      this.ゲームID,
      新しい履歴,
      次のカウンター,
      this.行動可能確認関数内部
    );
  }

  /**
   * 行動の状態を更新
   * @param 行動ID 対象の行動ID
   * @param 新しい状態 新しい状態
   * @returns 更新された行動管理
   */
  行動の状態を更新(行動ID: string, 新しい状態: 行動状態): 行動管理 {
    const 行動インデックス = this.行動履歴内部.findIndex(a => a.行動ID === 行動ID);
    if (行動インデックス === -1) {
      throw new Error(`行動「${行動ID}」は登録されていません`);
    }

    const 対象行動 = this.行動履歴内部[行動インデックス];
    if (対象行動.状態 === 新しい状態) {
      // 状態が同じ場合は変更しない
      return this;
    }

    const 更新行動: 行動情報 = {
      行動ID: 対象行動.行動ID,
      行動種類: 対象行動.行動種類,
      実行者ID: 対象行動.実行者ID,
      対象情報: 対象行動.対象情報,
      パラメータ: 対象行動.パラメータ,
      状態: 新しい状態,
      作成日時: 対象行動.作成日時
    };

    const 新しい履歴 = [...this.行動履歴内部];
    新しい履歴[行動インデックス] = 更新行動;

    return 行動管理.複製作成(
      this.ゲームID,
      新しい履歴,
      this.行動IDカウンター内部,
      this.行動可能確認関数内部
    );
  }

  /**
   * 行動が可能かチェック
   * @param 行動 チェック対象の行動
   * @returns チェック結果
   */
  行動が可能か(行動: 行動情報): 行動可能性チェック結果 {
    if (!this.行動可能確認関数内部) {
      // 確認関数が設定されていない場合は常に可能
      return { 可能: true };
    }

    return this.行動可能確認関数内部(行動);
  }

  /**
   * 行動を実行
   * @param 行動 実行する行動
   * @returns 実行結果と更新された行動管理のタプル
   */
  行動を実行(行動: 行動情報): [行動管理, { 実行可能: boolean; 理由?: string }] {
    // 行動が既に登録されているかチェック
    const 既存行動 = this.行動履歴内部.find(a => a.行動ID === 行動.行動ID);
    let 管理: 行動管理 = this;

    if (!既存行動) {
      // 行動が未登録の場合は登録
      管理 = this.行動を登録(行動);
    }

    // 行動可能性をチェック
    const チェック結果 = 管理.行動が可能か(行動);

    if (!チェック結果.可能) {
      // 実行できない場合は失敗状態に
      管理 = 管理.行動の状態を更新(行動.行動ID, '失敗');
      return [管理, { 実行可能: false, 理由: チェック結果.理由 }];
    }

    // 実行可能な場合は実行中状態に
    管理 = 管理.行動の状態を更新(行動.行動ID, '実行中');

    return [管理, { 実行可能: true }];
  }

  /**
   * 行動を成功で完了
   * @param 行動ID 対象の行動ID
   * @returns 更新された行動管理
   */
  行動を成功で完了(行動ID: string): 行動管理 {
    return this.行動の状態を更新(行動ID, '成功');
  }

  /**
   * 行動を失敗で完了
   * @param 行動ID 対象の行動ID
   * @returns 更新された行動管理
   */
  行動を失敗で完了(行動ID: string): 行動管理 {
    return this.行動の状態を更新(行動ID, '失敗');
  }

  /**
   * 行動をキャンセル
   * @param 行動ID 対象の行動ID
   * @returns 更新された行動管理
   */
  行動をキャンセル(行動ID: string): 行動管理 {
    return this.行動の状態を更新(行動ID, 'キャンセル');
  }

  /**
   * 行動を取得
   * @param 行動ID 取得する行動ID
   * @returns 行動情報、存在しない場合はundefined
   */
  行動を取得(行動ID: string): 行動情報 | undefined {
    return this.行動履歴内部.find(a => a.行動ID === 行動ID);
  }

  /**
   * すべての行動を取得
   */
  get 行動履歴(): readonly 行動情報[] {
    return [...this.行動履歴内部];
  }

  /**
   * 最後の行動を取得
   */
  get 最後の行動(): 行動情報 | undefined {
    return this.行動履歴内部[this.行動履歴内部.length - 1];
  }

  /**
   * 指定状態の行動を取得
   * @param 状態 フィルタリング対象の状態
   */
  状態で行動を取得(状態: 行動状態): readonly 行動情報[] {
    return this.行動履歴内部.filter(a => a.状態 === 状態);
  }

  /**
   * 指定実行者の行動を取得
   * @param 実行者ID フィルタリング対象の実行者ID
   */
  実行者で行動を取得(実行者ID: string): readonly 行動情報[] {
    return this.行動履歴内部.filter(a => a.実行者ID === 実行者ID);
  }

  /**
   * 指定種類の行動を取得
   * @param 行動種類 フィルタリング対象の行動種類
   */
  種類で行動を取得(行動種類: string): readonly 行動情報[] {
    return this.行動履歴内部.filter(a => a.行動種類 === 行動種類);
  }

  /**
   * 行動数を取得
   */
  get 行動数(): number {
    return this.行動履歴内部.length;
  }

  /**
   * リセット
   * @returns リセットされた行動管理
   */
  リセット(): 行動管理 {
    return new 行動管理(this.ゲームID);
  }
}
