/**
 * ゲーム基盤層：カード能力システム
 *
 * カード能力を管理し、能力の発動要求を生成する
 * 役割：「カード能力を定義・管理し、条件に応じて効果生成情報を提供する」
 *
 * 担当する責務：
 * - 能力定義の管理（能力IDで参照）
 * - 能力インスタンスの登録
 * - 能力の検索と取得
 * - 起動能力の発動要求生成
 * - 誘発能力のイベント入口提供
 * - 常在能力の保存
 * - 条件確認の委譲
 *
 * 担当しない責務：
 * - 能力定義データの保存（カード管理）
 * - 条件の判定（条件管理）
 * - 効果の生成（効果解決）
 * - 効果の実行（効果解決）
 * - 対象の選択（対象管理）
 * - ゾーン操作（ゾーン管理）
 * - 数値計算（数値管理）
 */

/**
 * 効果生成情報
 * 能力が生成する効果のメタ情報
 */
export interface 効果生成情報 {
  readonly 効果種類: string;
  readonly パラメータ?: unknown;
  readonly メタデータ?: Record<string, unknown>;
}

/**
 * 能力状態
 */
export type 能力状態 = '未発動' | '待機中' | '発動要求' | '解決中' | '成功' | '失敗' | 'キャンセル';

/**
 * 能力種類
 */
export type 能力種類 = '起動能力' | '誘発能力' | '常在能力';

/**
 * 条件確認インターフェース
 * ゲーム側で実装する条件確認ロジック
 */
export interface 能力条件確認 {
  (能力ID: string, パラメータ?: unknown): { 満たす: boolean; 理由?: string };
}

/**
 * イベント情報
 * 誘発能力がリッスンするイベント
 */
export interface イベント情報 {
  readonly イベント種類: string;
  readonly 発生元ID: string;
  readonly パラメータ?: unknown;
  readonly メタデータ?: Record<string, unknown>;
}

/**
 * 能力情報
 * カード能力のインスタンス情報
 */
export interface 能力情報 {
  readonly 能力インスタンスID: string;
  readonly 能力ID: string;
  readonly カードID: string;
  readonly 能力種類: 能力種類;
  readonly 効果生成情報: 効果生成情報;
  readonly 状態: 能力状態;
  readonly 作成日時: number;
}

/**
 * 発動要求情報
 * 能力から生成された効果生成要求
 */
export interface 発動要求情報 {
  readonly 発動要求ID: string;
  readonly 能力インスタンスID: string;
  readonly 能力ID: string;
  readonly カードID: string;
  readonly 能力種類: 能力種類;
  readonly 効果生成情報: 効果生成情報;
  readonly 作成日時: number;
}

/**
 * カード能力管理
 *
 * カード能力の登録・検索・発動要求生成を管理
 * イミュータブル設計：すべての操作が新規インスタンスを返す
 */
export class カード能力管理 {
  readonly ゲームID: string;
  private readonly 能力レジストリ内部: readonly 能力情報[];
  private readonly 発動要求IDカウンター内部: number;
  private readonly 能力インスタンスIDカウンター内部: number;
  private readonly 条件確認関数内部: 能力条件確認 | null;
  private readonly 誘発能力検索関数内部: ((イベント: イベント情報) => readonly 能力情報[]) | null;

  /**
   * コンストラクタ
   * @param ゲームID ゲーム識別子
   */
  constructor(ゲームID: string) {
    this.ゲームID = ゲームID;
    this.能力レジストリ内部 = [];
    this.発動要求IDカウンター内部 = 1;
    this.能力インスタンスIDカウンター内部 = 1;
    this.条件確認関数内部 = null;
    this.誘発能力検索関数内部 = null;
  }

  /**
   * プライベートコンストラクタ用ヘルパー
   */
  private static 複製作成(
    ゲームID: string,
    能力レジストリ: readonly 能力情報[],
    発動要求IDカウンター: number,
    能力インスタンスIDカウンター: number,
    条件確認関数: 能力条件確認 | null,
    誘発能力検索関数: ((イベント: イベント情報) => readonly 能力情報[]) | null
  ): カード能力管理 {
    const インスタンス = new カード能力管理(ゲームID);
    (インスタンス as any).能力レジストリ内部 = 能力レジストリ;
    (インスタンス as any).発動要求IDカウンター内部 = 発動要求IDカウンター;
    (インスタンス as any).能力インスタンスIDカウンター内部 = 能力インスタンスIDカウンター;
    (インスタンス as any).条件確認関数内部 = 条件確認関数;
    (インスタンス as any).誘発能力検索関数内部 = 誘発能力検索関数;
    return インスタンス;
  }

  /**
   * 条件確認関数を設定
   * @param 関数 条件確認を行う関数
   * @returns 更新されたカード能力管理
   */
  条件確認を設定(関数: 能力条件確認 | null): カード能力管理 {
    return カード能力管理.複製作成(
      this.ゲームID,
      this.能力レジストリ内部,
      this.発動要求IDカウンター内部,
      this.能力インスタンスIDカウンター内部,
      関数,
      this.誘発能力検索関数内部
    );
  }

  /**
   * 誘発能力検索関数を設定
   * @param 関数 イベントから誘発能力を検索する関数
   * @returns 更新されたカード能力管理
   */
  誘発能力検索を設定(関数: ((イベント: イベント情報) => readonly 能力情報[]) | null): カード能力管理 {
    return カード能力管理.複製作成(
      this.ゲームID,
      this.能力レジストリ内部,
      this.発動要求IDカウンター内部,
      this.能力インスタンスIDカウンター内部,
      this.条件確認関数内部,
      関数
    );
  }

  /**
   * 能力を登録
   * @param 能力ID 能力定義のID
   * @param カードID カードID
   * @param 能力種類 能力の種類
   * @param 効果生成情報 効果生成情報
   * @returns 登録された能力情報と更新されたカード能力管理のタプル
   */
  能力を登録(
    能力ID: string,
    カードID: string,
    能力種類: 能力種類,
    効果生成情報: 効果生成情報
  ): [能力情報, カード能力管理] {
    const 能力インスタンスID = `ability_${this.ゲームID}_${this.能力インスタンスIDカウンター内部}`;

    const 新しい能力: 能力情報 = {
      能力インスタンスID,
      能力ID,
      カードID,
      能力種類,
      効果生成情報,
      状態: '未発動',
      作成日時: Date.now()
    };

    const 次のカウンター = this.能力インスタンスIDカウンター内部 + 1;
    const 新しいレジストリ = [...this.能力レジストリ内部, 新しい能力];

    const 更新された管理 = カード能力管理.複製作成(
      this.ゲームID,
      新しいレジストリ,
      this.発動要求IDカウンター内部,
      次のカウンター,
      this.条件確認関数内部,
      this.誘発能力検索関数内部
    );

    return [新しい能力, 更新された管理];
  }

  /**
   * 能力の状態を更新
   * @param 能力インスタンスID 対象の能力インスタンスID
   * @param 新しい状態 新しい状態
   * @returns 更新されたカード能力管理
   */
  能力の状態を更新(能力インスタンスID: string, 新しい状態: 能力状態): カード能力管理 {
    const 能力インデックス = this.能力レジストリ内部.findIndex(a => a.能力インスタンスID === 能力インスタンスID);
    if (能力インデックス === -1) {
      throw new Error(`能力「${能力インスタンスID}」はレジストリに登録されていません`);
    }

    const 対象能力 = this.能力レジストリ内部[能力インデックス];
    if (対象能力.状態 === 新しい状態) {
      return this;
    }

    const 更新能力: 能力情報 = {
      能力インスタンスID: 対象能力.能力インスタンスID,
      能力ID: 対象能力.能力ID,
      カードID: 対象能力.カードID,
      能力種類: 対象能力.能力種類,
      効果生成情報: 対象能力.効果生成情報,
      状態: 新しい状態,
      作成日時: 対象能力.作成日時
    };

    const 新しいレジストリ = [...this.能力レジストリ内部];
    新しいレジストリ[能力インデックス] = 更新能力;

    return カード能力管理.複製作成(
      this.ゲームID,
      新しいレジストリ,
      this.発動要求IDカウンター内部,
      this.能力インスタンスIDカウンター内部,
      this.条件確認関数内部,
      this.誘発能力検索関数内部
    );
  }

  /**
   * 起動能力の発動要求を生成
   * @param 能力インスタンスID 能力インスタンスID
   * @returns 発動要求情報と更新されたカード能力管理のタプル
   */
  起動能力の発動要求を生成(能力インスタンスID: string): [発動要求情報, カード能力管理] {
    const 対象能力 = this.能力レジストリ内部.find(a => a.能力インスタンスID === 能力インスタンスID);
    if (!対象能力) {
      throw new Error(`能力「${能力インスタンスID}」が見つかりません`);
    }

    if (対象能力.能力種類 !== '起動能力') {
      throw new Error(`能力「${能力インスタンスID}」は起動能力ではありません`);
    }

    // 条件確認がある場合は実行
    if (this.条件確認関数内部) {
      const 条件確認関数 = this.条件確認関数内部;
      const 条件結果 = 条件確認関数(対象能力.能力ID, 対象能力.効果生成情報.パラメータ);
      if (!条件結果.満たす) {
        throw new Error(`能力「${能力インスタンスID}」の条件が満たされていません: ${条件結果.理由 || '不明'}`);
      }
    }

    const 発動要求ID = `activation_${this.ゲームID}_${this.発動要求IDカウンター内部}`;
    const 次のカウンター = this.発動要求IDカウンター内部 + 1;

    const 新しい発動要求: 発動要求情報 = {
      発動要求ID,
      能力インスタンスID: 対象能力.能力インスタンスID,
      能力ID: 対象能力.能力ID,
      カードID: 対象能力.カードID,
      能力種類: 対象能力.能力種類,
      効果生成情報: 対象能力.効果生成情報,
      作成日時: Date.now()
    };

    // 能力の状態を発動要求中に
    let 更新された管理 = this.能力の状態を更新(能力インスタンスID, '発動要求');

    // 発動要求IDカウンターを更新
    更新された管理 = カード能力管理.複製作成(
      更新された管理.ゲームID,
      更新された管理.能力レジストリ内部,
      次のカウンター,
      更新された管理.能力インスタンスIDカウンター内部,
      更新された管理.条件確認関数内部,
      更新された管理.誘発能力検索関数内部
    );

    return [新しい発動要求, 更新された管理];
  }

  /**
   * イベント通知を受け取り、該当する誘発能力の発動要求を生成
   * @param イベント イベント情報
   * @returns 生成された発動要求情報の配列と更新されたカード能力管理のタプル
   */
  イベント通知を受け取る(イベント: イベント情報): [readonly 発動要求情報[], カード能力管理] {
    // 誘発能力検索関数がない場合は空配列を返す
    if (!this.誘発能力検索関数内部) {
      return [[], this];
    }

    const 検索関数 = this.誘発能力検索関数内部;
    const 対象能力一覧 = 検索関数(イベント);

    const 発動要求一覧: 発動要求情報[] = [];
    let 管理: カード能力管理 = this;

    for (const 対象能力 of 対象能力一覧) {
      // 条件確認がある場合は実行
      if (管理.条件確認関数内部) {
        const 条件確認関数 = 管理.条件確認関数内部;
        const 条件結果 = 条件確認関数(対象能力.能力ID, 対象能力.効果生成情報.パラメータ);
        if (!条件結果.満たす) {
          continue;
        }
      }

      const 発動要求ID = `activation_${管理.ゲームID}_${管理.発動要求IDカウンター内部}`;
      const 新しい発動要求: 発動要求情報 = {
        発動要求ID,
        能力インスタンスID: 対象能力.能力インスタンスID,
        能力ID: 対象能力.能力ID,
        カードID: 対象能力.カードID,
        能力種類: 対象能力.能力種類,
        効果生成情報: 対象能力.効果生成情報,
        作成日時: Date.now()
      };

      発動要求一覧.push(新しい発動要求);

      // 能力の状態を発動要求中に
      管理 = 管理.能力の状態を更新(対象能力.能力インスタンスID, '発動要求');

      // カウンターを進める
      const 次のカウンター = 管理.発動要求IDカウンター内部 + 1;
      管理 = カード能力管理.複製作成(
        管理.ゲームID,
        管理.能力レジストリ内部,
        次のカウンター,
        管理.能力インスタンスIDカウンター内部,
        管理.条件確認関数内部,
        管理.誘発能力検索関数内部
      );
    }

    return [発動要求一覧, 管理];
  }

  /**
   * 能力を取得
   * @param 能力インスタンスID 取得する能力インスタンスID
   * @returns 能力情報、存在しない場合はundefined
   */
  能力を取得(能力インスタンスID: string): 能力情報 | undefined {
    return this.能力レジストリ内部.find(a => a.能力インスタンスID === 能力インスタンスID);
  }

  /**
   * すべての能力を取得
   */
  get 能力一覧(): readonly 能力情報[] {
    return [...this.能力レジストリ内部];
  }

  /**
   * 指定カードの能力を取得
   * @param カードID カードID
   */
  カードの能力を取得(カードID: string): readonly 能力情報[] {
    return this.能力レジストリ内部.filter(a => a.カードID === カードID);
  }

  /**
   * 指定能力IDの能力を取得
   * @param 能力ID 能力ID
   */
  能力IDで検索(能力ID: string): readonly 能力情報[] {
    return this.能力レジストリ内部.filter(a => a.能力ID === 能力ID);
  }

  /**
   * 指定種類の能力を取得
   * @param 能力種類 能力種類
   */
  種類で検索(能力種類: 能力種類): readonly 能力情報[] {
    return this.能力レジストリ内部.filter(a => a.能力種類 === 能力種類);
  }

  /**
   * 指定状態の能力を取得
   * @param 状態 能力状態
   */
  状態で検索(状態: 能力状態): readonly 能力情報[] {
    return this.能力レジストリ内部.filter(a => a.状態 === 状態);
  }

  /**
   * 能力数を取得
   */
  get 能力数(): number {
    return this.能力レジストリ内部.length;
  }

  /**
   * リセット
   * @returns リセットされたカード能力管理
   */
  リセット(): カード能力管理 {
    return new カード能力管理(this.ゲームID);
  }
}
