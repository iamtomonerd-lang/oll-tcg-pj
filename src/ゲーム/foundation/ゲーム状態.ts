/**
 * ゲーム基盤層：ゲーム状態管理
 *
 * ゲーム全体の状態を管理する基盤クラス
 * イミュータブル設計で操作の追跡を可能にする
 *
 * 注意：
 * 汎用パーツとの統合は別途実装
 * ここでは基本的なゲーム状態管理の骨組みを実装
 */

import type {
  ゲーム状態,
  ゲーム設定,
  ゲーム操作履歴
} from './型';

/**
 * プレイヤー状態
 */
interface プレイヤー状態 {
  readonly ID: string;
  readonly 名前: string;
  readonly ライフ: number;
}

/**
 * ゲーム状態管理クラス
 *
 * 責務：
 * - ゲーム全体の状態を管理
 * - プレイヤー情報の管理
 * - ターンシステムの進行
 * - 基本的なゲームフロー
 *
 * 設計：
 * - イミュータブル：すべての操作が新規インスタンスを返す
 * - 汎用パーツ統合予定：将来的にプレイヤー管理・ゾーン管理などと統合
 */
export class ゲーム状態管理 {
  readonly ゲームID: string;
  private readonly 状態内部: ゲーム状態;
  private readonly 設定内部: ゲーム設定;
  private readonly ターン番号内部: number;
  private readonly 現在のプレイヤーIndex内部: number;
  private readonly 現在のフェイズ内部: '開始' | 'メイン1' | '攻撃' | 'メイン2' | '終了';
  private readonly プレイヤー一覧内部: プレイヤー状態[];
  private readonly 操作履歴: ゲーム操作履歴[];

  /**
   * コンストラクタ
   */
  constructor(
    ゲームID: string,
    設定: ゲーム設定
  ) {
    this.ゲームID = ゲームID;
    this.状態内部 = '初期化前';
    this.設定内部 = 設定;
    this.ターン番号内部 = 0;
    this.現在のプレイヤーIndex内部 = 0;
    this.現在のフェイズ内部 = '開始';
    this.プレイヤー一覧内部 = [];
    this.操作履歴 = [];
  }

  /**
   * プライベートコンストラクタ用ヘルパー
   */
  private static 複製作成(
    ゲームID: string,
    状態: ゲーム状態,
    設定: ゲーム設定,
    ターン: number,
    プレイヤーIndex: number,
    フェイズ: '開始' | 'メイン1' | '攻撃' | 'メイン2' | '終了',
    プレイヤー一覧: プレイヤー状態[],
    履歴: ゲーム操作履歴[]
  ): ゲーム状態管理 {
    const インスタンス = new ゲーム状態管理(ゲームID, 設定);
    (インスタンス as any).状態内部 = 状態;
    (インスタンス as any).ターン番号内部 = ターン;
    (インスタンス as any).現在のプレイヤーIndex内部 = プレイヤーIndex;
    (インスタンス as any).現在のフェイズ内部 = フェイズ;
    (インスタンス as any).プレイヤー一覧内部 = プレイヤー一覧;
    (インスタンス as any).操作履歴 = 履歴;
    return インスタンス;
  }

  /**
   * ゲーム状態を取得
   */
  get 状態(): ゲーム状態 {
    return this.状態内部;
  }

  /**
   * ターン番号を取得
   */
  get ターン番号(): number {
    return this.ターン番号内部;
  }

  /**
   * 現在のフェイズを取得
   */
  get 現在のフェイズ(): '開始' | 'メイン1' | '攻撃' | 'メイン2' | '終了' {
    return this.現在のフェイズ内部;
  }

  /**
   * プレイヤー数を取得
   */
  get プレイヤー数(): number {
    return this.プレイヤー一覧内部.length;
  }

  /**
   * 現在のプレイヤーIDを取得
   */
  get 現在のプレイヤーID(): string | null {
    if (this.プレイヤー一覧内部.length === 0) {
      return null;
    }
    return this.プレイヤー一覧内部[this.現在のプレイヤーIndex内部].ID;
  }

  /**
   * ゲームを初期化
   * - プレイヤーを生成
   * - ライフを設定
   */
  ゲームを初期化(): ゲーム状態管理 {
    if (this.状態内部 !== '初期化前') {
      throw new Error('既に初期化済みのゲームです');
    }

    const 新しいプレイヤー一覧: プレイヤー状態[] = [];

    for (let i = 0; i < this.設定内部.プレイヤー数; i++) {
      新しいプレイヤー一覧.push({
        ID: `player_${i + 1}`,
        名前: `プレイヤー${i + 1}`,
        ライフ: this.設定内部.初期ライフ
      });
    }

    const 新しい管理 = ゲーム状態管理.複製作成(
      this.ゲームID,
      'プレイ中',
      this.設定内部,
      1,
      0,
      '開始',
      新しいプレイヤー一覧,
      [...this.操作履歴]
    );

    新しい管理.操作履歴.push({
      種類: '初期化',
      内容: { プレイヤー数: this.設定内部.プレイヤー数 },
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * ターンを開始
   */
  ターンを開始(): ゲーム状態管理 {
    if (this.状態内部 !== 'プレイ中') {
      throw new Error('ゲームがプレイ中ではありません');
    }

    const 新しい管理 = ゲーム状態管理.複製作成(
      this.ゲームID,
      this.状態内部,
      this.設定内部,
      this.ターン番号内部,
      this.現在のプレイヤーIndex内部,
      'メイン1',
      this.プレイヤー一覧内部,
      [...this.操作履歴]
    );

    新しい管理.操作履歴.push({
      種類: 'ターン開始',
      プレイヤーID: this.現在のプレイヤーID || undefined,
      内容: { ターン番号: this.ターン番号内部 },
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * フェイズを移動
   */
  フェイズを移動(): ゲーム状態管理 {
    const フェイズ順序: ('開始' | 'メイン1' | '攻撃' | 'メイン2' | '終了')[] = [
      '開始',
      'メイン1',
      '攻撃',
      'メイン2',
      '終了'
    ];

    const 現在のインデックス = フェイズ順序.indexOf(this.現在のフェイズ内部);
    const 次のインデックス = (現在のインデックス + 1) % フェイズ順序.length;
    const 次のフェイズ = フェイズ順序[次のインデックス];

    const 新しい管理 = ゲーム状態管理.複製作成(
      this.ゲームID,
      this.状態内部,
      this.設定内部,
      this.ターン番号内部,
      this.現在のプレイヤーIndex内部,
      次のフェイズ,
      this.プレイヤー一覧内部,
      [...this.操作履歴]
    );

    新しい管理.操作履歴.push({
      種類: 'フェイズ移動',
      プレイヤーID: this.現在のプレイヤーID || undefined,
      内容: { 前のフェイズ: this.現在のフェイズ内部, 次のフェイズ },
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * ターンを終了（プレイヤーを交代）
   */
  ターンを終了(): ゲーム状態管理 {
    const 次のプレイヤーIndex = (this.現在のプレイヤーIndex内部 + 1) % this.プレイヤー一覧内部.length;
    const 次のターン = this.現在のプレイヤーIndex内部 === this.プレイヤー一覧内部.length - 1
      ? this.ターン番号内部 + 1
      : this.ターン番号内部;

    const 新しい管理 = ゲーム状態管理.複製作成(
      this.ゲームID,
      this.状態内部,
      this.設定内部,
      次のターン,
      次のプレイヤーIndex,
      '開始',
      this.プレイヤー一覧内部,
      [...this.操作履歴]
    );

    新しい管理.操作履歴.push({
      種類: 'ターン終了',
      プレイヤーID: this.現在のプレイヤーID || undefined,
      内容: { ターン番号: this.ターン番号内部 },
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * プレイヤーのライフを変更
   */
  ライフを変更(プレイヤーID: string, 差分: number): ゲーム状態管理 {
    const プレイヤーIndex = this.プレイヤー一覧内部.findIndex(p => p.ID === プレイヤーID);
    if (プレイヤーIndex === -1) {
      throw new Error(`プレイヤー「${プレイヤーID}」が見つかりません`);
    }

    const 新しいプレイヤー一覧 = [...this.プレイヤー一覧内部];
    const 現在のプレイヤー = 新しいプレイヤー一覧[プレイヤーIndex];
    新しいプレイヤー一覧[プレイヤーIndex] = {
      ...現在のプレイヤー,
      ライフ: 現在のプレイヤー.ライフ + 差分
    };

    const 新しい管理 = ゲーム状態管理.複製作成(
      this.ゲームID,
      this.状態内部,
      this.設定内部,
      this.ターン番号内部,
      this.現在のプレイヤーIndex内部,
      this.現在のフェイズ内部,
      新しいプレイヤー一覧,
      [...this.操作履歴]
    );

    新しい管理.操作履歴.push({
      種類: 'その他',
      プレイヤーID,
      内容: { ライフ変更: 差分, 新しいライフ: 新しいプレイヤー一覧[プレイヤーIndex].ライフ },
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * プレイヤーのライフを取得
   */
  ライフを取得(プレイヤーID: string): number {
    const プレイヤー = this.プレイヤー一覧内部.find(p => p.ID === プレイヤーID);
    if (!プレイヤー) {
      throw new Error(`プレイヤー「${プレイヤーID}」が見つかりません`);
    }
    return プレイヤー.ライフ;
  }

  /**
   * ゲームを終了
   */
  ゲームを終了(勝者ID: string | null, 理由: string): ゲーム状態管理 {
    const 新しい管理 = ゲーム状態管理.複製作成(
      this.ゲームID,
      '終了',
      this.設定内部,
      this.ターン番号内部,
      this.現在のプレイヤーIndex内部,
      this.現在のフェイズ内部,
      this.プレイヤー一覧内部,
      [...this.操作履歴]
    );

    新しい管理.操作履歴.push({
      種類: 'ゲーム終了',
      内容: { 勝者ID, 理由 },
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * 操作履歴を取得
   */
  履歴を取得(): readonly ゲーム操作履歴[] {
    return [...this.操作履歴];
  }
}
