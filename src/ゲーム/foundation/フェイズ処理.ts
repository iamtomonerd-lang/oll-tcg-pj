/**
 * ゲーム基盤層：フェイズ処理システム
 *
 * 現在のフェイズに応じた処理の登録・管理・実行を担当
 * 役割：「フェイズ内で何が実行されるかを管理する」
 *
 * 担当する責務：
 * - フェイズ定義管理
 * - フェイズ順序管理
 * - フェイズ開始処理
 * - フェイズ中処理
 * - フェイズ終了処理
 * - 処理登録
 * - 登録された処理の実行順管理
 *
 * 担当しない責務：
 * - ターン番号管理（ターン管理）
 * - 現在プレイヤー管理（ターン管理）
 * - カード使用処理（カードエンジン）
 * - 攻撃処理（攻撃エンジン）
 * - 勝利判定（勝利判定パーツ）
 * - コスト処理（ゲームエンジン）
 * - ドロー処理そのもの（カード管理パーツ）
 */

/**
 * フェイズ処理
 * フェイズに登録する処理単位
 */
export interface フェイズ処理 {
  readonly 名前: string;
  実行(): void;
}

/**
 * フェイズ定義
 */
interface フェイズ定義 {
  readonly 名前: string;
  readonly 順番: number;
  readonly 開始処理一覧: readonly フェイズ処理[];
  readonly 中間処理一覧: readonly フェイズ処理[];
  readonly 終了処理一覧: readonly フェイズ処理[];
}

/**
 * フェイズ処理システム
 *
 * フェイズ内の処理管理と実行を担当
 * イミュータブル設計：すべての操作が新規インスタンスを返す
 */
export class フェイズ処理システム {
  readonly ゲームID: string;
  private readonly フェイズ一覧内部: readonly フェイズ定義[];
  private readonly 現在フェイズ名内部: string | null;

  /**
   * コンストラクタ
   * @param ゲームID ゲーム識別子
   */
  constructor(ゲームID: string) {
    this.ゲームID = ゲームID;
    this.フェイズ一覧内部 = [];
    this.現在フェイズ名内部 = null;
  }

  /**
   * プライベートコンストラクタ用ヘルパー
   */
  private static 複製作成(
    ゲームID: string,
    フェイズ一覧: readonly フェイズ定義[],
     現在フェイズ名: string | null
  ): フェイズ処理システム {
    const インスタンス = new フェイズ処理システム(ゲームID);
    (インスタンス as any).フェイズ一覧内部 = フェイズ一覧;
    (インスタンス as any).現在フェイズ名内部 = 現在フェイズ名;
    return インスタンス;
  }

  /**
   * フェイズを登録
   * @param フェイズ名 フェイズ名
   * @param 順番 フェイズの順番
   * @returns 更新されたフェイズ処理システム
   */
  フェイズを登録(フェイズ名: string, 順番: number): フェイズ処理システム {
    // 既に登録されているか確認
    if (this.フェイズ一覧内部.some(p => p.名前 === フェイズ名)) {
      // 既に登録されている場合は変更しない
      return this;
    }

    const 新しいフェイズ定義: フェイズ定義 = {
      名前: フェイズ名,
      順番,
      開始処理一覧: [],
      中間処理一覧: [],
      終了処理一覧: []
    };

    const 新しいフェイズ一覧 = [...this.フェイズ一覧内部, 新しいフェイズ定義];

    return フェイズ処理システム.複製作成(
      this.ゲームID,
      新しいフェイズ一覧,
      this.現在フェイズ名内部
    );
  }

  /**
   * フェイズを取得
   * @param フェイズ名 取得するフェイズ名
   * @returns フェイズ定義、存在しない場合はundefined
   */
  フェイズを取得(フェイズ名: string): Readonly<フェイズ定義> | undefined {
    return this.フェイズ一覧内部.find(p => p.名前 === フェイズ名);
  }

  /**
   * すべてのフェイズを取得
   */
  get フェイズ一覧(): readonly Readonly<フェイズ定義>[] {
    return [...this.フェイズ一覧内部];
  }

  /**
   * 現在フェイズを取得
   */
  get 現在フェイズ名(): string | null {
    return this.現在フェイズ名内部;
  }

  /**
   * フェイズが登録されているか確認
   */
  はフェイズが登録されているか(フェイズ名: string): boolean {
    return this.フェイズ一覧内部.some(p => p.名前 === フェイズ名);
  }

  /**
   * 開始処理を登録
   * @param フェイズ名 処理を追加するフェイズ名
   * @param 処理 追加する処理
   * @returns 更新されたフェイズ処理システム
   */
  開始処理を登録(フェイズ名: string, 処理: フェイズ処理): フェイズ処理システム {
    const フェイズIndex = this.フェイズ一覧内部.findIndex(p => p.名前 === フェイズ名);
    if (フェイズIndex === -1) {
      throw new Error(`フェイズ「${フェイズ名}」は登録されていません`);
    }

    const 対象フェイズ = this.フェイズ一覧内部[フェイズIndex];
    const 更新フェイズ: フェイズ定義 = {
      名前: 対象フェイズ.名前,
      順番: 対象フェイズ.順番,
      開始処理一覧: [...対象フェイズ.開始処理一覧, 処理],
      中間処理一覧: 対象フェイズ.中間処理一覧,
      終了処理一覧: 対象フェイズ.終了処理一覧
    };

    const 新しいフェイズ一覧 = [...this.フェイズ一覧内部];
    新しいフェイズ一覧[フェイズIndex] = 更新フェイズ;

    return フェイズ処理システム.複製作成(
      this.ゲームID,
      新しいフェイズ一覧,
      this.現在フェイズ名内部
    );
  }

  /**
   * 中間処理を登録
   * @param フェイズ名 処理を追加するフェイズ名
   * @param 処理 追加する処理
   * @returns 更新されたフェイズ処理システム
   */
  中間処理を登録(フェイズ名: string, 処理: フェイズ処理): フェイズ処理システム {
    const フェイズIndex = this.フェイズ一覧内部.findIndex(p => p.名前 === フェイズ名);
    if (フェイズIndex === -1) {
      throw new Error(`フェイズ「${フェイズ名}」は登録されていません`);
    }

    const 対象フェイズ = this.フェイズ一覧内部[フェイズIndex];
    const 更新フェイズ: フェイズ定義 = {
      名前: 対象フェイズ.名前,
      順番: 対象フェイズ.順番,
      開始処理一覧: 対象フェイズ.開始処理一覧,
      中間処理一覧: [...対象フェイズ.中間処理一覧, 処理],
      終了処理一覧: 対象フェイズ.終了処理一覧
    };

    const 新しいフェイズ一覧 = [...this.フェイズ一覧内部];
    新しいフェイズ一覧[フェイズIndex] = 更新フェイズ;

    return フェイズ処理システム.複製作成(
      this.ゲームID,
      新しいフェイズ一覧,
      this.現在フェイズ名内部
    );
  }

  /**
   * 終了処理を登録
   * @param フェイズ名 処理を追加するフェイズ名
   * @param 処理 追加する処理
   * @returns 更新されたフェイズ処理システム
   */
  終了処理を登録(フェイズ名: string, 処理: フェイズ処理): フェイズ処理システム {
    const フェイズIndex = this.フェイズ一覧内部.findIndex(p => p.名前 === フェイズ名);
    if (フェイズIndex === -1) {
      throw new Error(`フェイズ「${フェイズ名}」は登録されていません`);
    }

    const 対象フェイズ = this.フェイズ一覧内部[フェイズIndex];
    const 更新フェイズ: フェイズ定義 = {
      名前: 対象フェイズ.名前,
      順番: 対象フェイズ.順番,
      開始処理一覧: 対象フェイズ.開始処理一覧,
      中間処理一覧: 対象フェイズ.中間処理一覧,
      終了処理一覧: [...対象フェイズ.終了処理一覧, 処理]
    };

    const 新しいフェイズ一覧 = [...this.フェイズ一覧内部];
    新しいフェイズ一覧[フェイズIndex] = 更新フェイズ;

    return フェイズ処理システム.複製作成(
      this.ゲームID,
      新しいフェイズ一覧,
      this.現在フェイズ名内部
    );
  }

  /**
   * フェイズを移動
   * @param 新しいフェイズ名 移動先フェイズ名
   * @returns 更新されたフェイズ処理システム
   */
  フェイズを移動(新しいフェイズ名: string): フェイズ処理システム {
    if (!this.はフェイズが登録されているか(新しいフェイズ名)) {
      throw new Error(`フェイズ「${新しいフェイズ名}」は登録されていません`);
    }

    if (this.現在フェイズ名内部 === 新しいフェイズ名) {
      return this;
    }

    return フェイズ処理システム.複製作成(
      this.ゲームID,
      this.フェイズ一覧内部,
      新しいフェイズ名
    );
  }

  /**
   * 開始処理を実行
   * @returns 実行結果
   */
  開始処理を実行(): { 実行した処理数: number } {
    if (!this.現在フェイズ名内部) {
      return { 実行した処理数: 0 };
    }

    const フェイズ = this.フェイズを取得(this.現在フェイズ名内部);
    if (!フェイズ) {
      return { 実行した処理数: 0 };
    }

    let 実行数 = 0;
    for (const 処理 of フェイズ.開始処理一覧) {
      処理.実行();
      実行数++;
    }

    return { 実行した処理数: 実行数 };
  }

  /**
   * 中間処理を実行
   * @returns 実行結果
   */
  中間処理を実行(): { 実行した処理数: number } {
    if (!this.現在フェイズ名内部) {
      return { 実行した処理数: 0 };
    }

    const フェイズ = this.フェイズを取得(this.現在フェイズ名内部);
    if (!フェイズ) {
      return { 実行した処理数: 0 };
    }

    let 実行数 = 0;
    for (const 処理 of フェイズ.中間処理一覧) {
      処理.実行();
      実行数++;
    }

    return { 実行した処理数: 実行数 };
  }

  /**
   * 終了処理を実行
   * @returns 実行結果
   */
  終了処理を実行(): { 実行した処理数: number } {
    if (!this.現在フェイズ名内部) {
      return { 実行した処理数: 0 };
    }

    const フェイズ = this.フェイズを取得(this.現在フェイズ名内部);
    if (!フェイズ) {
      return { 実行した処理数: 0 };
    }

    let 実行数 = 0;
    for (const 処理 of フェイズ.終了処理一覧) {
      処理.実行();
      実行数++;
    }

    return { 実行した処理数: 実行数 };
  }

  /**
   * すべての処理を実行（開始→中間→終了）
   * @returns 実行結果
   */
  すべての処理を実行(): { 実行した処理数: number; 実行順序: string[] } {
    const 実行順序: string[] = [];
    let 合計実行数 = 0;

    // 開始処理
    const 開始結果 = this.開始処理を実行();
    合計実行数 += 開始結果.実行した処理数;
    if (開始結果.実行した処理数 > 0) {
      実行順序.push('開始処理');
    }

    // 中間処理
    const 中間結果 = this.中間処理を実行();
    合計実行数 += 中間結果.実行した処理数;
    if (中間結果.実行した処理数 > 0) {
      実行順序.push('中間処理');
    }

    // 終了処理
    const 終了結果 = this.終了処理を実行();
    合計実行数 += 終了結果.実行した処理数;
    if (終了結果.実行した処理数 > 0) {
      実行順序.push('終了処理');
    }

    return { 実行した処理数: 合計実行数, 実行順序 };
  }

  /**
   * リセット
   * @returns リセットされたフェイズ処理システム
   */
  リセット(): フェイズ処理システム {
    return フェイズ処理システム.複製作成(
      this.ゲームID,
      [],
      null
    );
  }
}
