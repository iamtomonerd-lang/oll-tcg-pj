/**
 * ゲーム基盤層：効果解決システム
 *
 * ゲーム中に発生した効果を管理し、順番に解決する
 * 役割：「効果を処理可能な単位に分解し、正しい順序で実行する」
 *
 * 担当する責務：
 * - 効果受付
 * - 効果の順序管理
 * - 効果の実行管理
 * - 効果状態管理
 * - 効果解決待ち管理
 * - 複数効果処理
 *
 * 担当しない責務：
 * - カードデータ管理（カード管理）
 * - カード文章解析（カード管理）
 * - コスト計算（ゲームエンジン）
 * - ダメージ計算（ゲームエンジン）
 * - 勝利判定（勝利判定パーツ）
 * - 対象選択そのもの（対象管理）
 * - 数値変更そのもの（数値管理）
 * - ゾーン操作そのもの（ゾーン管理）
 * - 条件確認（条件管理）
 */

/**
 * 効果状態
 */
export type 効果状態 = '未解決' | '解決中' | '成功' | '失敗' | 'キャンセル';

/**
 * 効果情報
 */
export interface 効果情報 {
  readonly 効果ID: string;
  readonly 効果種類: string;
  readonly 発生元ID: string;
  readonly 対象情報?: unknown;
  readonly パラメータ?: unknown;
  readonly 状態: 効果状態;
  readonly 作成日時: number;
}

/**
 * 効果実行インターフェース
 * 実際の効果処理を実行する外部システムに委譲する際のコールバック
 */
export interface 効果実行関数 {
  (効果: 効果情報): { 成功: boolean; メッセージ?: string };
}

/**
 * 置換確認インターフェース
 * 効果結果を置換する条件を確認する外部システム
 */
export interface 置換確認関数 {
  (効果: 効果情報, 元の結果: { 成功: boolean; メッセージ?: string }): { 成功: boolean; メッセージ?: string };
}

/**
 * 効果解決システム
 *
 * 効果を管理し、キュー順に解決する
 * イミュータブル設計：すべての操作が新規インスタンスを返す
 */
export class 効果解決システム {
  readonly ゲームID: string;
  private readonly 効果キュー内部: readonly 効果情報[];
  private readonly 効果IDカウンター内部: number;
  private readonly 効果実行関数内部: 効果実行関数 | null;
  private readonly 置換確認関数内部: 置換確認関数 | null;

  /**
   * コンストラクタ
   * @param ゲームID ゲーム識別子
   */
  constructor(ゲームID: string) {
    this.ゲームID = ゲームID;
    this.効果キュー内部 = [];
    this.効果IDカウンター内部 = 1;
    this.効果実行関数内部 = null;
    this.置換確認関数内部 = null;
  }

  /**
   * プライベートコンストラクタ用ヘルパー
   */
  private static 複製作成(
    ゲームID: string,
    効果キュー: readonly 効果情報[],
    カウンター: number,
    効果実行関数: 効果実行関数 | null,
    置換確認関数: 置換確認関数 | null
  ): 効果解決システム {
    const インスタンス = new 効果解決システム(ゲームID);
    (インスタンス as any).効果キュー内部 = 効果キュー;
    (インスタンス as any).効果IDカウンター内部 = カウンター;
    (インスタンス as any).効果実行関数内部 = 効果実行関数;
    (インスタンス as any).置換確認関数内部 = 置換確認関数;
    return インスタンス;
  }

  /**
   * 効果実行関数を設定
   * @param 関数 効果を実行する関数
   * @returns 更新された効果解決システム
   */
  効果実行関数を設定(関数: 効果実行関数 | null): 効果解決システム {
    return 効果解決システム.複製作成(
      this.ゲームID,
      this.効果キュー内部,
      this.効果IDカウンター内部,
      関数,
      this.置換確認関数内部
    );
  }

  /**
   * 置換確認関数を設定
   * @param 関数 置換を確認する関数
   * @returns 更新された効果解決システム
   */
  置換確認関数を設定(関数: 置換確認関数 | null): 効果解決システム {
    return 効果解決システム.複製作成(
      this.ゲームID,
      this.効果キュー内部,
      this.効果IDカウンター内部,
      this.効果実行関数内部,
      関数
    );
  }

  /**
   * 効果を生成
   * @param 効果種類 効果の種類
   * @param 発生元ID 発生元のID
   * @param 対象情報 対象情報（オプション）
   * @param パラメータ パラメータ（オプション）
   * @returns 生成された効果情報と更新されたシステムのタプル
   */
  効果を生成(
    効果種類: string,
    発生元ID: string,
    対象情報?: unknown,
    パラメータ?: unknown
  ): [効果情報, 効果解決システム] {
    const 効果ID = `effect_${this.ゲームID}_${this.効果IDカウンター内部}`;

    const 新しい効果: 効果情報 = {
      効果ID,
      効果種類,
      発生元ID,
      対象情報,
      パラメータ,
      状態: '未解決',
      作成日時: Date.now()
    };

    const 次のカウンター = this.効果IDカウンター内部 + 1;
    const 更新されたシステム = 効果解決システム.複製作成(
      this.ゲームID,
      this.効果キュー内部,
      次のカウンター,
      this.効果実行関数内部,
      this.置換確認関数内部
    );

    return [新しい効果, 更新されたシステム];
  }

  /**
   * 効果をキューに追加
   * @param 効果 追加する効果
   * @returns 更新された効果解決システム
   */
  効果をキューに追加(効果: 効果情報): 効果解決システム {
    const 新しいキュー = [...this.効果キュー内部, 効果];

    return 効果解決システム.複製作成(
      this.ゲームID,
      新しいキュー,
      this.効果IDカウンター内部,
      this.効果実行関数内部,
      this.置換確認関数内部
    );
  }

  /**
   * 効果の状態を更新
   * @param 効果ID 対象の効果ID
   * @param 新しい状態 新しい状態
   * @returns 更新された効果解決システム
   */
  効果の状態を更新(効果ID: string, 新しい状態: 効果状態): 効果解決システム {
    const 効果インデックス = this.効果キュー内部.findIndex(e => e.効果ID === 効果ID);
    if (効果インデックス === -1) {
      throw new Error(`効果「${効果ID}」はキューに登録されていません`);
    }

    const 対象効果 = this.効果キュー内部[効果インデックス];
    if (対象効果.状態 === 新しい状態) {
      return this;
    }

    const 更新効果: 効果情報 = {
      効果ID: 対象効果.効果ID,
      効果種類: 対象効果.効果種類,
      発生元ID: 対象効果.発生元ID,
      対象情報: 対象効果.対象情報,
      パラメータ: 対象効果.パラメータ,
      状態: 新しい状態,
      作成日時: 対象効果.作成日時
    };

    const 新しいキュー = [...this.効果キュー内部];
    新しいキュー[効果インデックス] = 更新効果;

    return 効果解決システム.複製作成(
      this.ゲームID,
      新しいキュー,
      this.効果IDカウンター内部,
      this.効果実行関数内部,
      this.置換確認関数内部
    );
  }

  /**
   * 効果を実行
   * @param 効果 実行する効果
   * @returns 実行結果と更新されたシステムのタプル
   */
  効果を実行(効果: 効果情報): [効果解決システム, { 成功: boolean; メッセージ?: string }] {
    // 効果がキューに登録されているかチェック
    const 既存効果 = this.効果キュー内部.find(e => e.効果ID === 効果.効果ID);
    let システム: 効果解決システム = this;

    if (!既存効果) {
      // 効果が未登録の場合はキューに追加
      システム = this.効果をキューに追加(効果);
    }

    // 効果を実行中状態に
    システム = システム.効果の状態を更新(効果.効果ID, '解決中');

    // 効果実行関数がない場合はスキップ
    if (!システム.効果実行関数内部) {
      システム = システム.効果の状態を更新(効果.効果ID, '成功');
      return [システム, { 成功: true }];
    }

    // 外部システムで効果を実行
    const 実行関数 = システム.効果実行関数内部;
    let 実行結果 = 実行関数(効果);

    // 置換確認があれば結果を置換
    if (システム.置換確認関数内部) {
      const 置換関数 = システム.置換確認関数内部;
      実行結果 = 置換関数(効果, 実行結果);
    }

    // 結果に応じて状態を更新
    const 最終状態 = 実行結果.成功 ? '成功' : '失敗';
    システム = システム.効果の状態を更新(効果.効果ID, 最終状態);

    return [システム, 実行結果];
  }

  /**
   * 効果を成功で完了
   * @param 効果ID 対象の効果ID
   * @returns 更新された効果解決システム
   */
  効果を成功で完了(効果ID: string): 効果解決システム {
    return this.効果の状態を更新(効果ID, '成功');
  }

  /**
   * 効果を失敗で完了
   * @param 効果ID 対象の効果ID
   * @returns 更新された効果解決システム
   */
  効果を失敗で完了(効果ID: string): 効果解決システム {
    return this.効果の状態を更新(効果ID, '失敗');
  }

  /**
   * 効果をキャンセル
   * @param 効果ID 対象の効果ID
   * @returns 更新された効果解決システム
   */
  効果をキャンセル(効果ID: string): 効果解決システム {
    return this.効果の状態を更新(効果ID, 'キャンセル');
  }

  /**
   * 効果を取得
   * @param 効果ID 取得する効果ID
   * @returns 効果情報、存在しない場合はundefined
   */
  効果を取得(効果ID: string): 効果情報 | undefined {
    return this.効果キュー内部.find(e => e.効果ID === 効果ID);
  }

  /**
   * すべての効果をキューから取得
   */
  get 効果キュー(): readonly 効果情報[] {
    return [...this.効果キュー内部];
  }

  /**
   * 最初の効果を取得（キューの先頭）
   */
  get 最初の効果(): 効果情報 | undefined {
    return this.効果キュー内部[0];
  }

  /**
   * 最後の効果を取得（キューの末尾）
   */
  get 最後の効果(): 効果情報 | undefined {
    return this.効果キュー内部[this.効果キュー内部.length - 1];
  }

  /**
   * 指定状態の効果を取得
   * @param 状態 フィルタリング対象の状態
   */
  状態で効果を取得(状態: 効果状態): readonly 効果情報[] {
    return this.効果キュー内部.filter(e => e.状態 === 状態);
  }

  /**
   * 指定発生元の効果を取得
   * @param 発生元ID フィルタリング対象の発生元ID
   */
  発生元で効果を取得(発生元ID: string): readonly 効果情報[] {
    return this.効果キュー内部.filter(e => e.発生元ID === 発生元ID);
  }

  /**
   * 指定種類の効果を取得
   * @param 効果種類 フィルタリング対象の効果種類
   */
  種類で効果を取得(効果種類: string): readonly 効果情報[] {
    return this.効果キュー内部.filter(e => e.効果種類 === 効果種類);
  }

  /**
   * 効果数を取得
   */
  get 効果数(): number {
    return this.効果キュー内部.length;
  }

  /**
   * キューの最初の効果を取り出す（FIFO）
   * @returns 取り出された効果と更新されたシステムのタプル
   */
  効果をキューから取り出す(): [効果情報 | undefined, 効果解決システム] {
    if (this.効果キュー内部.length === 0) {
      return [undefined, this];
    }

    const 取り出した効果 = this.効果キュー内部[0];
    const 新しいキュー = this.効果キュー内部.slice(1);

    const 更新されたシステム = 効果解決システム.複製作成(
      this.ゲームID,
      新しいキュー,
      this.効果IDカウンター内部,
      this.効果実行関数内部,
      this.置換確認関数内部
    );

    return [取り出した効果, 更新されたシステム];
  }

  /**
   * リセット
   * @returns リセットされた効果解決システム
   */
  リセット(): 効果解決システム {
    return new 効果解決システム(this.ゲームID);
  }
}
