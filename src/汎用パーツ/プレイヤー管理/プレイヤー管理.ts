/**
 * 汎用パーツ：プレイヤー管理
 *
 * プレイヤーを管理し、変更を追跡する
 * イミュータブル：すべての操作が新しいプレイヤーを返す
 *
 * 責務：プレイヤー情報を保持・変更・履歴を管理するのみ
 * ゲームルール判定は層2以上で実装
 */

import type { プレイヤー, プレイヤー状態 } from './プレイヤー';
import { プレイヤーを作成 } from './プレイヤー';

/**
 * プレイヤー変更記録（履歴用）
 */
export interface プレイヤー変更記録 {
  readonly 変更種類: '名前変更' | '状態変更' | 'パーツ追加' | 'パーツ削除';
  readonly 変更前: プレイヤー;
  readonly 変更後: プレイヤー;
  readonly 詳細: Record<string, unknown>;
  readonly 変更日時: number;
}

/**
 * パーツ参照（キー-値ペア）
 */
export interface パーツ参照 {
  readonly キー: string;
  readonly パーツ: unknown;
}

/**
 * プレイヤーを管理する
 * イミュータブル：すべての操作が新しいプレイヤーを返す
 *
 * 層外共有資産として、純粋にプレイヤー情報を管理する
 */
export class プレイヤー管理 {
  private 現在のプレイヤー: プレイヤー;
  private 所有パーツ: Map<string, unknown> = new Map();
  private 変更履歴: プレイヤー変更記録[] = [];

  /**
   * 指定したID、名前でプレイヤーを作成
   */
  constructor(ID: string, 名前: string, 状態: プレイヤー状態 = '有効') {
    this.現在のプレイヤー = プレイヤーを作成(ID, 名前, 状態);
  }

  /**
   * 現在のプレイヤーを取得
   */
  取得(): プレイヤー {
    return { ...this.現在のプレイヤー };
  }

  /**
   * IDを取得
   */
  IDを取得(): string {
    return this.現在のプレイヤー.ID;
  }

  /**
   * 名前を取得
   */
  名前を取得(): string {
    return this.現在のプレイヤー.名前;
  }

  /**
   * 状態を取得
   */
  状態を取得(): プレイヤー状態 {
    return this.現在のプレイヤー.状態;
  }

  /**
   * パーツキー一覧を取得
   */
  パーツキー一覧を取得(): string[] {
    return [...this.現在のプレイヤー.パーツキー一覧];
  }

  /**
   * 保持しているパーツ数を取得
   */
  保持パーツ数を取得(): number {
    return this.現在のプレイヤー.パーツキー一覧.length;
  }

  /**
   * 名前を変更（イミュータブル）
   */
  名前を変更(新しい名前: string): プレイヤー管理 {
    if (新しい名前 === this.現在のプレイヤー.名前) {
      return this;
    }

    const 新しい管理 = new プレイヤー管理(
      this.現在のプレイヤー.ID,
      新しい名前,
      this.現在のプレイヤー.状態
    );

    新しい管理.所有パーツ = new Map(this.所有パーツ);
    新しい管理.変更履歴 = [...this.変更履歴];
    新しい管理.現在のプレイヤー = {
      ...this.現在のプレイヤー,
      名前: 新しい名前
    };

    新しい管理.変更履歴.push({
      変更種類: '名前変更',
      変更前: this.現在のプレイヤー,
      変更後: 新しい管理.現在のプレイヤー,
      詳細: { 旧名前: this.現在のプレイヤー.名前, 新名前: 新しい名前 },
      変更日時: Date.now()
    });

    return 新しい管理;
  }

  /**
   * 状態を変更（イミュータブル）
   */
  状態を変更(新しい状態: プレイヤー状態): プレイヤー管理 {
    if (新しい状態 === this.現在のプレイヤー.状態) {
      return this;
    }

    const 新しい管理 = new プレイヤー管理(
      this.現在のプレイヤー.ID,
      this.現在のプレイヤー.名前,
      新しい状態
    );

    新しい管理.所有パーツ = new Map(this.所有パーツ);
    新しい管理.変更履歴 = [...this.変更履歴];
    新しい管理.現在のプレイヤー = {
      ...this.現在のプレイヤー,
      状態: 新しい状態
    };

    新しい管理.変更履歴.push({
      変更種類: '状態変更',
      変更前: this.現在のプレイヤー,
      変更後: 新しい管理.現在のプレイヤー,
      詳細: { 旧状態: this.現在のプレイヤー.状態, 新状態: 新しい状態 },
      変更日時: Date.now()
    });

    return 新しい管理;
  }

  /**
   * パーツを追加（イミュータブル）
   */
  パーツを追加(キー: string, パーツ: unknown): プレイヤー管理 {
    if (this.所有パーツ.has(キー)) {
      // 既に存在する場合は何もしない
      return this;
    }

    const 新しい管理 = new プレイヤー管理(
      this.現在のプレイヤー.ID,
      this.現在のプレイヤー.名前,
      this.現在のプレイヤー.状態
    );

    新しい管理.所有パーツ = new Map(this.所有パーツ);
    新しい管理.所有パーツ.set(キー, パーツ);
    新しい管理.変更履歴 = [...this.変更履歴];

    const 新しいキー一覧 = Array.from(新しい管理.所有パーツ.keys());
    新しい管理.現在のプレイヤー = {
      ...this.現在のプレイヤー,
      パーツキー一覧: 新しいキー一覧
    };

    新しい管理.変更履歴.push({
      変更種類: 'パーツ追加',
      変更前: this.現在のプレイヤー,
      変更後: 新しい管理.現在のプレイヤー,
      詳細: { 追加キー: キー },
      変更日時: Date.now()
    });

    return 新しい管理;
  }

  /**
   * パーツを取得
   */
  パーツを取得(キー: string): unknown {
    return this.所有パーツ.get(キー);
  }

  /**
   * パーツを保持しているかを確認
   */
  パーツを保持しているか(キー: string): boolean {
    return this.所有パーツ.has(キー);
  }

  /**
   * パーツを削除（イミュータブル）
   */
  パーツを削除(キー: string): プレイヤー管理 {
    if (!this.所有パーツ.has(キー)) {
      // 存在しないパーツなら何もしない
      return this;
    }

    const 新しい管理 = new プレイヤー管理(
      this.現在のプレイヤー.ID,
      this.現在のプレイヤー.名前,
      this.現在のプレイヤー.状態
    );

    新しい管理.所有パーツ = new Map(this.所有パーツ);
    新しい管理.所有パーツ.delete(キー);
    新しい管理.変更履歴 = [...this.変更履歴];

    const 新しいキー一覧 = Array.from(新しい管理.所有パーツ.keys());
    新しい管理.現在のプレイヤー = {
      ...this.現在のプレイヤー,
      パーツキー一覧: 新しいキー一覧
    };

    新しい管理.変更履歴.push({
      変更種類: 'パーツ削除',
      変更前: this.現在のプレイヤー,
      変更後: 新しい管理.現在のプレイヤー,
      詳細: { 削除キー: キー },
      変更日時: Date.now()
    });

    return 新しい管理;
  }

  /**
   * すべてのパーツを削除（イミュータブル）
   */
  パーツをすべて削除(): プレイヤー管理 {
    if (this.所有パーツ.size === 0) {
      return this;
    }

    const 新しい管理 = new プレイヤー管理(
      this.現在のプレイヤー.ID,
      this.現在のプレイヤー.名前,
      this.現在のプレイヤー.状態
    );

    新しい管理.変更履歴 = [...this.変更履歴];

    const 削除キー一覧 = Array.from(this.所有パーツ.keys());
    for (const キー of 削除キー一覧) {
      新しい管理.変更履歴.push({
        変更種類: 'パーツ削除',
        変更前: this.現在のプレイヤー,
        変更後: 新しい管理.現在のプレイヤー,
        詳細: { 削除キー: キー },
        変更日時: Date.now()
      });
    }

    新しい管理.現在のプレイヤー = {
      ...this.現在のプレイヤー,
      パーツキー一覧: []
    };

    return 新しい管理;
  }

  /**
   * 変更履歴を取得
   */
  履歴を取得(): プレイヤー変更記録[] {
    return [...this.変更履歴];
  }

  /**
   * 最新の変更記録を取得
   */
  最新の変更を取得(): プレイヤー変更記録 | undefined {
    if (this.変更履歴.length === 0) {
      return undefined;
    }
    return { ...this.変更履歴[this.変更履歴.length - 1] };
  }

  /**
   * 変更履歴のサイズを取得
   */
  履歴のサイズ(): number {
    return this.変更履歴.length;
  }

  /**
   * 変更履歴が空かを確認
   */
  履歴が空か(): boolean {
    return this.変更履歴.length === 0;
  }

  /**
   * 変更履歴をリセット
   */
  履歴をリセット(): プレイヤー管理 {
    const 新しい管理 = new プレイヤー管理(
      this.現在のプレイヤー.ID,
      this.現在のプレイヤー.名前,
      this.現在のプレイヤー.状態
    );

    新しい管理.所有パーツ = new Map(this.所有パーツ);
    新しい管理.現在のプレイヤー = { ...this.現在のプレイヤー };

    return 新しい管理;
  }
}
