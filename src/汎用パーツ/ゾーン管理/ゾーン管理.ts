/**
 * 汎用パーツ：ゾーン管理
 *
 * ゲームのゾーン（領域）を管理する
 * イミュータブル：すべての操作が新しいインスタンスを返す
 *
 * 責務：オブジェクト保持・移動・公開設定・履歴管理のみ
 * ゲームルール判定は層2以上で実装
 */

import type { ゾーン, 公開設定, ゾーン変更履歴 } from './ゾーン';

/**
 * ゾーンを管理する
 * イミュータブル：すべての操作が新しいインスタンスを返す
 *
 * 層外共有資産として、純粋にゾーン状態を管理する
 */
export class ゾーン管理 implements ゾーン {
  readonly ID: string;
  readonly 名前: string;
  private オブジェクト一覧内部: unknown[] = [];
  readonly 順序管理: boolean;
  readonly 公開設定: 公開設定;
  private 変更履歴: ゾーン変更履歴[] = [];

  constructor(
    ID: string,
    名前: string,
    公開設定: 公開設定,
    順序管理: boolean = true
  ) {
    this.ID = ID;
    this.名前 = 名前;
    this.公開設定 = 公開設定;
    this.順序管理 = 順序管理;
  }

  /**
   * オブジェクト一覧を取得
   */
  get オブジェクト一覧(): readonly unknown[] {
    return [...this.オブジェクト一覧内部];
  }

  /**
   * オブジェクトを追加（イミュータブル）
   */
  オブジェクトを追加(オブジェクト: unknown): ゾーン管理 {
    const 新しいゾーン = new ゾーン管理(this.ID, this.名前, this.公開設定, this.順序管理);
    新しいゾーン.オブジェクト一覧内部 = [...this.オブジェクト一覧内部, オブジェクト];
    新しいゾーン.変更履歴 = [...this.変更履歴];

    新しいゾーン.変更履歴.push({
      種類: 'オブジェクト追加',
      変更前オブジェクト数: this.オブジェクト一覧内部.length,
      変更後オブジェクト数: 新しいゾーン.オブジェクト一覧内部.length,
      時刻: Date.now()
    });

    return 新しいゾーン;
  }

  /**
   * オブジェクトを複数追加（イミュータブル）
   */
  オブジェクトを複数追加(オブジェクト一覧: unknown[]): ゾーン管理 {
    if (オブジェクト一覧.length === 0) {
      return this;
    }

    const 新しいゾーン = new ゾーン管理(this.ID, this.名前, this.公開設定, this.順序管理);
    新しいゾーン.オブジェクト一覧内部 = [...this.オブジェクト一覧内部, ...オブジェクト一覧];
    新しいゾーン.変更履歴 = [...this.変更履歴];

    新しいゾーン.変更履歴.push({
      種類: 'オブジェクト追加',
      変更前オブジェクト数: this.オブジェクト一覧内部.length,
      変更後オブジェクト数: 新しいゾーン.オブジェクト一覧内部.length,
      時刻: Date.now()
    });

    return 新しいゾーン;
  }

  /**
   * オブジェクトを削除（イミュータブル）
   * インデックスで指定
   */
  オブジェクトをインデックス削除(インデックス: number): ゾーン管理 {
    if (インデックス < 0 || インデックス >= this.オブジェクト一覧内部.length) {
      return this;
    }

    const 新しいゾーン = new ゾーン管理(this.ID, this.名前, this.公開設定, this.順序管理);
    新しいゾーン.オブジェクト一覧内部 = this.オブジェクト一覧内部.filter((_, idx) => idx !== インデックス);
    新しいゾーン.変更履歴 = [...this.変更履歴];

    新しいゾーン.変更履歴.push({
      種類: 'オブジェクト削除',
      変更前オブジェクト数: this.オブジェクト一覧内部.length,
      変更後オブジェクト数: 新しいゾーン.オブジェクト一覧内部.length,
      時刻: Date.now()
    });

    return 新しいゾーン;
  }

  /**
   * オブジェクトを値で削除（イミュータブル）
   * 最初にマッチしたものを削除
   */
  オブジェクトを値削除(オブジェクト: unknown): ゾーン管理 {
    const インデックス = this.オブジェクト一覧内部.indexOf(オブジェクト);
    if (インデックス === -1) {
      return this;
    }

    return this.オブジェクトをインデックス削除(インデックス);
  }

  /**
   * すべてのオブジェクトを削除（イミュータブル）
   */
  すべてのオブジェクトを削除(): ゾーン管理 {
    if (this.オブジェクト一覧内部.length === 0) {
      return this;
    }

    const 新しいゾーン = new ゾーン管理(this.ID, this.名前, this.公開設定, this.順序管理);
    新しいゾーン.変更履歴 = [...this.変更履歴];

    新しいゾーン.変更履歴.push({
      種類: 'オブジェクト削除',
      変更前オブジェクト数: this.オブジェクト一覧内部.length,
      変更後オブジェクト数: 0,
      時刻: Date.now()
    });

    return 新しいゾーン;
  }

  /**
   * オブジェクトをゾーン間で移動（イミュータブル）
   * このゾーンからオブジェクトを移動する
   */
  オブジェクトを移動出す(インデックス: number): {
    新しいゾーン: ゾーン管理;
    オブジェクト: unknown | undefined;
  } {
    if (インデックス < 0 || インデックス >= this.オブジェクト一覧内部.length) {
      return {
        新しいゾーン: this,
        オブジェクト: undefined
      };
    }

    const オブジェクト = this.オブジェクト一覧内部[インデックス];
    const 新しいゾーン = this.オブジェクトをインデックス削除(インデックス);

    return {
      新しいゾーン,
      オブジェクト
    };
  }

  /**
   * オブジェクトをゾーン間で移動（イミュータブル）
   * 別のゾーンから受け取るのに使用（履歴付き）
   */
  オブジェクトを移動受け取る(
    オブジェクト: unknown,
    移動元ゾーンID: string
  ): ゾーン管理 {
    const 新しいゾーン = this.オブジェクトを追加(オブジェクト);

    新しいゾーン.変更履歴[新しいゾーン.変更履歴.length - 1] = {
      種類: 'ゾーン間移動',
      変更前オブジェクト数: this.オブジェクト一覧内部.length,
      変更後オブジェクト数: 新しいゾーン.オブジェクト一覧内部.length,
      時刻: Date.now(),
      詳細: {
        移動元ゾーンID,
        移動先ゾーンID: this.ID
      }
    };

    return 新しいゾーン;
  }

  /**
   * オブジェクト数を取得
   */
  オブジェクト数を取得(): number {
    return this.オブジェクト一覧内部.length;
  }

  /**
   * ゾーンが空かを確認
   */
  は空か(): boolean {
    return this.オブジェクト一覧内部.length === 0;
  }

  /**
   * 先頭のオブジェクトを取得
   */
  先頭を取得(): unknown | undefined {
    if (!this.順序管理) {
      return undefined;
    }
    return this.オブジェクト一覧内部[0];
  }

  /**
   * 末尾のオブジェクトを取得
   */
  末尾を取得(): unknown | undefined {
    if (!this.順序管理) {
      return undefined;
    }
    return this.オブジェクト一覧内部[this.オブジェクト一覧内部.length - 1];
  }

  /**
   * 指定位置のオブジェクトを取得
   */
  位置から取得(インデックス: number): unknown | undefined {
    if (!this.順序管理) {
      return undefined;
    }
    if (インデックス < 0 || インデックス >= this.オブジェクト一覧内部.length) {
      return undefined;
    }
    return this.オブジェクト一覧内部[インデックス];
  }

  /**
   * 名前を変更（イミュータブル）
   */
  名前を変更(新しい名前: string): ゾーン管理 {
    if (新しい名前 === this.名前) {
      return this;
    }

    const 新しいゾーン = new ゾーン管理(this.ID, 新しい名前, this.公開設定, this.順序管理);
    新しいゾーン.オブジェクト一覧内部 = [...this.オブジェクト一覧内部];
    新しいゾーン.変更履歴 = [...this.変更履歴];

    return 新しいゾーン;
  }

  /**
   * 公開設定を変更（イミュータブル）
   */
  公開設定を変更(新しい公開設定: 公開設定): ゾーン管理 {
    const 新しいゾーン = new ゾーン管理(this.ID, this.名前, 新しい公開設定, this.順序管理);
    新しいゾーン.オブジェクト一覧内部 = [...this.オブジェクト一覧内部];
    新しいゾーン.変更履歴 = [...this.変更履歴];

    return 新しいゾーン;
  }

  /**
   * 順序を変更（イミュータブル）
   * 指定インデックスのオブジェクトを新しい位置に移動
   */
  順序を変更(現在のインデックス: number, 新しいインデックス: number): ゾーン管理 {
    if (!this.順序管理) {
      return this;
    }

    if (
      現在のインデックス < 0 ||
      現在のインデックス >= this.オブジェクト一覧内部.length ||
      新しいインデックス < 0 ||
      新しいインデックス >= this.オブジェクト一覧内部.length ||
      現在のインデックス === 新しいインデックス
    ) {
      return this;
    }

    const 新しい一覧 = [...this.オブジェクト一覧内部];
    const [移動対象] = 新しい一覧.splice(現在のインデックス, 1);
    新しい一覧.splice(新しいインデックス, 0, 移動対象);

    const 新しいゾーン = new ゾーン管理(this.ID, this.名前, this.公開設定, this.順序管理);
    新しいゾーン.オブジェクト一覧内部 = 新しい一覧;
    新しいゾーン.変更履歴 = [...this.変更履歴];

    新しいゾーン.変更履歴.push({
      種類: '順序変更',
      変更前オブジェクト数: this.オブジェクト一覧内部.length,
      変更後オブジェクト数: 新しい一覧.length,
      時刻: Date.now()
    });

    return 新しいゾーン;
  }

  /**
   * 履歴を取得
   */
  履歴を取得(): ゾーン変更履歴[] {
    return [...this.変更履歴];
  }

  /**
   * 最新の履歴を取得
   */
  最新履歴を取得(): ゾーン変更履歴 | undefined {
    if (this.変更履歴.length === 0) {
      return undefined;
    }
    return { ...this.変更履歴[this.変更履歴.length - 1] };
  }

  /**
   * 履歴のサイズを取得
   */
  履歴のサイズ(): number {
    return this.変更履歴.length;
  }

  /**
   * 履歴が空かを確認
   */
  履歴が空か(): boolean {
    return this.変更履歴.length === 0;
  }

  /**
   * 履歴をクリア（イミュータブル）
   */
  履歴をクリア(): ゾーン管理 {
    if (this.変更履歴.length === 0) {
      return this;
    }

    const 新しいゾーン = new ゾーン管理(this.ID, this.名前, this.公開設定, this.順序管理);
    新しいゾーン.オブジェクト一覧内部 = [...this.オブジェクト一覧内部];

    return 新しいゾーン;
  }

  /**
   * リセット（イミュータブル）
   * オブジェクトをすべて削除、履歴は保持
   */
  リセット(): ゾーン管理 {
    if (this.オブジェクト一覧内部.length === 0) {
      return this;
    }

    const 新しいゾーン = new ゾーン管理(this.ID, this.名前, this.公開設定, this.順序管理);
    新しいゾーン.変更履歴 = [...this.変更履歴];

    return 新しいゾーン;
  }

  /**
   * 完全リセット（イミュータブル）
   * オブジェクトと履歴をすべてリセット
   */
  完全リセット(): ゾーン管理 {
    return new ゾーン管理(this.ID, this.名前, this.公開設定, this.順序管理);
  }
}
