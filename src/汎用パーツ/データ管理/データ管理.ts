/**
 * 汎用パーツ：データ管理
 *
 * データを管理し、変更を追跡する
 * イミュータブル：すべての操作が新しいデータを返す
 */

import type { データストレージ, データ変更, データバリデーター } from './データ';
import { 深くコピー } from './データ';

/**
 * データ変更リスナー
 */
export type データリスナー = (変更: データ変更) => void;

/**
 * データを管理する
 * イミュータブル：すべての操作が新しいデータを返す
 */
export class データ管理 {
  private リスナーたち: データリスナー[] = [];
  private 現在のデータ: データストレージ;
  private バリデーター?: データバリデーター;

  constructor(初期データ: データストレージ = {}) {
    this.現在のデータ = 深くコピー(初期データ);
  }

  /**
   * 現在のデータを取得
   */
  取得(): データストレージ {
    return 深くコピー(this.現在のデータ);
  }

  /**
   * バリデーターを設定
   */
  バリデーターを設定(バリデーター: データバリデーター): void {
    this.バリデーター = バリデーター;
  }

  /**
   * 値を設定（イミュータブル）
   */
  値を設定(キー: string, 値: unknown): データ管理 {
    if (this.バリデーター) {
      const 検証 = this.バリデーター(キー, 値);
      if (!検証.有効) {
        throw new Error(`バリデーション失敗: ${検証.エラー}`);
      }
    }

    const 新しい管理 = new データ管理(this.現在のデータ);
    新しい管理.リスナーたち = [...this.リスナーたち];
    新しい管理.バリデーター = this.バリデーター;

    const 変更前 = this.現在のデータ[キー];
    新しい管理.現在のデータ[キー] = 値;

    const 変更: データ変更 = {
      キー,
      変更前,
      変更後: 値
    };

    新しい管理.リスナーたちに通知(変更);
    return 新しい管理;
  }

  /**
   * 複数の値を設定（イミュータブル）
   */
  複数を設定(データ: Record<string, unknown>): データ管理 {
    let 管理: データ管理 = this;
    for (const [キー, 値] of Object.entries(データ)) {
      管理 = 管理.値を設定(キー, 値);
    }
    return 管理;
  }

  /**
   * キーを削除（イミュータブル）
   */
  削除(キー: string): データ管理 {
    if (!(キー in this.現在のデータ)) {
      return this;
    }

    const 新しい管理 = new データ管理(this.現在のデータ);
    新しい管理.リスナーたち = [...this.リスナーたち];
    新しい管理.バリデーター = this.バリデーター;

    const 変更前 = this.現在のデータ[キー];
    delete 新しい管理.現在のデータ[キー];

    const 変更: データ変更 = {
      キー,
      変更前,
      変更後: undefined
    };

    新しい管理.リスナーたちに通知(変更);
    return 新しい管理;
  }

  /**
   * 複数のキーを削除（イミュータブル）
   */
  複数削除(キーたち: string[]): データ管理 {
    let 管理: データ管理 = this;
    for (const キー of キーたち) {
      管理 = 管理.削除(キー);
    }
    return 管理;
  }

  /**
   * すべてのデータをクリア（イミュータブル）
   */
  クリア(): データ管理 {
    const 新しい管理 = new データ管理();
    新しい管理.リスナーたち = [...this.リスナーたち];
    新しい管理.バリデーター = this.バリデーター;

    const キーたち = Object.keys(this.現在のデータ);
    for (const キー of キーたち) {
      const 変更: データ変更 = {
        キー,
        変更前: this.現在のデータ[キー],
        変更後: undefined
      };
      新しい管理.リスナーたちに通知(変更);
    }

    return 新しい管理;
  }

  /**
   * キーが存在するか確認
   */
  キーが存在するか(キー: string): boolean {
    return キー in this.現在のデータ;
  }

  /**
   * すべてのキーを取得
   */
  キーたちを取得(): string[] {
    return Object.keys(this.現在のデータ);
  }

  /**
   * データのサイズを取得
   */
  サイズを取得(): number {
    return Object.keys(this.現在のデータ).length;
  }

  /**
   * データが空か確認
   */
  が空か(): boolean {
    return this.サイズを取得() === 0;
  }

  /**
   * リスナーを登録
   */
  リスナーを登録(リスナー: データリスナー): void {
    this.リスナーたち.push(リスナー);
  }

  /**
   * リスナーを削除
   */
  リスナーを削除(リスナー: データリスナー): void {
    const index = this.リスナーたち.indexOf(リスナー);
    if (index !== -1) {
      this.リスナーたち.splice(index, 1);
    }
  }

  /**
   * すべてのリスナーを削除
   */
  リスナーをクリア(): void {
    this.リスナーたち.length = 0;
  }

  /**
   * リスナーたちに通知
   */
  private リスナーたちに通知(変更: データ変更): void {
    for (const リスナー of this.リスナーたち) {
      リスナー(変更);
    }
  }

  /**
   * マージ（別のデータを統合、イミュータブル）
   */
  マージ(他のデータ: データストレージ): データ管理 {
    let 管理: データ管理 = this;
    for (const [キー, 値] of Object.entries(他のデータ)) {
      管理 = 管理.値を設定(キー, 値);
    }
    return 管理;
  }

  /**
   * 条件に合致するデータをフィルタリング（イミュータブル）
   */
  フィルタリング(
    述語: (キー: string, 値: unknown) => boolean
  ): データ管理 {
    const 新しいデータ: データストレージ = {};
    for (const [キー, 値] of Object.entries(this.現在のデータ)) {
      if (述語(キー, 値)) {
        新しいデータ[キー] = 値;
      }
    }

    const 新しい管理 = new データ管理(新しいデータ);
    新しい管理.リスナーたち = [...this.リスナーたち];
    新しい管理.バリデーター = this.バリデーター;

    return 新しい管理;
  }
}
