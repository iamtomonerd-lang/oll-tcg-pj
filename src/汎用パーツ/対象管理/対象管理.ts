/**
 * 汎用パーツ：対象管理
 *
 * 処理対象となるオブジェクトを候補から決定・保持する
 * イミュータブル：すべての操作が新しいインスタンスを返す
 *
 * 責務：対象の候補管理・選択・確定のみ
 * 効果処理・条件判定は層2以上で実装
 */

import type { 対象, 対象管理状態, 対象管理変更履歴 } from './対象';

/**
 * 対象を管理する
 * イミュータブル：すべての操作が新しいインスタンスを返す
 *
 * 層外共有資産として、純粋に対象管理を行う
 */
export class 対象管理 {
  readonly ID: string;
  private 状態内部: 対象管理状態 = '未開始';
  private 候補一覧内部: 対象[] = [];
  private 確定対象一覧内部: 対象[] = [];
  private 最小選択数内部: number = 0;
  private 最大選択数内部: number = 1;
  private 変更履歴: 対象管理変更履歴[] = [];

  constructor(ID: string) {
    this.ID = ID;
  }

  /**
   * 状態を取得
   */
  get 状態(): 対象管理状態 {
    return this.状態内部;
  }

  /**
   * 候補一覧を取得
   */
  get 候補一覧(): readonly 対象[] {
    return [...this.候補一覧内部];
  }

  /**
   * 確定対象一覧を取得
   */
  get 確定対象一覧(): readonly 対象[] {
    return [...this.確定対象一覧内部];
  }

  /**
   * 最小選択数を取得
   */
  get 最小選択数(): number {
    return this.最小選択数内部;
  }

  /**
   * 最大選択数を取得
   */
  get 最大選択数(): number {
    return this.最大選択数内部;
  }

  /**
   * 候補を設定（イミュータブル）
   * 既存の候補と選択をリセット
   */
  候補を設定(候補一覧: 対象[], 最小選択数?: number, 最大選択数?: number): 対象管理 {
    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = '候補設定済み';
    新しい管理.候補一覧内部 = [...候補一覧];
    新しい管理.最小選択数内部 = 最小選択数 ?? 0;
    新しい管理.最大選択数内部 = 最大選択数 ?? 1;
    新しい管理.変更履歴 = [...this.変更履歴];

    新しい管理.変更履歴.push({
      種類: '候補設定',
      変更前: this.候補一覧内部.length,
      変更後: 候補一覧.length,
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * 候補を追加（イミュータブル）
   */
  候補を追加(対象: 対象): 対象管理 {
    if (this.候補一覧内部.some(c => c.id === 対象.id)) {
      return this;
    }

    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = this.状態内部;
    新しい管理.候補一覧内部 = [...this.候補一覧内部, 対象];
    新しい管理.確定対象一覧内部 = [...this.確定対象一覧内部];
    新しい管理.最小選択数内部 = this.最小選択数内部;
    新しい管理.最大選択数内部 = this.最大選択数内部;
    新しい管理.変更履歴 = [...this.変更履歴];

    新しい管理.変更履歴.push({
      種類: '候補追加',
      変更前: this.候補一覧内部.length,
      変更後: 新しい管理.候補一覧内部.length,
      時刻: Date.now(),
      詳細: { 詳細内容: `対象ID: ${対象.id}` }
    });

    return 新しい管理;
  }

  /**
   * 複数候補を追加（イミュータブル）
   */
  複数候補を追加(候補一覧: 対象[]): 対象管理 {
    const 追加する候補 = 候補一覧.filter(
      c => !this.候補一覧内部.some(existing => existing.id === c.id)
    );

    if (追加する候補.length === 0) {
      return this;
    }

    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = this.状態内部;
    新しい管理.候補一覧内部 = [...this.候補一覧内部, ...追加する候補];
    新しい管理.確定対象一覧内部 = [...this.確定対象一覧内部];
    新しい管理.最小選択数内部 = this.最小選択数内部;
    新しい管理.最大選択数内部 = this.最大選択数内部;
    新しい管理.変更履歴 = [...this.変更履歴];

    新しい管理.変更履歴.push({
      種類: '候補追加',
      変更前: this.候補一覧内部.length,
      変更後: 新しい管理.候補一覧内部.length,
      時刻: Date.now(),
      詳細: { 詳細内容: `${追加する候補.length}個の候補を追加` }
    });

    return 新しい管理;
  }

  /**
   * 候補を削除（イミュータブル）
   */
  候補を削除(対象ID: string): 対象管理 {
    const インデックス = this.候補一覧内部.findIndex(c => c.id === 対象ID);
    if (インデックス === -1) {
      return this;
    }

    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = this.状態内部;
    新しい管理.候補一覧内部 = this.候補一覧内部.filter((_, idx) => idx !== インデックス);
    新しい管理.確定対象一覧内部 = [...this.確定対象一覧内部];
    新しい管理.最小選択数内部 = this.最小選択数内部;
    新しい管理.最大選択数内部 = this.最大選択数内部;
    新しい管理.変更履歴 = [...this.変更履歴];

    新しい管理.変更履歴.push({
      種類: '候補削除',
      変更前: this.候補一覧内部.length,
      変更後: 新しい管理.候補一覧内部.length,
      時刻: Date.now(),
      詳細: { 詳細内容: `対象ID: ${対象ID}` }
    });

    return 新しい管理;
  }

  /**
   * 対象を追加（選択）（イミュータブル）
   */
  対象を追加(対象: 対象): 対象管理 {
    // 候補にあるか確認
    if (!this.候補一覧内部.some(c => c.id === 対象.id)) {
      return this;
    }

    // 既に確定されているか確認
    if (this.確定対象一覧内部.some(c => c.id === 対象.id)) {
      return this;
    }

    // 最大選択数に達しているか確認
    if (this.確定対象一覧内部.length >= this.最大選択数内部) {
      return this;
    }

    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = '選択中';
    新しい管理.候補一覧内部 = [...this.候補一覧内部];
    新しい管理.確定対象一覧内部 = [...this.確定対象一覧内部, 対象];
    新しい管理.最小選択数内部 = this.最小選択数内部;
    新しい管理.最大選択数内部 = this.最大選択数内部;
    新しい管理.変更履歴 = [...this.変更履歴];

    新しい管理.変更履歴.push({
      種類: '対象追加',
      変更前: this.確定対象一覧内部.length,
      変更後: 新しい管理.確定対象一覧内部.length,
      時刻: Date.now(),
      詳細: { 詳細内容: `対象ID: ${対象.id}` }
    });

    return 新しい管理;
  }

  /**
   * 対象を削除（イミュータブル）
   */
  対象を削除(対象ID: string): 対象管理 {
    const インデックス = this.確定対象一覧内部.findIndex(c => c.id === 対象ID);
    if (インデックス === -1) {
      return this;
    }

    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = this.確定対象一覧内部.length > 1 ? '選択中' : '候補設定済み';
    新しい管理.候補一覧内部 = [...this.候補一覧内部];
    新しい管理.確定対象一覧内部 = this.確定対象一覧内部.filter((_, idx) => idx !== インデックス);
    新しい管理.最小選択数内部 = this.最小選択数内部;
    新しい管理.最大選択数内部 = this.最大選択数内部;
    新しい管理.変更履歴 = [...this.変更履歴];

    新しい管理.変更履歴.push({
      種類: '対象削除',
      変更前: this.確定対象一覧内部.length,
      変更後: 新しい管理.確定対象一覧内部.length,
      時刻: Date.now(),
      詳細: { 詳細内容: `対象ID: ${対象ID}` }
    });

    return 新しい管理;
  }

  /**
   * 確定対象数を取得
   */
  確定対象数を取得(): number {
    return this.確定対象一覧内部.length;
  }

  /**
   * 最小選択数に達しているか確認
   */
  は最小選択数以上か(): boolean {
    return this.確定対象一覧内部.length >= this.最小選択数内部;
  }

  /**
   * 最大選択数に達しているか確認
   */
  は最大選択数に達しているか(): boolean {
    return this.確定対象一覧内部.length >= this.最大選択数内部;
  }

  /**
   * 選択がすべて満たされているか確認
   */
  は選択完了か(): boolean {
    return (
      this.確定対象一覧内部.length >= this.最小選択数内部 &&
      this.確定対象一覧内部.length <= this.最大選択数内部
    );
  }

  /**
   * 対象を確定（イミュータブル）
   */
  対象を確定(): 対象管理 {
    // 最小選択数に達していないか確認
    if (this.確定対象一覧内部.length < this.最小選択数内部) {
      return this;
    }

    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = '確定';
    新しい管理.候補一覧内部 = [...this.候補一覧内部];
    新しい管理.確定対象一覧内部 = [...this.確定対象一覧内部];
    新しい管理.最小選択数内部 = this.最小選択数内部;
    新しい管理.最大選択数内部 = this.最大選択数内部;
    新しい管理.変更履歴 = [...this.変更履歴];

    新しい管理.変更履歴.push({
      種類: '状態変更',
      変更前: this.状態内部,
      変更後: '確定',
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * 確定状態か確認
   */
  は確定済みか(): boolean {
    return this.状態内部 === '確定';
  }

  /**
   * 選択をキャンセル（イミュータブル）
   */
  選択をキャンセル(): 対象管理 {
    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = 'キャンセル';
    新しい管理.候補一覧内部 = [...this.候補一覧内部];
    新しい管理.確定対象一覧内部 = [];
    新しい管理.最小選択数内部 = this.最小選択数内部;
    新しい管理.最大選択数内部 = this.最大選択数内部;
    新しい管理.変更履歴 = [...this.変更履歴];

    新しい管理.変更履歴.push({
      種類: '状態変更',
      変更前: this.状態内部,
      変更後: 'キャンセル',
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * キャンセル状態か確認
   */
  はキャンセル済みか(): boolean {
    return this.状態内部 === 'キャンセル';
  }

  /**
   * 履歴を取得
   */
  履歴を取得(): 対象管理変更履歴[] {
    return [...this.変更履歴];
  }

  /**
   * 最新の履歴を取得
   */
  最新履歴を取得(): 対象管理変更履歴 | undefined {
    if (this.変更履歴.length === 0) {
      return undefined;
    }
    return { ...this.変更履歴[this.変更履歴.length - 1] };
  }

  /**
   * 履歴のサイズを取得
   */
  履歴のサイズ(): number {
    return this.変更履歴.length;
  }

  /**
   * 履歴が空かを確認
   */
  履歴が空か(): boolean {
    return this.変更履歴.length === 0;
  }

  /**
   * 履歴をクリア（イミュータブル）
   */
  履歴をクリア(): 対象管理 {
    if (this.変更履歴.length === 0) {
      return this;
    }

    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = this.状態内部;
    新しい管理.候補一覧内部 = [...this.候補一覧内部];
    新しい管理.確定対象一覧内部 = [...this.確定対象一覧内部];
    新しい管理.最小選択数内部 = this.最小選択数内部;
    新しい管理.最大選択数内部 = this.最大選択数内部;

    return 新しい管理;
  }

  /**
   * リセット（イミュータブル）
   * 状態を未開始に戻す、候補は保持、選択をクリア
   */
  リセット(): 対象管理 {
    if (this.状態内部 === '未開始' && this.確定対象一覧内部.length === 0) {
      return this;
    }

    const 新しい管理 = new 対象管理(this.ID);
    新しい管理.状態内部 = '未開始';
    新しい管理.候補一覧内部 = [...this.候補一覧内部];
    新しい管理.最小選択数内部 = this.最小選択数内部;
    新しい管理.最大選択数内部 = this.最大選択数内部;
    新しい管理.変更履歴 = [...this.変更履歴];

    新しい管理.変更履歴.push({
      種類: '状態変更',
      変更前: this.状態内部,
      変更後: '未開始',
      時刻: Date.now()
    });

    return 新しい管理;
  }

  /**
   * 完全リセット（イミュータブル）
   * すべてをリセット
   */
  完全リセット(): 対象管理 {
    return new 対象管理(this.ID);
  }
}
