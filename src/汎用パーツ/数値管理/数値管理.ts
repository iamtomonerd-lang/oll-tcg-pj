/**
 * 汎用パーツ：数値管理
 *
 * 数値を管理し、変更を追跡する
 * イミュータブル：すべての操作が新しい数値を返す
 */

import type { 数値 } from './数値';
import { 数値を作成 } from './数値';

/**
 * 数値変更情報
 */
export interface 数値変更 {
  readonly 変更前: 数値;
  readonly 変更後: 数値;
}

/**
 * 数値変更リスナー
 */
export type 数値リスナー = (変更: 数値変更) => void;

/**
 * 数値を管理する
 * イミュータブル：すべての操作が新しい数値を返す
 */
export class 数値管理 {
  private リスナーたち: 数値リスナー[] = [];
  private 現在の数値: 数値;

  constructor(現在値: number, 最大値: number | null = null) {
    this.現在の数値 = 数値を作成(現在値, 最大値);
  }

  /**
   * 現在の数値を取得
   */
  取得(): 数値 {
    return this.現在の数値;
  }

  /**
   * 現在値を取得
   */
  現在値を取得(): number {
    return this.現在の数値.現在値;
  }

  /**
   * 最大値を取得
   */
  最大値を取得(): number | null {
    return this.現在の数値.最大値;
  }

  /**
   * 現在値を変更（イミュータブル）
   */
  現在値を変更(新しい値: number): 数値管理 {
    const 新しい管理 = new 数値管理(
      新しい値,
      this.現在の数値.最大値
    );
    新しい管理.リスナーたち = [...this.リスナーたち];

    const 変更: 数値変更 = {
      変更前: this.現在の数値,
      変更後: 新しい管理.現在の数値
    };

    新しい管理.リスナーたちに通知(変更);
    return 新しい管理;
  }

  /**
   * 最大値を変更（イミュータブル）
   */
  最大値を変更(新しい最大値: number | null): 数値管理 {
    const 新しい管理 = new 数値管理(
      this.現在の数値.現在値,
      新しい最大値
    );
    新しい管理.リスナーたち = [...this.リスナーたち];

    const 変更: 数値変更 = {
      変更前: this.現在の数値,
      変更後: 新しい管理.現在の数値
    };

    新しい管理.リスナーたちに通知(変更);
    return 新しい管理;
  }

  /**
   * 値を増加させる（イミュータブル）
   */
  増加させる(量: number): 数値管理 {
    return this.現在値を変更(this.現在の数値.現在値 + 量);
  }

  /**
   * 値を減少させる（イミュータブル）
   */
  減少させる(量: number): 数値管理 {
    return this.現在値を変更(this.現在の数値.現在値 - 量);
  }

  /**
   * 値を最大値にクリップする（イミュータブル）
   */
  クリップ(): 数値管理 {
    if (this.現在の数値.最大値 === null) {
      return this;
    }

    const クリップ後の値 = Math.min(
      this.現在の数値.現在値,
      this.現在の数値.最大値
    );

    if (クリップ後の値 === this.現在の数値.現在値) {
      return this;
    }

    return this.現在値を変更(クリップ後の値);
  }

  /**
   * リスナーを登録
   */
  リスナーを登録(リスナー: 数値リスナー): void {
    this.リスナーたち.push(リスナー);
  }

  /**
   * リスナーを削除
   */
  リスナーを削除(リスナー: 数値リスナー): void {
    const index = this.リスナーたち.indexOf(リスナー);
    if (index !== -1) {
      this.リスナーたち.splice(index, 1);
    }
  }

  /**
   * すべてのリスナーを削除
   */
  リスナーをクリア(): void {
    this.リスナーたち.length = 0;
  }

  /**
   * リスナーたちに通知
   */
  private リスナーたちに通知(変更: 数値変更): void {
    for (const リスナー of this.リスナーたち) {
      リスナー(変更);
    }
  }
}
