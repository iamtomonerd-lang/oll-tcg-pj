/**
 * 汎用パーツ：数値管理
 *
 * 数値を管理し、変更を追跡する
 * イミュータブル：すべての操作が新しい数値を返す
 *
 * 責務：数値を保持、変更、履歴を管理するのみ
 * 通知・リスナー機能は層2の責務
 */

import type { 数値 } from './数値';
import { 数値を作成 } from './数値';

/**
 * 数値変更記録（履歴用）
 */
export interface 数値変更記録 {
  readonly 変更前: 数値;
  readonly 変更後: 数値;
  readonly 変更日時: number;
}

/**
 * 数値を管理する
 * イミュータブル：すべての操作が新しい数値を返す
 *
 * 層外共有資産として、純粋に数値を管理する
 */
export class 数値管理 {
  private 現在の数値: 数値;
  private 変更履歴: 数値変更記録[] = [];

  /**
   * 指定した名前、初期値、最大値、最小値で数値を作成
   */
  constructor(
    名前: string,
    現在値: number,
    最大値: number | null = null,
    最小値: number | null = null
  ) {
    this.現在の数値 = 数値を作成(名前, 現在値, 最大値, 最小値);
  }

  /**
   * 現在の数値を取得
   */
  取得(): 数値 {
    return { ...this.現在の数値 };
  }

  /**
   * 名前を取得
   */
  名前を取得(): string {
    return this.現在の数値.名前;
  }

  /**
   * 現在値を取得
   */
  現在値を取得(): number {
    return this.現在の数値.現在値;
  }

  /**
   * 最大値を取得
   */
  最大値を取得(): number | null {
    return this.現在の数値.最大値;
  }

  /**
   * 最小値を取得
   */
  最小値を取得(): number | null {
    return this.現在の数値.最小値;
  }

  /**
   * 現在値を指定値に変更（イミュータブル）
   * 最大値・最小値の制限を適用
   */
  現在値を変更(新しい値: number): 数値管理 {
    const 制限後の値 = this.制限を適用(新しい値);

    if (制限後の値 === this.現在の数値.現在値) {
      return this;
    }

    const 新しい管理 = new 数値管理(
      this.現在の数値.名前,
      制限後の値,
      this.現在の数値.最大値,
      this.現在の数値.最小値
    );

    新しい管理.変更履歴 = [...this.変更履歴];
    新しい管理.変更履歴.push({
      変更前: this.現在の数値,
      変更後: 新しい管理.現在の数値,
      変更日時: Date.now()
    });

    return 新しい管理;
  }

  /**
   * 最大値を変更（イミュータブル）
   */
  最大値を変更(新しい最大値: number | null): 数値管理 {
    if (新しい最大値 === this.現在の数値.最大値) {
      return this;
    }

    const 制限後の値 = 新しい最大値 !== null
      ? Math.min(this.現在の数値.現在値, 新しい最大値)
      : this.現在の数値.現在値;

    const 新しい管理 = new 数値管理(
      this.現在の数値.名前,
      制限後の値,
      新しい最大値,
      this.現在の数値.最小値
    );

    新しい管理.変更履歴 = [...this.変更履歴];
    if (制限後の値 !== this.現在の数値.現在値) {
      新しい管理.変更履歴.push({
        変更前: this.現在の数値,
        変更後: 新しい管理.現在の数値,
        変更日時: Date.now()
      });
    }

    return 新しい管理;
  }

  /**
   * 最小値を変更（イミュータブル）
   */
  最小値を変更(新しい最小値: number | null): 数値管理 {
    if (新しい最小値 === this.現在の数値.最小値) {
      return this;
    }

    const 制限後の値 = 新しい最小値 !== null
      ? Math.max(this.現在の数値.現在値, 新しい最小値)
      : this.現在の数値.現在値;

    const 新しい管理 = new 数値管理(
      this.現在の数値.名前,
      制限後の値,
      this.現在の数値.最大値,
      新しい最小値
    );

    新しい管理.変更履歴 = [...this.変更履歴];
    if (制限後の値 !== this.現在の数値.現在値) {
      新しい管理.変更履歴.push({
        変更前: this.現在の数値,
        変更後: 新しい管理.現在の数値,
        変更日時: Date.now()
      });
    }

    return 新しい管理;
  }

  /**
   * 値を増加させる（イミュータブル）
   */
  増加させる(量: number): 数値管理 {
    return this.現在値を変更(this.現在の数値.現在値 + 量);
  }

  /**
   * 値を減少させる（イミュータブル）
   */
  減少させる(量: number): 数値管理 {
    return this.現在値を変更(this.現在の数値.現在値 - 量);
  }

  /**
   * 値を最大値にクリップする（イミュータブル）
   */
  クリップ(): 数値管理 {
    if (this.現在の数値.最大値 === null) {
      return this;
    }

    return this.現在値を変更(
      Math.min(this.現在の数値.現在値, this.現在の数値.最大値)
    );
  }

  /**
   * 値を最小値にクリップする（イミュータブル）
   */
  最小値でクリップ(): 数値管理 {
    if (this.現在の数値.最小値 === null) {
      return this;
    }

    return this.現在値を変更(
      Math.max(this.現在の数値.現在値, this.現在の数値.最小値)
    );
  }

  /**
   * 最大値・最小値の制限を適用
   */
  private 制限を適用(値: number): number {
    let 制限後 = 値;

    if (this.現在の数値.最大値 !== null) {
      制限後 = Math.min(制限後, this.現在の数値.最大値);
    }

    if (this.現在の数値.最小値 !== null) {
      制限後 = Math.max(制限後, this.現在の数値.最小値);
    }

    return 制限後;
  }

  /**
   * 変更履歴を取得
   */
  履歴を取得(): 数値変更記録[] {
    return [...this.変更履歴];
  }

  /**
   * 最新の変更記録を取得
   */
  最新の変更を取得(): 数値変更記録 | undefined {
    if (this.変更履歴.length === 0) {
      return undefined;
    }
    return { ...this.変更履歴[this.変更履歴.length - 1] };
  }

  /**
   * 変更履歴のサイズを取得
   */
  履歴のサイズ(): number {
    return this.変更履歴.length;
  }

  /**
   * 変更履歴が空かを確認
   */
  履歴が空か(): boolean {
    return this.変更履歴.length === 0;
  }

  /**
   * 変更履歴をリセット
   */
  履歴をリセット(): 数値管理 {
    const 新しい管理 = new 数値管理(
      this.現在の数値.名前,
      this.現在の数値.現在値,
      this.現在の数値.最大値,
      this.現在の数値.最小値
    );
    return 新しい管理;
  }
}
