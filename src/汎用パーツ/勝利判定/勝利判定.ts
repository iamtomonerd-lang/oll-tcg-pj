/**
 * 汎用パーツ：勝利判定
 *
 * ゲーム結果の勝敗状態を管理する
 * イミュータブル：すべての操作が新しいインスタンスを返す
 *
 * 責務：勝敗状態を保持・変更・履歴を管理するのみ
 * ゲームルール判定は層2以上で実装
 */

import type { 勝敗状態, 判定履歴 } from './勝利状態';

/**
 * 勝利判定を管理する
 * イミュータブル：すべての操作が新しいインスタンスを返す
 *
 * 層外共有資産として、純粋に勝敗状態を管理する
 */
export class 勝利判定 {
  private 現在の状態: 勝敗状態 = '未決定';
  private 仮状態: 勝敗状態 | null = null;
  private 状態変更履歴: 判定履歴[] = [];

  constructor() {
    // 初期状態：未決定
  }

  /**
   * 現在の状態を取得（確定状態優先）
   */
  現在の結果を取得(): 勝敗状態 {
    if (this.仮状態 !== null) {
      return this.仮状態;
    }
    return this.現在の状態;
  }

  /**
   * 確定済みの状態を取得
   */
  確定済み状態を取得(): 勝敗状態 {
    return this.現在の状態;
  }

  /**
   * 仮結果が設定されているかを確認
   */
  仮結果が設定されているか(): boolean {
    return this.仮状態 !== null;
  }

  /**
   * 仮結果を取得
   */
  仮結果を取得(): 勝敗状態 | null {
    return this.仮状態;
  }

  /**
   * 仮結果を設定（イミュータブル）
   * 確定前の状態を設定
   */
  仮結果を設定(新しい状態: 勝敗状態): 勝利判定 {
    const 新しい判定 = new 勝利判定();
    新しい判定.現在の状態 = this.現在の状態;
    新しい判定.状態変更履歴 = [...this.状態変更履歴];
    新しい判定.仮状態 = 新しい状態;

    return 新しい判定;
  }

  /**
   * 仮結果を変更（イミュータブル）
   * 置換処理などで、仮結果を別の結果に変更
   */
  仮結果を変更(新しい状態: 勝敗状態): 勝利判定 {
    if (this.仮状態 === null) {
      // 仮結果が設定されていない場合は設定
      return this.仮結果を設定(新しい状態);
    }

    if (this.仮状態 === 新しい状態) {
      return this;
    }

    const 新しい判定 = new 勝利判定();
    新しい判定.現在の状態 = this.現在の状態;
    新しい判定.状態変更履歴 = [...this.状態変更履歴];
    新しい判定.仮状態 = 新しい状態;

    return 新しい判定;
  }

  /**
   * 結果を確定（イミュータブル）
   * 仮結果または現在の状態を確定状態にする
   */
  結果を確定(): 勝利判定 {
    const 確定する状態 = this.仮状態 !== null ? this.仮状態 : this.現在の状態;

    if (確定する状態 === this.現在の状態 && this.仮状態 === null) {
      // 変更がない場合は元のインスタンスを返す
      return this;
    }

    const 新しい判定 = new 勝利判定();
    新しい判定.状態変更履歴 = [...this.状態変更履歴];

    // 変更履歴に記録
    if (確定する状態 !== this.現在の状態) {
      新しい判定.状態変更履歴.push({
        変更前: this.現在の状態,
        変更後: 確定する状態,
        時刻: Date.now()
      });
    }

    新しい判定.現在の状態 = 確定する状態;
    新しい判定.仮状態 = null;

    return 新しい判定;
  }

  /**
   * 仮結果をクリア（イミュータブル）
   * 仮結果を取り消して、確定状態に戻す
   */
  仮結果をクリア(): 勝利判定 {
    if (this.仮状態 === null) {
      return this;
    }

    const 新しい判定 = new 勝利判定();
    新しい判定.現在の状態 = this.現在の状態;
    新しい判定.状態変更履歴 = [...this.状態変更履歴];
    新しい判定.仮状態 = null;

    return 新しい判定;
  }

  /**
   * 結果を変更（イミュータブル）
   * 確定済みの結果を変更する
   * （通常は使わないが、置換処理などで必要になることも）
   */
  結果を変更(新しい状態: 勝敗状態): 勝利判定 {
    if (新しい状態 === this.現在の状態) {
      return this;
    }

    const 新しい判定 = new 勝利判定();
    新しい判定.状態変更履歴 = [...this.状態変更履歴];

    新しい判定.状態変更履歴.push({
      変更前: this.現在の状態,
      変更後: 新しい状態,
      時刻: Date.now()
    });

    新しい判定.現在の状態 = 新しい状態;
    新しい判定.仮状態 = null;

    return 新しい判定;
  }

  /**
   * リセット（イミュータブル）
   * 状態を未決定に戻す
   * 履歴は保持
   */
  リセット(): 勝利判定 {
    if (this.現在の状態 === '未決定' && this.仮状態 === null) {
      return this;
    }

    const 新しい判定 = new 勝利判定();
    新しい判定.状態変更履歴 = [...this.状態変更履歴];
    新しい判定.現在の状態 = '未決定';
    新しい判定.仮状態 = null;

    return 新しい判定;
  }

  /**
   * 履歴を取得
   */
  履歴を取得(): 判定履歴[] {
    return [...this.状態変更履歴];
  }

  /**
   * 最新の履歴を取得
   */
  最新履歴を取得(): 判定履歴 | undefined {
    if (this.状態変更履歴.length === 0) {
      return undefined;
    }
    return { ...this.状態変更履歴[this.状態変更履歴.length - 1] };
  }

  /**
   * 履歴のサイズを取得
   */
  履歴のサイズ(): number {
    return this.状態変更履歴.length;
  }

  /**
   * 履歴が空かを確認
   */
  履歴が空か(): boolean {
    return this.状態変更履歴.length === 0;
  }

  /**
   * 履歴をクリア（イミュータブル）
   * 状態は保持するが、履歴をリセット
   */
  履歴をクリア(): 勝利判定 {
    if (this.状態変更履歴.length === 0) {
      return this;
    }

    const 新しい判定 = new 勝利判定();
    新しい判定.現在の状態 = this.現在の状態;
    新しい判定.仮状態 = this.仮状態;

    return 新しい判定;
  }

  /**
   * 完全リセット（イミュータブル）
   * 状態と履歴をすべてリセット
   */
  完全リセット(): 勝利判定 {
    const 新しい判定 = new 勝利判定();
    return 新しい判定;
  }
}
