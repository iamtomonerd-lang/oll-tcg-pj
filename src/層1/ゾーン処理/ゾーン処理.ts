/**
 * 層1：ゾーン処理
 *
 * ゾーンに対して共通で行える基本的な処理を提供する。
 *
 * この層の役割：
 * - ゾーンへのカード/トークンの追加・取り出し
 * - ゾーン間のカード/トークン移動
 * - ゾーン情報の取得
 * - ゾーンの公開情報管理
 *
 * この層が提供しない機能：
 * - 具体的なゾーン種別（手札、場など）
 * - ゾーン容量の制限ルール
 * - ゾーン固有のルール
 * - ゲーム固有のシャッフル処理（層2で定義）
 */

import type { ゾーン } from '層0';
import { 挿入, 取り出す } from '../共通処理';

/**
 * ゾーン内のエンティティ
 *
 * ゾーンに実際に格納されるオブジェクト
 * カードまたはゲームオブジェクト（トークンなど）
 */
export interface ゾーン内エンティティ {
  /** エンティティの一意なID */
  id: string;
  /** エンティティのデータ */
  データ: unknown;
}

/**
 * ゾーンインスタンス
 *
 * ゾーン定義と実際のエンティティを組み合わせたもの
 */
export interface ゾーンインスタンス {
  /** ゾーン定義 */
  定義: ゾーン;
  /** ゾーン内に格納されているエンティティ */
  エンティティ: ゾーン内エンティティ[];
}

/**
 * ゾーンにエンティティを追加
 *
 * @param ゾーン 対象のゾーン
 * @param エンティティ 追加するエンティティ
 * @param 位置 挿入位置（デフォルトは末尾）
 * @returns 更新されたゾーン
 */
export function エンティティを追加(
  ゾーン: ゾーンインスタンス,
  エンティティ: ゾーン内エンティティ,
  位置?: number
): ゾーンインスタンス {
  if (位置 !== undefined) {
    return {
      ...ゾーン,
      エンティティ: 挿入(ゾーン.エンティティ, 位置, エンティティ)
    };
  }

  return {
    ...ゾーン,
    エンティティ: [...ゾーン.エンティティ, エンティティ]
  };
}

/**
 * ゾーンからエンティティを取り出す
 *
 * @param ゾーン 対象のゾーン
 * @param インデックス 取り出す位置
 * @returns [取り出したエンティティ, 更新されたゾーン]
 */
export function エンティティを取り出す(
  ゾーン: ゾーンインスタンス,
  インデックス: number
): [ゾーン内エンティティ | undefined, ゾーンインスタンス] {
  const [エンティティ, 更新後のエンティティ] = 取り出す(ゾーン.エンティティ, インデックス);

  return [
    エンティティ,
    {
      ...ゾーン,
      エンティティ: 更新後のエンティティ
    }
  ];
}

/**
 * ゾーンからエンティティをIDで取り出す
 *
 * @param ゾーン 対象のゾーン
 * @param エンティティID 取り出すエンティティのID
 * @returns [取り出したエンティティ, 更新されたゾーン]
 */
export function エンティティをIDで取り出す(
  ゾーン: ゾーンインスタンス,
  エンティティID: string
): [ゾーン内エンティティ | undefined, ゾーンインスタンス] {
  const インデックス = ゾーン.エンティティ.findIndex((e) => e.id === エンティティID);

  if (インデックス === -1) {
    return [undefined, ゾーン];
  }

  return エンティティを取り出す(ゾーン, インデックス);
}

/**
 * ゾーン内のエンティティ個数を取得
 *
 * @param ゾーン 対象のゾーン
 * @returns ゾーン内のエンティティ個数
 */
export function エンティティ個数(ゾーン: ゾーンインスタンス): number {
  return ゾーン.エンティティ.length;
}

/**
 * ゾーン内にエンティティが存在するか判定
 *
 * @param ゾーン 対象のゾーン
 * @param エンティティID 検索するエンティティのID
 * @returns エンティティが存在するか
 */
export function エンティティが存在するか(
  ゾーン: ゾーンインスタンス,
  エンティティID: string
): boolean {
  return ゾーン.エンティティ.some((e) => e.id === エンティティID);
}

/**
 * ゾーン内のエンティティをIDで検索
 *
 * @param ゾーン 対象のゾーン
 * @param エンティティID 検索するエンティティのID
 * @returns 見つかったエンティティ、なければ undefined
 */
export function エンティティをIDで検索(
  ゾーン: ゾーンインスタンス,
  エンティティID: string
): ゾーン内エンティティ | undefined {
  return ゾーン.エンティティ.find((e) => e.id === エンティティID);
}

/**
 * ゾーン間でエンティティを移動
 *
 * @param 元のゾーン 移動元のゾーン
 * @param 先のゾーン 移動先のゾーン
 * @param エンティティID 移動するエンティティのID
 * @returns [更新された元のゾーン, 更新された先のゾーン]
 */
export function エンティティを移動(
  元のゾーン: ゾーンインスタンス,
  先のゾーン: ゾーンインスタンス,
  エンティティID: string
): [ゾーンインスタンス, ゾーンインスタンス] {
  const [エンティティ, 更新後の元のゾーン] = エンティティをIDで取り出す(元のゾーン, エンティティID);

  if (!エンティティ) {
    return [元のゾーン, 先のゾーン];
  }

  const 更新後の先のゾーン = エンティティを追加(先のゾーン, エンティティ);

  return [更新後の元のゾーン, 更新後の先のゾーン];
}

/**
 * ゾーンが空か判定
 *
 * @param ゾーン 対象のゾーン
 * @returns ゾーンが空か
 */
export function ゾーンが空か(ゾーン: ゾーンインスタンス): boolean {
  return ゾーン.エンティティ.length === 0;
}

/**
 * ゾーンをクリア（すべてのエンティティを削除）
 *
 * @param ゾーン 対象のゾーン
 * @returns クリアされたゾーン
 */
export function ゾーンをクリア(ゾーン: ゾーンインスタンス): ゾーンインスタンス {
  return {
    ...ゾーン,
    エンティティ: []
  };
}
