/**
 * 層1：カード処理
 *
 * カード自身に対する共通的な処理を提供する。
 *
 * この層の役割：
 * - カードの状態の変更
 * - カードの数値情報の変更
 * - カードの分類情報の変更
 * - カードの基本情報の変更
 *
 * この層が提供しない機能：
 * - カード効果の処理
 * - カードの具体的なルール
 * - カードの生成（層3で定義）
 */

import type { カード, カード状態, カード数値情報, カード分類情報 } from '層0';

/**
 * カードの状態名を追加
 *
 * @param カード 対象のカード
 * @param 状態名 追加する状態名
 * @returns 更新されたカード
 */
export function 状態を追加(カード: カード, 状態名: string): カード {
  const 新しい状態: カード状態 = {
    所持数: カード.状態.所持数 + 1,
    内容: [...カード.状態.内容, 状態名]
  };

  return {
    ...カード,
    状態: 新しい状態
  };
}

/**
 * カードの状態名を削除
 *
 * @param カード 対象のカード
 * @param 状態名 削除する状態名
 * @returns 更新されたカード
 */
export function 状態を削除(カード: カード, 状態名: string): カード {
  const 新しい状態: カード状態 = {
    所持数: Math.max(0, カード.状態.所持数 - 1),
    内容: カード.状態.内容.filter((s: string) => s !== 状態名)
  };

  return {
    ...カード,
    状態: 新しい状態
  };
}

/**
 * カードが特定の状態を持つか判定
 *
 * @param カード 対象のカード
 * @param 状態名 確認する状態名
 * @returns カードが状態を持つか
 */
export function 状態を持つか(カード: カード, 状態名: string): boolean {
  return カード.状態.内容.includes(状態名);
}

/**
 * カードの数値情報を取得
 *
 * @param カード 対象のカード
 * @param インデックス 数値のインデックス
 * @returns 指定位置の数値、存在しなければ undefined
 */
export function 数値を取得(カード: カード, インデックス: number): number | undefined {
  return カード.数値情報.内容[インデックス];
}

/**
 * カードの数値情報を変更
 *
 * @param カード 対象のカード
 * @param インデックス 変更する数値のインデックス
 * @param 新しい値 新しい値
 * @returns 更新されたカード
 */
export function 数値を変更(カード: カード, インデックス: number, 新しい値: number): カード {
  if (インデックス < 0 || インデックス >= カード.数値情報.所持数) {
    return カード;
  }

  const 新しい数値情報: カード数値情報 = {
    ...カード.数値情報,
    内容: カード.数値情報.内容.map((値: number, i: number) => (i === インデックス ? 新しい値 : 値))
  };

  return {
    ...カード,
    数値情報: 新しい数値情報
  };
}

/**
 * カードの数値情報を加算
 *
 * @param カード 対象のカード
 * @param インデックス 加算する数値のインデックス
 * @param 加算値 加算する値
 * @returns 更新されたカード
 */
export function 数値を加算(カード: カード, インデックス: number, 加算値: number): カード {
  const 現在の値 = 数値を取得(カード, インデックス);

  if (現在の値 === undefined) {
    return カード;
  }

  return 数値を変更(カード, インデックス, 現在の値 + 加算値);
}

