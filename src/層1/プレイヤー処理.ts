/**
 * 層1：プレイヤー処理
 *
 * プレイヤー自身に対する共通的な処理を提供する。
 *
 * この層の役割：
 * - プレイヤーの状態の変更
 * - プレイヤーの数値情報の変更
 * - プレイヤーの分類情報の変更
 * - プレイヤーの基本情報の変更
 *
 * この層が提供しない機能：
 * - プレイヤーのゾーン管理（ゾーン処理で行う）
 * - プレイヤー固有のルール
 * - プレイヤーの生成（層3で定義）
 */

import type { プレイヤー, プレイヤー状態, プレイヤー数値情報, プレイヤー分類情報 } from '../層0/プレイヤー';

/**
 * プレイヤーの状態名を追加
 *
 * @param プレイヤー 対象のプレイヤー
 * @param 状態名 追加する状態名
 * @returns 更新されたプレイヤー
 */
export function 状態を追加(プレイヤー: プレイヤー, 状態名: string): プレイヤー {
  const 新しい状態: プレイヤー状態 = {
    所持数: プレイヤー.状態.所持数 + 1,
    内容: [...プレイヤー.状態.内容, 状態名]
  };

  return {
    ...プレイヤー,
    状態: 新しい状態
  };
}

/**
 * プレイヤーの状態名を削除
 *
 * @param プレイヤー 対象のプレイヤー
 * @param 状態名 削除する状態名
 * @returns 更新されたプレイヤー
 */
export function 状態を削除(プレイヤー: プレイヤー, 状態名: string): プレイヤー {
  const 新しい状態: プレイヤー状態 = {
    所持数: Math.max(0, プレイヤー.状態.所持数 - 1),
    内容: プレイヤー.状態.内容.filter((s) => s !== 状態名)
  };

  return {
    ...プレイヤー,
    状態: 新しい状態
  };
}

/**
 * プレイヤーが特定の状態を持つか判定
 *
 * @param プレイヤー 対象のプレイヤー
 * @param 状態名 確認する状態名
 * @returns プレイヤーが状態を持つか
 */
export function 状態を持つか(プレイヤー: プレイヤー, 状態名: string): boolean {
  return プレイヤー.状態.内容.includes(状態名);
}

/**
 * プレイヤーの数値情報を取得
 *
 * @param プレイヤー 対象のプレイヤー
 * @param インデックス 数値のインデックス
 * @returns 指定位置の数値、存在しなければ undefined
 */
export function 数値を取得(プレイヤー: プレイヤー, インデックス: number): number | undefined {
  return プレイヤー.数値情報.内容[インデックス];
}

/**
 * プレイヤーの数値情報を変更
 *
 * @param プレイヤー 対象のプレイヤー
 * @param インデックス 変更する数値のインデックス
 * @param 新しい値 新しい値
 * @returns 更新されたプレイヤー
 */
export function 数値を変更(プレイヤー: プレイヤー, インデックス: number, 新しい値: number): プレイヤー {
  if (インデックス < 0 || インデックス >= プレイヤー.数値情報.所持数) {
    return プレイヤー;
  }

  const 新しい数値情報: プレイヤー数値情報 = {
    ...プレイヤー.数値情報,
    内容: プレイヤー.数値情報.内容.map((値, i) => (i === インデックス ? 新しい値 : 値))
  };

  return {
    ...プレイヤー,
    数値情報: 新しい数値情報
  };
}

/**
 * プレイヤーの数値情報を加算
 *
 * @param プレイヤー 対象のプレイヤー
 * @param インデックス 加算する数値のインデックス
 * @param 加算値 加算する値
 * @returns 更新されたプレイヤー
 */
export function 数値を加算(プレイヤー: プレイヤー, インデックス: number, 加算値: number): プレイヤー {
  const 現在の値 = 数値を取得(プレイヤー, インデックス);

  if (現在の値 === undefined) {
    return プレイヤー;
  }

  return 数値を変更(プレイヤー, インデックス, 現在の値 + 加算値);
}

