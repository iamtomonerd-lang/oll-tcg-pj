/**
 * 層1：ゲームオブジェクト処理
 *
 * ゲームオブジェクトに対する共通的な処理を提供する。
 *
 * この層の役割：
 * - ゲームオブジェクトの状態の変更
 * - ゲームオブジェクトの数値情報の変更
 * - ゲームオブジェクトの分類情報の変更
 * - ゲームオブジェクトの基本情報の変更
 *
 * この層が提供しない機能：
 * - ゲームオブジェクト固有のルール
 * - ゲームオブジェクトの生成（層3で定義）
 */

import type {
  ゲームオブジェクト,
  ゲームオブジェクト状態,
  ゲームオブジェクト数値情報,
  ゲームオブジェクト分類情報
} from '../層0/ゲームオブジェクト';

/**
 * ゲームオブジェクトの状態名を追加
 *
 * @param ゲームオブジェクト 対象のゲームオブジェクト
 * @param 状態名 追加する状態名
 * @returns 更新されたゲームオブジェクト
 */
export function 状態を追加(ゲームオブジェクト: ゲームオブジェクト, 状態名: string): ゲームオブジェクト {
  const 新しい状態: ゲームオブジェクト状態 = {
    所持数: ゲームオブジェクト.状態.所持数 + 1,
    内容: [...ゲームオブジェクト.状態.内容, 状態名]
  };

  return {
    ...ゲームオブジェクト,
    状態: 新しい状態
  };
}

/**
 * ゲームオブジェクトの状態名を削除
 *
 * @param ゲームオブジェクト 対象のゲームオブジェクト
 * @param 状態名 削除する状態名
 * @returns 更新されたゲームオブジェクト
 */
export function 状態を削除(ゲームオブジェクト: ゲームオブジェクト, 状態名: string): ゲームオブジェクト {
  const 新しい状態: ゲームオブジェクト状態 = {
    所持数: Math.max(0, ゲームオブジェクト.状態.所持数 - 1),
    内容: ゲームオブジェクト.状態.内容.filter((s) => s !== 状態名)
  };

  return {
    ...ゲームオブジェクト,
    状態: 新しい状態
  };
}

/**
 * ゲームオブジェクトが特定の状態を持つか判定
 *
 * @param ゲームオブジェクト 対象のゲームオブジェクト
 * @param 状態名 確認する状態名
 * @returns ゲームオブジェクトが状態を持つか
 */
export function 状態を持つか(ゲームオブジェクト: ゲームオブジェクト, 状態名: string): boolean {
  return ゲームオブジェクト.状態.内容.includes(状態名);
}

/**
 * ゲームオブジェクトの数値情報を取得
 *
 * @param ゲームオブジェクト 対象のゲームオブジェクト
 * @param インデックス 数値のインデックス
 * @returns 指定位置の数値、存在しなければ undefined
 */
export function 数値を取得(ゲームオブジェクト: ゲームオブジェクト, インデックス: number): number | undefined {
  return ゲームオブジェクト.数値情報.内容[インデックス];
}

/**
 * ゲームオブジェクトの数値情報を変更
 *
 * @param ゲームオブジェクト 対象のゲームオブジェクト
 * @param インデックス 変更する数値のインデックス
 * @param 新しい値 新しい値
 * @returns 更新されたゲームオブジェクト
 */
export function 数値を変更(ゲームオブジェクト: ゲームオブジェクト, インデックス: number, 新しい値: number): ゲームオブジェクト {
  if (インデックス < 0 || インデックス >= ゲームオブジェクト.数値情報.所持数) {
    return ゲームオブジェクト;
  }

  const 新しい数値情報: ゲームオブジェクト数値情報 = {
    ...ゲームオブジェクト.数値情報,
    内容: ゲームオブジェクト.数値情報.内容.map((値, i) => (i === インデックス ? 新しい値 : 値))
  };

  return {
    ...ゲームオブジェクト,
    数値情報: 新しい数値情報
  };
}

/**
 * ゲームオブジェクトの数値情報を加算
 *
 * @param ゲームオブジェクト 対象のゲームオブジェクト
 * @param インデックス 加算する数値のインデックス
 * @param 加算値 加算する値
 * @returns 更新されたゲームオブジェクト
 */
export function 数値を加算(ゲームオブジェクト: ゲームオブジェクト, インデックス: number, 加算値: number): ゲームオブジェクト {
  const 現在の値 = 数値を取得(ゲームオブジェクト, インデックス);

  if (現在の値 === undefined) {
    return ゲームオブジェクト;
  }

  return 数値を変更(ゲームオブジェクト, インデックス, 現在の値 + 加算値);
}

