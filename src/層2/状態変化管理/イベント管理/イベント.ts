/**
 * 層2：イベント管理 - イベントインターフェース
 *
 * 状態変化から生まれた「出来事」を表現する。
 *
 * 設計方針：
 * - 状態変化が「事実」ならイベントは「その解釈」
 * - 層2は単に状態変化を通知するだけ
 * - ゲーム層が通知を受け取って判断する
 */

import type { 状態変化 } from '../状態変化';

/**
 * イベントインターフェース
 *
 * 状態変化から発生した出来事を表現する。
 *
 * 重要：
 * - イベント管理は「何が起きたか」を通知するのみ
 * - 「それが何を意味するか」はゲーム層が判定
 * - 「何をすべきか」はゲーム層が決定
 */
export interface イベント {
  /**
   * イベントの種類
   *
   * 例：
   * - 'ゾーン変更'（状態変化の種類が位置変更）
   * - '数値変更'（状態変化の種類が数値変更）
   * - '状態追加'（状態変化の種類が状態追加）
   *
   * 注意：
   * - '破壊'、'召喚'などのゲーム用語は使わない
   * - 実装時はゲーム層が「ゾーン変更 → 破壊」と解釈
   */
  種類: string;

  /**
   * このイベントを発生させた状態変化
   *
   * 用途：
   * - ゲーム層が「何が変わったか」を確認
   * - イベントリスナーが詳細情報にアクセス
   * - 履歴追跡
   */
  元となる変化: 状態変化;

  /**
   * タイムスタンプ
   *
   * イベント発生時刻（ミリ秒単位）
   */
  発生時刻: number;

  /**
   * イベント固有のメタデータ
   *
   * 型情報を保持しない（unknownを使用）
   * 例：
   * - { 変更差分: 5 }（ダメージ量）
   * - { 移動前ゾーン: 'hand', 移動先ゾーン: 'field' }
   *
   * ゲーム層がどの型かを知っている
   */
  データ?: unknown;
}
