/**
 * 層2：イベント管理 - イベント管理エンジン
 *
 * 状態変化からイベントを生成し、登録されたリスナーに通知する。
 *
 * 設計方針：
 * - 状態変化を受け取る
 * - イベントを生成する
 * - リスナーに通知する
 * - ゲーム層の判定には関与しない
 */

import type { イベント } from './イベント';
import type { 状態変化 } from '../状態変化';

/**
 * イベント通知ハンドラの型
 *
 * イベントが発生したときに呼び出される関数
 *
 * @param イベント - 発生したイベント
 */
type イベントリスナー = (イベント: イベント) => void;

/**
 * イベント管理クラス
 *
 * 状態変化からイベントを生成し、複数のリスナーに通知する。
 *
 * 流れ：
 * 1. 状態管理が状態変化を報告
 * 2. イベント管理がイベントを生成
 * 3. 登録されたリスナーに通知
 * 4. ゲーム層がイベントを解釈・処理
 *
 * 重要：
 * - イベント管理は「何が起きたか」を通知するのみ
 * - 「それが何を意味するか」は判定しない
 * - 「何をすべきか」は決定しない
 */
export class イベント管理 {
  /**
   * イベントリスナーの一覧
   *
   * 複数のリスナーをサポート
   * - UI更新
   * - ゲームロジック
   * - ログ記録
   * など様々な用途に対応
   */
  private リスナー: イベントリスナー[] = [];

  /**
   * 発生したイベントの履歴
   *
   * ゲーム進行の記録・監査用
   */
  private イベント履歴: イベント[] = [];

  /**
   * 状態変化からイベントを生成して通知
   *
   * @param 変化 - 状態変化
   */
  変化から通知(変化: 状態変化): void {
    // イベントを生成
    const イベント = this.イベントを生成(変化);

    // 履歴に記録
    this.イベント履歴.push(イベント);

    // すべてのリスナーに通知
    for (const リスナー of this.リスナー) {
      リスナー(イベント);
    }
  }

  /**
   * 複数の状態変化からイベントを生成して通知
   *
   * @param 変化配列 - 状態変化の配列
   */
  複数変化から通知(変化配列: 状態変化[]): void {
    for (const 変化 of 変化配列) {
      this.変化から通知(変化);
    }
  }

  /**
   * 状態変化からイベントを生成
   *
   * イベント管理の責務：
   * - 状態変化の種類をイベント種類にマッピング
   * - イベントメタデータを構成
   * - タイムスタンプを記録
   *
   * イベント管理の責務でないこと：
   * - 「それが破壊か召喚かを判定」 ← ゲーム層の責務
   * - 「効果を発動すべきか判定」 ← ゲーム層の責務
   * - 「ゲームルールを適用」 ← ゲーム層の責務
   *
   * @param 変化 - 状態変化
   * @returns 生成されたイベント
   */
  private イベントを生成(変化: 状態変化): イベント {
    return {
      種類: 変化.種類,
      元となる変化: 変化,
      発生時刻: Date.now(),
      データ: {
        対象: 変化.対象,
        変更前: 変化.変更前,
        変更後: 変化.変更後
      }
    };
  }

  /**
   * イベントリスナーを登録
   *
   * 登録されたリスナーはイベント発生時に呼び出される。
   *
   * 用途：
   * - UI更新
   * - ゲーム進行の記録
   * - ログ出力
   * - デバッグ
   *
   * @param リスナー - イベント発生時に呼び出される関数
   */
  リスナーを登録(リスナー: イベントリスナー): void {
    this.リスナー.push(リスナー);
  }

  /**
   * イベントリスナーを削除
   *
   * @param リスナー - 削除するリスナー
   */
  リスナーを削除(リスナー: イベントリスナー): void {
    const インデックス = this.リスナー.indexOf(リスナー);
    if (インデックス !== -1) {
      this.リスナー.splice(インデックス, 1);
    }
  }

  /**
   * すべてのリスナーを削除
   */
  リスナーをクリア(): void {
    this.リスナー = [];
  }

  /**
   * 最新のイベントを取得
   *
   * @returns 直近のイベント、存在しない場合はundefined
   */
  最新(): イベント | undefined {
    return this.イベント履歴[this.イベント履歴.length - 1];
  }

  /**
   * 指定インデックスのイベントを取得
   *
   * @param インデックス - 取得対象のインデックス（0から始まる）
   * @returns 指定位置のイベント、範囲外の場合はundefined
   */
  を取得(インデックス: number): イベント | undefined {
    if (インデックス < 0 || インデックス >= this.イベント履歴.length) {
      return undefined;
    }
    return this.イベント履歴[インデックス];
  }

  /**
   * イベント履歴全体を取得
   *
   * @returns イベント履歴の配列（新しい配列を返す）
   */
  履歴を取得(): イベント[] {
    return [...this.イベント履歴];
  }

  /**
   * イベント履歴のサイズを取得
   *
   * @returns 記録されているイベントの数
   */
  サイズ(): number {
    return this.イベント履歴.length;
  }

  /**
   * イベント履歴が空かを確認
   *
   * @returns 履歴が空の場合true
   */
  が空か(): boolean {
    return this.イベント履歴.length === 0;
  }

  /**
   * イベント履歴をリセット
   *
   * 過去のすべてのイベントを削除する。
   * リスナーは保持される。
   */
  リセット(): void {
    this.イベント履歴 = [];
  }

  /**
   * 完全なリセット
   *
   * 履歴とリスナーをすべて削除する。
   * 新規開始時に使用。
   */
  完全リセット(): void {
    this.イベント履歴 = [];
    this.リスナー = [];
  }

  /**
   * デバッグ用：イベント種類の一覧を取得
   *
   * @returns 記録されたイベント種類の配列
   */
  デバッグ情報(): string[] {
    return this.イベント履歴.map(イベント => イベント.種類);
  }
}
