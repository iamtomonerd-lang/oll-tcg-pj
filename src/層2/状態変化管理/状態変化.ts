/**
 * 層2：状態変化管理 - 状態変化インターフェース
 *
 * 状態変更の共通形式を定義する。
 *
 * 設計方針：
 * - 変更の事実のみを記録
 * - カード型・プレイヤー型への直接参照なし
 * - ゲーム固有の処理なし
 * - 「何を変えるか」ではなく「どう変わったか」を記録
 */

/**
 * 状態変化インターフェース
 *
 * TCGの任意の状態変更を記録するジェネリック形式。
 *
 * 例：
 * - HP変更: { 種類: '数値変更', 対象: player, 変更前: 20, 変更後: 15 }
 * - カード移動: { 種類: '位置変更', 対象: card, 変更前: zone1, 変更後: zone2 }
 * - 状態付与: { 種類: '状態追加', 対象: card, 変更前: [], 変更後: ['毒'] }
 *
 * 重要：
 * - 変更の理由は含まない（それはイベント管理の責務）
 * - 何を変えるかを決めない（それはゲーム層の責務）
 * - 単に「何が、どう変わったか」のみを記録
 */
export interface 状態変化 {
  /**
   * 状態変化の種類
   *
   * 例：
   * - '数値変更'（HP、コストなど）
   * - '位置変更'（カード移動）
   * - '状態追加'（毒、封印など）
   * - '状態削除'
   * - '能力変更'
   *
   * 層2では種類のみ記録し、具体的な解釈はゲーム層に委ねる
   */
  種類: string;

  /**
   * 変更対象のオブジェクト
   *
   * 型情報を保持しない（unknownを使用）
   * ゲーム層がどの型かを知っている
   *
   * 用途：
   * - イベントリスナーが「誰の変更か」を判定
   * - 変更履歴の追跡
   * - 通知対象の決定
   */
  対象: unknown;

  /**
   * 変更前の状態
   *
   * 型情報を保持しない（unknownを使用）
   * ゲーム層がどの型かを知っている
   *
   * 用途：
   * - 変更の差分を記録
   * - ロールバック時の参照
   * - 監査ログ
   */
  変更前: unknown;

  /**
   * 変更後の状態
   *
   * 型情報を保持しない（unknownを使用）
   * ゲーム層がどの型かを知っている
   *
   * 用途：
   * - 現在の状態を確認
   * - イベント通知時の情報源
   * - 履歴管理
   */
  変更後: unknown;
}
