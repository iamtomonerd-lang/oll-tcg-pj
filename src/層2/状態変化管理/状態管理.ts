/**
 * 層2：状態変化管理 - 状態管理クラス
 *
 * 状態変化を記録・管理し、必要に応じて通知する。
 *
 * 設計方針：
 * - 状態変化を時系列で記録
 * - 通知機能で変更を外部へ伝える
 * - 通知先が何をするかは知らない
 * - イミュータビリティを保証
 */

import type { 状態変化 } from './状態変化';

/**
 * 状態変化通知ハンドラの型
 *
 * 状態変化が記録されたときに呼び出される関数
 *
 * @param 変化 - 発生した状態変化
 */
type 状態変化リスナー = (変化: 状態変化) => void;

/**
 * 状態管理クラス
 *
 * TCGゲームの状態変化を管理する。
 *
 * 流れ：
 * 1. ゲーム層が状態変化を報告 → 変更()
 * 2. 状態管理が履歴に記録
 * 3. 登録されたリスナーに通知
 * 4. イベント管理などが受け取って処理
 *
 * 重要：
 * - 何を変えるかは決めない
 * - 変更が正当かを判定しない
 * - 単に記録と通知のみを行う
 */
export class 状態管理 {
  /**
   * 状態変化の履歴
   *
   * 登録順に保持（時系列）
   */
  private 履歴: 状態変化[] = [];

  /**
   * 状態変化リスナーの一覧
   *
   * 複数のリスナーをサポート
   * イベント通知、UIの更新、ログ記録など
   * 様々な用途に対応
   */
  private リスナー: 状態変化リスナー[] = [];

  /**
   * 状態変化を記録して通知
   *
   * @param 変化 - 記録する状態変化
   */
  変更(変化: 状態変化): void {
    // 履歴に追加
    this.履歴.push(変化);

    // すべてのリスナーに通知
    for (const リスナー of this.リスナー) {
      リスナー(変化);
    }
  }

  /**
   * 複数の状態変化を順番に記録
   *
   * @param 変化配列 - 記録する状態変化の配列
   */
  複数変更(変化配列: 状態変化[]): void {
    for (const 変化 of 変化配列) {
      this.変更(変化);
    }
  }

  /**
   * 最新の状態変化を取得
   *
   * @returns 直近の状態変化、存在しない場合はundefined
   */
  最新(): 状態変化 | undefined {
    return this.履歴[this.履歴.length - 1];
  }

  /**
   * 指定インデックスの状態変化を取得
   *
   * @param インデックス - 取得対象のインデックス（0から始まる）
   * @returns 指定位置の状態変化、範囲外の場合はundefined
   */
  を取得(インデックス: number): 状態変化 | undefined {
    if (インデックス < 0 || インデックス >= this.履歴.length) {
      return undefined;
    }
    return this.履歴[インデックス];
  }

  /**
   * 履歴全体を取得
   *
   * @returns 状態変化の履歴配列（新しい配列を返す）
   */
  履歴を取得(): 状態変化[] {
    return [...this.履歴];
  }

  /**
   * 履歴のサイズを取得
   *
   * @returns 記録されている状態変化の数
   */
  サイズ(): number {
    return this.履歴.length;
  }

  /**
   * 履歴が空かを確認
   *
   * @returns 履歴が空の場合true
   */
  が空か(): boolean {
    return this.履歴.length === 0;
  }

  /**
   * 状態変化リスナーを登録
   *
   * 登録されたリスナーは、状態変化が発生するたびに呼び出される。
   *
   * 用途：
   * - イベント通知
   * - UI更新
   * - ログ記録
   * - 検証・監査
   *
   * @param リスナー - 状態変化時に呼び出される関数
   */
  リスナーを登録(リスナー: 状態変化リスナー): void {
    this.リスナー.push(リスナー);
  }

  /**
   * 状態変化リスナーを削除
   *
   * @param リスナー - 削除するリスナー
   */
  リスナーを削除(リスナー: 状態変化リスナー): void {
    const インデックス = this.リスナー.indexOf(リスナー);
    if (インデックス !== -1) {
      this.リスナー.splice(インデックス, 1);
    }
  }

  /**
   * すべてのリスナーを削除
   */
  リスナーをクリア(): void {
    this.リスナー = [];
  }

  /**
   * 履歴をリセット
   *
   * 過去のすべての状態変化を削除する。
   * リスナーは保持される。
   */
  リセット(): void {
    this.履歴 = [];
  }

  /**
   * 完全なリセット
   *
   * 履歴とリスナーをすべて削除する。
   * 新規開始時に使用。
   */
  完全リセット(): void {
    this.履歴 = [];
    this.リスナー = [];
  }

  /**
   * デバッグ用：状態変化の種類一覧を取得
   *
   * @returns 記録された状態変化の種類の配列
   */
  デバッグ情報(): string[] {
    return this.履歴.map(変化 => 変化.種類);
  }
}
