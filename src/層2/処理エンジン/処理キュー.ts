/**
 * 層2：処理エンジン - 処理キュー
 *
 * 処理を順番に管理し、実行するためのキュー。
 *
 * 設計方針：
 * - FIFO（先入先出）で処理を実行
 * - 同期・非同期処理の両方に対応
 * - 処理の実行順を保証
 * - 外部に処理結果を返さない（処理内で完結）
 */

import type { 処理 } from './処理';

/**
 * 処理キュー
 *
 * 処理を順番に管理し実行する。
 * ゲームループから使用される基本的なエンジン部品。
 */
export class 処理キュー {
  /**
   * 処理を格納するキュー
   */
  private キュー: 処理[] = [];

  /**
   * キューに処理を追加
   *
   * @param 処理 追加する処理
   */
  追加(処理: 処理): void {
    this.キュー.push(処理);
  }

  /**
   * 次の処理を取得（キューから削除しない）
   *
   * @returns 次の処理、キューが空ならundefined
   */
  次の処理を確認(): 処理 | undefined {
    return this.キュー[0];
  }

  /**
   * キューに格納されている処理数を取得
   *
   * @returns 処理数
   */
  処理数(): number {
    return this.キュー.length;
  }

  /**
   * キューが空か判定
   *
   * @returns 空ならtrue、処理があるならfalse
   */
  が空か(): boolean {
    return this.キュー.length === 0;
  }

  /**
   * すべての処理を順番に実行
   *
   * 同期・非同期の両方の処理に対応し、
   * 登録順に実行される。
   *
   * 処理が例外をスローした場合、その時点で実行を停止する。
   *
   * @throws 処理内で例外が発生した場合
   */
  async 実行(): Promise<void> {
    while (this.キュー.length > 0) {
      // キューの最初の処理を取得して削除
      const 処理 = this.キュー.shift();

      if (処理) {
        // 処理を実行（同期・非同期両対応）
        await Promise.resolve(処理.実行());
      }
    }
  }

  /**
   * キューをクリア（すべての処理を削除）
   */
  クリア(): void {
    this.キュー = [];
  }

  /**
   * キューの状態をデバッグ用に確認
   *
   * @returns キューに格納された処理の種類の配列
   */
  デバッグ情報(): string[] {
    return this.キュー.map(処理 => 処理.種類);
  }
}
