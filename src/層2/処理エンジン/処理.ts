/**
 * 層2：処理エンジン - 処理
 *
 * 処理の共通インターフェースを定義する。
 *
 * 設計方針：
 * - 処理は「何をするか」のみを定義
 * - ゲーム状態やプレイヤー情報は持たない
 * - 実行結果の処理も呼び出し元に委ねる
 */

/**
 * 処理の基本インターフェース
 *
 * すべての処理はこのインターフェースに従う。
 * 層2で扱う処理は単純に「実行される単位」であり、
 * ゲーム固有の判断ロジックは含まない。
 */
export interface 処理 {
  /**
   * 処理の種類を示す識別子
   *
   * 例："カード をプレイ", "ターン 終了", "効果 発動"など
   * ゲーム側で具体的な名前を決める。
   */
  種類: string;

  /**
   * 処理を実行する
   *
   * 同期処理：void を返す
   * 非同期処理：Promise<void> を返す
   *
   * この関数内では：
   * ✅ 必要な処理を実行
   * ✅ 状態を更新
   * ✅ 次のキューイングがあれば呼び出し
   *
   * ❌ 自身の完了判定をしない
   * ❌ キューを制御しない
   * ❌ ゲーム固有のルール判定をしない
   */
  実行(): void | Promise<void>;
}
