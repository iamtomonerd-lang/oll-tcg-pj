/**
 * 層2：効果解決 - 効果インターフェース
 *
 * 効果の基本形式を定義する。
 *
 * 設計方針：
 * - 効果は「解決すると処理になる」という概念
 * - 効果そのものはゲーム内容を知らない
 * - ゲーム側が効果を生成し、層2が解決する
 */

import type { 処理 } from '../処理エンジン/処理';

/**
 * 効果インターフェース
 *
 * 「これが発動するとどんな処理になるか」を定義する。
 *
 * 例：
 * - カードを1枚引く効果
 * - ダメージを与える効果
 * - 状態を付与する効果
 *
 * 重要：
 * 効果そのものは「何が起きるか」を知らない。
 * ゲーム側が効果を生成する際に、処理内容を決める。
 */
export interface 効果 {
  /**
   * 効果の名前・説明
   *
   * 例："カードを引く", "ダメージを与える"
   * ゲーム側で具体的な名前を決める。
   */
  名前: string;

  /**
   * 効果を解決して、処理を生成する
   *
   * この関数内では：
   * ✅ ゲーム側で生成した処理を返す
   * ✅ 複数の処理を返す場合がある
   *
   * ❌ 条件判定をしない
   * ❌ ダメージ計算をしない
   * ❌ 対象選択をしない
   *
   * @returns この効果によって生成される処理の配列
   */
  解決(): 処理[];
}
