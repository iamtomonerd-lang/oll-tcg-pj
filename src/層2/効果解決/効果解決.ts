/**
 * 層2：効果解決 - 効果解決エンジン
 *
 * 効果を受け取り、処理に変換する。
 *
 * 流れ：
 * 効果（ゲーム側で生成）
 *   ↓
 * 効果解決（層2：処理に変換）
 *   ↓
 * 処理キュー（層2：順番管理）
 *   ↓
 * 処理実行（層2：実行）
 */

import type { 処理 } from '../処理エンジン/処理';
import { 処理キュー } from '../処理エンジン/処理キュー';
import type { 効果 } from './効果';

/**
 * 効果解決クラス
 *
 * 効果を受け取り、処理キューに登録する。
 * 複数の効果を順番に解決し、すべての処理を管理する。
 */
export class 効果解決 {
  /**
   * 処理キュー
   * この効果解決から生成されたすべての処理を管理する
   */
  private キュー: 処理キュー;

  /**
   * コンストラクタ
   */
  constructor() {
    this.キュー = new 処理キュー();
  }

  /**
   * 効果を解決して処理キューに追加
   *
   * 処理の流れ：
   * 1. 効果の解決メソッドを呼び出す
   * 2. 返された処理を受け取る
   * 3. 各処理をキューに追加する
   *
   * @param 効果 解決する効果
   */
  解決(効果: 効果): void {
    // 効果から処理を生成
    const 処理配列 = 効果.解決();

    // 生成された処理をキューに追加
    for (const 処理 of 処理配列) {
      this.キュー.追加(処理);
    }
  }

  /**
   * 複数の効果を順番に解決
   *
   * @param 効果配列 解決する効果の配列
   */
  複数解決(効果配列: 効果[]): void {
    for (const 効果 of 効果配列) {
      this.解決(効果);
    }
  }

  /**
   * 処理キューを取得
   *
   * @returns この効果解決に紐付いた処理キュー
   */
  キューを取得(): 処理キュー {
    return this.キュー;
  }

  /**
   * すべての処理を実行
   *
   * 効果から生成されたすべての処理を
   * 登録順に実行する。
   *
   * @throws 処理実行中に例外が発生した場合
   */
  async 実行(): Promise<void> {
    await this.キュー.実行();
  }

  /**
   * キューをリセット
   */
  リセット(): void {
    this.キュー.クリア();
  }

  /**
   * キューの状態を確認（デバッグ用）
   *
   * @returns キューに格納された処理の名前の配列
   */
  デバッグ情報(): string[] {
    return this.キュー.デバッグ情報();
  }

  /**
   * キューに格納されている処理数を取得
   *
   * @returns 処理数
   */
  処理数(): number {
    return this.キュー.処理数();
  }
}
