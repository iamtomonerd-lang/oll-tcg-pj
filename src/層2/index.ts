/**
 * 層2：処理エンジン・効果解決・状態変化管理
 *
 * TCGゲームの共通処理を管理する層。
 *
 * ## 層2のモジュール
 *
 * - **処理エンジン** - 処理を順番に実行
 * - **効果解決** - 効果を処理に変換
 * - **状態変化管理** - 状態変更を安全に管理
 *   - 状態変化：何が変わったかを記録
 *   - 状態管理：履歴を管理
 *   - イベント管理：状態変化からイベントを生成・通知
 *
 * ## 層2の責務
 *
 * - 処理の順番管理
 * - 同期・非同期処理の統一実行
 * - 効果から処理への変換
 * - 効果処理の順番管理
 * - 状態変化の記録と通知
 * - 状態変化からイベントを生成
 *
 * ## 層2が定義しない機能
 *
 * - ゲーム固有のルール判定
 * - カード能力の定義
 * - 条件判定
 * - ダメージ・ドロー・コスト計算
 * - 対象選択
 * - ターン進行ロジック
 * - イベント解釈（「何が起きたか」は判定、「それが何を意味するか」は判定しない）
 * - 優先権処理
 * - カード移動ルール
 * - 破壊・召喚処理
 *
 * これらはすべて層3以降で定義される。
 */

